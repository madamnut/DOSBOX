; ============================================================
; hello.asm
;
; 목적:
;   1) 화면에 "Press any key..." 문자열을 출력한다
;   2) 키보드에서 아무 키나 하나 입력받을 때까지 기다린다
;   3) 키가 눌리면 프로그램을 종료한다
;
; 형식:
;   DOS .COM 프로그램
;
; 빌드:
;   nasm -f bin hello.asm -o hello.com
;
; 실행:
;   DOSBox에서 hello 또는 hello.com
; ============================================================


org 100h
; ------------------------------------------------------------
; .COM 프로그램은 DOS가 메모리의 오프셋 0100h부터
; 프로그램 본문을 올려서 실행하는 것이 기본 전제다.
;
; 그래서 NASM에게:
;   "이 프로그램은 0100h에 로드된다고 가정하고
;    주소 계산을 해라"
; 라고 알려주는 지시어가 org 100h 이다.
;
; 이 줄은 CPU 명령어가 아니라 "어셈블러용 지시어"다.
; 즉 실행 중에 CPU가 처리하는 줄이 아니라,
; NASM이 바이너리 만들 때 참고하는 정보다.
; ------------------------------------------------------------


mov ah, 09h
; ------------------------------------------------------------
; AX 레지스터의 상위 8비트인 AH에 09h를 넣는다.
;
; 여기서 AH는 "어떤 DOS 기능을 쓸지"를 지정하는 역할을 한다.
; 지금 우리가 곧 호출할 것은 int 21h 이고,
; int 21h 는 DOS의 여러 기능을 제공하는 인터럽트 서비스다.
;
; 그중 AH = 09h 는:
;   "문자열을 화면에 출력하라"
; 는 기능 번호다.
;
; 즉 이 한 줄은:
;   "이따가 int 21h를 호출할 건데,
;    그때 문자열 출력 기능을 쓰겠다"
; 라고 예약하는 것이다.
; ------------------------------------------------------------

mov dx, msg
; ------------------------------------------------------------
; DX 레지스터에 msg의 주소(offset)를 넣는다.
;
; msg는 아래쪽에 정의된 문자열 데이터 라벨이다.
; 즉:
;   msg db 'Press any key...$'
; 여기의 시작 위치 주소가 msg다.
;
; DOS의 int 21h / AH=09h 기능은
; 출력할 문자열의 주소를 DS:DX 형태로 받는다.
;
; 지금 .COM 프로그램은 시작 시점에 보통
; CS, DS, ES, SS가 같은 세그먼트를 가리키는 단순한 구조라서,
; 우리가 따로 DS를 만지지 않아도 이 예제에서는 잘 동작한다.
;
; 따라서 이 줄은:
;   "출력할 문자열은 msg 위치에 있다"
; 를 DOS에게 알려주기 위해 DX에 그 주소를 넣는 것이다.
; ------------------------------------------------------------

int 21h
; ------------------------------------------------------------
; DOS 서비스 인터럽트 호출.
;
; 지금 직전에 우리가 준비한 값은:
;   AH = 09h   -> 문자열 출력 기능
;   DX = msg   -> 출력할 문자열 주소
;
; 따라서 이 인터럽트는 DOS에게:
;   "msg 위치에 있는 문자열을 화면에 출력해라"
; 라고 요청한다.
;
; 주의:
; AH=09h 문자열 출력은 일반적인 C 문자열처럼
; 0(null)로 끝나는 문자열이 아니라,
; '$' 문자가 나올 때까지 출력한다.
;
; 그래서 아래 문자열 끝에 '$'를 붙여둔 것이다.
; ------------------------------------------------------------


mov ah, 00h
; ------------------------------------------------------------
; 이번에는 BIOS 키보드 서비스를 준비한다.
;
; 곧 int 16h 를 호출할 건데,
; int 16h 는 BIOS의 키보드 관련 인터럽트다.
;
; 그중 AH = 00h 는:
;   "키 하나를 입력받아라"
;   "키가 눌릴 때까지 기다려라"
; 라는 기능이다.
;
; 즉 이 줄은:
;   "다음 키보드 인터럽트에서는 아무 키나 하나 받을 거다"
; 라고 설정하는 것이다.
; ------------------------------------------------------------

int 16h
; ------------------------------------------------------------
; BIOS 키보드 인터럽트 호출.
;
; AH=00h 상태로 호출했으므로:
;   - 키가 아직 안 눌렸으면 여기서 대기한다
;   - 사용자가 아무 키나 누르면 그 키 정보를 받아온다
;
; 이 호출이 끝나면 보통:
;   AL = ASCII 코드(문자 키인 경우)
;   AH = 스캔 코드
; 같은 식으로 키 정보가 들어온다.
;
; 하지만 지금 예제에서는
; "무슨 키가 눌렸는지"는 아직 중요하지 않다.
; 그냥 "아무 키 하나 눌렸다"는 사실만 필요하므로
; 반환값은 따로 사용하지 않는다.
;
; 즉 현재 목적은:
;   "프로그램이 바로 꺼지지 않게,
;    키 입력을 한 번 기다리게 만든다"
; 이다.
; ------------------------------------------------------------


mov ax, 4c00h
; ------------------------------------------------------------
; DOS 종료 기능을 준비한다.
;
; int 21h에서 AH=4Ch 는:
;   "프로그램 종료"
; 기능이다.
;
; 여기서 AX 전체에 4C00h를 넣었으므로:
;   AH = 4Ch   -> 종료 기능 번호
;   AL = 00h   -> 종료 코드(return code) 0
;
; 즉:
;   "정상 종료 코드 0으로 프로그램을 끝내라"
; 라고 설정한 것이다.
;
; 왜 AH만 안 넣고 AX 전체를 넣었냐면,
; 종료 기능 번호와 종료 코드를 한 번에 넣기 편해서다.
; ------------------------------------------------------------

int 21h
; ------------------------------------------------------------
; 다시 DOS 서비스 인터럽트를 호출한다.
;
; 이번에는 AH=4Ch 상태이므로
; DOS에게 프로그램 종료를 요청한다.
;
; 이 호출이 실행되면 프로그램은 끝나고
; DOS 프롬프트로 돌아간다.
; ------------------------------------------------------------


msg db 'Press any key...$'
; ------------------------------------------------------------
; 문자열 데이터 정의.
;
; msg:
;   이 문자열의 시작 위치를 가리키는 라벨 이름이다.
;
; db:
;   define byte
;   바이트 단위 데이터를 메모리에 배치하라는 뜻이다.
;
; 'Press any key...$'
;   실제 저장되는 문자열 내용.
;
; 끝의 '$'가 매우 중요하다.
; DOS의 int 21h / AH=09h 문자열 출력 기능은
; '$'를 문자열 종료 표시로 사용한다.
;
; 즉 DOS는 이 문자열을 출력하다가
; '$'를 만나면 멈춘다.
;
; 만약 '$'를 안 넣으면
; 뒤쪽 메모리까지 계속 읽어버려서
; 이상한 글자가 나오거나 오동작할 수 있다.
; ------------------------------------------------------------