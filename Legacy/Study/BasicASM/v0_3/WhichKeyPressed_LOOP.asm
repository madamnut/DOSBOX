; ============================================================
; game.asm
;
; 목적:
;   1) 프로그램이 시작되면 안내 문구를 보여준다
;   2) 키 입력을 한 번 받고 끝나는 것이 아니라,
;      계속해서 키 입력을 기다린다
;   3) 방향키를 누르면 어떤 방향인지 출력한다
;   4) ESC를 누르면 루프를 끝내고 프로그램을 종료한다
;
; 이 예제가 중요한 이유:
;   지금부터는 "한 번 실행하고 끝"이 아니라
;   "계속 반복하는 구조"로 바뀐다.
;
;   게임은 기본적으로 거의 항상 이런 구조를 가진다:
;
;     입력 받기
;     상태 갱신
;     화면 다시 그리기
;     반복
;
;   지금은 아직 상태 갱신/화면 다시 그리기가 약하지만,
;   최소한 "입력을 계속 받는 반복 구조"를 먼저 만든다.
;
; 빌드:
;   nasm -f bin game.asm -o game.com
;
; 실행:
;   DOSBox에서 game 또는 game.com
; ============================================================


org 100h
; ------------------------------------------------------------
; .COM 프로그램은 DOS가 보통 메모리의 오프셋 0100h부터 적재한다.
; 따라서 어셈블러에게
;   "이 코드는 100h부터 놓인다고 가정하고 주소 계산해라"
; 라고 알려준다.
;
; 이 줄은 실행 명령이 아니라 어셈블러 지시어다.
; ------------------------------------------------------------


start:
; ------------------------------------------------------------
; 프로그램 시작 지점.
; .COM 프로그램은 보통 여기부터 실행된다고 보면 된다.
; ------------------------------------------------------------


    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 인터럽트 int 21h 의 문자열 출력 기능을 준비한다.
    ;
    ; AH=09h :
    ;   "$"로 끝나는 문자열 출력
    ; --------------------------------------------------------

    mov dx, intro_msg
    ; --------------------------------------------------------
    ; 출력할 문자열의 주소를 DX에 넣는다.
    ;
    ; intro_msg는 아래쪽에 정의된 문자열 데이터의 시작 주소다.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; DOS 서비스를 호출해서 intro_msg를 화면에 출력한다.
    ; --------------------------------------------------------



main_loop:
; ------------------------------------------------------------
; 여기가 이번 예제의 핵심.
;
; "반복 루프"의 시작점이다.
;
; 지금부터 프로그램은:
;   1) 키를 기다린다
;   2) 어떤 키인지 판별한다
;   3) 결과를 출력한다
;   4) ESC가 아니면 다시 여기로 돌아온다
;
; 즉, 이 라벨이 게임의 가장 원시적인 메인 루프 역할을 한다.
; ------------------------------------------------------------


    mov ah, 00h
    ; --------------------------------------------------------
    ; BIOS 키보드 인터럽트 int 16h 기능 준비.
    ;
    ; AH=00h :
    ;   키 하나를 입력받을 때까지 기다린다
    ;
    ; 즉 프로그램이 여기서 멈춰 있다가,
    ; 사용자가 키를 누르면 다음 줄로 진행한다.
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; BIOS 키보드 서비스 호출.
    ;
    ; 입력이 들어오면 일반적으로:
    ;   AL = ASCII 코드(문자 키인 경우)
    ;   AH = 스캔 코드
    ;
    ; ESC 같은 키는 AL로 바로 비교 가능하다.
    ; 방향키 같은 특수키는 AL=00h 이고
    ; 실제 구분은 AH(스캔 코드)를 본다.
    ; --------------------------------------------------------


    cmp al, 1Bh
    ; --------------------------------------------------------
    ; AL이 ESC의 ASCII 코드(1Bh)인지 비교한다.
    ;
    ; 즉:
    ;   "방금 누른 키가 ESC인가?"
    ; 를 검사한다.
    ; --------------------------------------------------------

    je exit_program
    ; --------------------------------------------------------
    ; ESC면 반복 루프를 끝내고 종료 루틴으로 간다.
    ;
    ; 이번 예제에서 ESC는 루프 탈출 키다.
    ; --------------------------------------------------------


    cmp al, 00h
    ; --------------------------------------------------------
    ; AL이 00h인지 확인한다.
    ;
    ; 방향키 같은 특수키는 BIOS 입력에서
    ; AL=00h 로 오는 경우가 많다.
    ;
    ; 따라서:
    ;   AL=00h 이면 특수키일 가능성이 높다
    ;   -> AH 스캔 코드를 봐야 한다
    ; --------------------------------------------------------

    jne key_other
    ; --------------------------------------------------------
    ; AL이 00h가 아니면 방향키 같은 특수키 경로가 아니다.
    ; 그러면 "OTHER KEY" 처리 쪽으로 보낸다.
    ; --------------------------------------------------------


    cmp ah, 48h
    ; --------------------------------------------------------
    ; AH 스캔 코드가 48h인지 비교
    ; 보통 위 방향키(Up Arrow)의 스캔 코드
    ; --------------------------------------------------------

    je key_up
    ; --------------------------------------------------------
    ; 위 방향키면 key_up 으로 점프
    ; --------------------------------------------------------


    cmp ah, 50h
    ; --------------------------------------------------------
    ; AH 스캔 코드가 50h인지 비교
    ; 보통 아래 방향키(Down Arrow)의 스캔 코드
    ; --------------------------------------------------------

    je key_down
    ; --------------------------------------------------------
    ; 아래 방향키면 key_down 으로 점프
    ; --------------------------------------------------------


    cmp ah, 4Bh
    ; --------------------------------------------------------
    ; AH 스캔 코드가 4Bh인지 비교
    ; 보통 왼쪽 방향키(Left Arrow)의 스캔 코드
    ; --------------------------------------------------------

    je key_left
    ; --------------------------------------------------------
    ; 왼쪽 방향키면 key_left 으로 점프
    ; --------------------------------------------------------


    cmp ah, 4Dh
    ; --------------------------------------------------------
    ; AH 스캔 코드가 4Dh인지 비교
    ; 보통 오른쪽 방향키(Right Arrow)의 스캔 코드
    ; --------------------------------------------------------

    je key_right
    ; --------------------------------------------------------
    ; 오른쪽 방향키면 key_right 로 점프
    ; --------------------------------------------------------


    jmp key_other
    ; --------------------------------------------------------
    ; 여기까지 왔다는 것은:
    ;   - ESC도 아니고
    ;   - 방향키 4종도 아니다
    ;
    ; 따라서 나머지는 OTHER KEY로 처리한다.
    ; --------------------------------------------------------



key_up:
; ------------------------------------------------------------
; 위 방향키를 눌렀을 때 들어오는 처리 구간
; ------------------------------------------------------------

    mov ah, 09h
    ; 문자열 출력 기능 준비

    mov dx, up_msg
    ; "UP" 메시지 주소 준비

    int 21h
    ; 화면에 "UP" 출력

    jmp main_loop
    ; --------------------------------------------------------
    ; 여기서 이번 예제의 핵심 포인트.
    ;
    ; 예전에는 출력 후 program_exit 으로 갔는데,
    ; 지금은 종료하지 않고 main_loop 로 돌아간다.
    ;
    ; 즉:
    ;   입력 -> 판별 -> 출력 -> 다시 입력 대기
    ; 구조가 된다.
    ; --------------------------------------------------------



key_down:
; ------------------------------------------------------------
; 아래 방향키를 눌렀을 때 들어오는 처리 구간
; ------------------------------------------------------------

    mov ah, 09h
    ; 문자열 출력 기능 준비

    mov dx, down_msg
    ; "DOWN" 메시지 주소 준비

    int 21h
    ; 화면에 "DOWN" 출력

    jmp main_loop
    ; 다시 입력 대기 루프로 복귀



key_left:
; ------------------------------------------------------------
; 왼쪽 방향키를 눌렀을 때 들어오는 처리 구간
; ------------------------------------------------------------

    mov ah, 09h
    ; 문자열 출력 기능 준비

    mov dx, left_msg
    ; "LEFT" 메시지 주소 준비

    int 21h
    ; 화면에 "LEFT" 출력

    jmp main_loop
    ; 다시 입력 대기 루프로 복귀



key_right:
; ------------------------------------------------------------
; 오른쪽 방향키를 눌렀을 때 들어오는 처리 구간
; ------------------------------------------------------------

    mov ah, 09h
    ; 문자열 출력 기능 준비

    mov dx, right_msg
    ; "RIGHT" 메시지 주소 준비

    int 21h
    ; 화면에 "RIGHT" 출력

    jmp main_loop
    ; 다시 입력 대기 루프로 복귀



key_other:
; ------------------------------------------------------------
; ESC도 아니고, 방향키도 아닌 키를 눌렀을 때의 처리 구간
; ------------------------------------------------------------

    mov ah, 09h
    ; 문자열 출력 기능 준비

    mov dx, other_msg
    ; "OTHER KEY" 메시지 주소 준비

    int 21h
    ; 화면에 "OTHER KEY" 출력

    jmp main_loop
    ; 이것도 다시 루프로 돌아가서 계속 입력을 받는다



exit_program:
; ------------------------------------------------------------
; ESC를 눌렀을 때 들어오는 종료 처리 구간
; ------------------------------------------------------------

    mov ah, 09h
    ; 먼저 종료 메시지를 한번 보여주기 위해
    ; 문자열 출력 기능 준비

    mov dx, exit_msg
    ; 종료 메시지 주소 준비

    int 21h
    ; "EXIT" 메시지 출력

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 프로그램 종료 기능 준비
    ;
    ; AH=4Ch : 종료
    ; AL=00h : 종료 코드 0
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; DOS 종료 서비스 호출
    ; 프로그램이 끝나고 DOS 프롬프트로 돌아간다.
    ; --------------------------------------------------------



intro_msg db 'Press arrow keys. Press ESC to quit.$'
; ------------------------------------------------------------
; 프로그램 시작 시 한 번만 출력되는 안내 문구
;
; 끝의 '$'는 DOS int 21h / AH=09h 문자열 출력 종료 문자다.
; ------------------------------------------------------------

up_msg db 13, 10, 'UP$'
; ------------------------------------------------------------
; 줄바꿈 후 "UP" 출력
;
; 13 = CR (Carriage Return)
; 10 = LF (Line Feed)
; ------------------------------------------------------------

down_msg db 13, 10, 'DOWN$'
; ------------------------------------------------------------
; 줄바꿈 후 "DOWN" 출력
; ------------------------------------------------------------

left_msg db 13, 10, 'LEFT$'
; ------------------------------------------------------------
; 줄바꿈 후 "LEFT" 출력
; ------------------------------------------------------------

right_msg db 13, 10, 'RIGHT$'
; ------------------------------------------------------------
; 줄바꿈 후 "RIGHT" 출력
; ------------------------------------------------------------

other_msg db 13, 10, 'OTHER KEY$'
; ------------------------------------------------------------
; 줄바꿈 후 "OTHER KEY" 출력
; ------------------------------------------------------------

exit_msg db 13, 10, 'EXIT$'
; ------------------------------------------------------------
; ESC를 눌렀을 때 마지막으로 출력할 종료 메시지
; ------------------------------------------------------------