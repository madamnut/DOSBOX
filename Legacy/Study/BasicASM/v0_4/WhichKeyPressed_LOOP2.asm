; ============================================================
; game.asm
;
; 목적:
;   1) 텍스트 화면을 지운다
;   2) 안내 문구와 현재 상태 문자열을 출력한다
;   3) 키 입력을 계속 받는다
;   4) 방향키를 누르면 상태 문자열을 바꾼다
;   5) 화면을 다시 지우고 새 상태를 다시 출력한다
;   6) ESC를 누르면 종료한다
;
; 이번 예제의 핵심:
;   이전 버전은 키를 누를 때마다 아래로 메시지가 계속 쌓였다.
;   이번 버전은 "현재 상태"만 화면에 보여주도록 바꾼다.
;
;   즉 흐름이 조금 더 게임스럽다:
;
;     입력 받기
;     상태 변경
;     화면 다시 그리기
;     반복
;
; 형식:
;   DOS .COM 프로그램
;
; 빌드:
;   nasm -f bin game.asm -o game.com
;
; 실행:
;   DOSBox에서 game 또는 game.com
; ============================================================


org 100h
; ------------------------------------------------------------
; .COM 프로그램은 DOS가 보통 메모리 오프셋 0100h부터 적재한다.
; 따라서 어셈블러에게 주소 계산 기준을 100h로 잡으라고 알려준다.
; ------------------------------------------------------------


start:
; ------------------------------------------------------------
; 프로그램 시작 지점.
; 시작하자마자 현재 상태를 초기값으로 설정하고
; 첫 화면을 그린다.
; ------------------------------------------------------------

    mov word [current_msg_ptr], state_wait
    ; --------------------------------------------------------
    ; current_msg_ptr 변수에 state_wait 문자열의 주소를 넣는다.
    ;
    ; current_msg_ptr는 "지금 화면에 어떤 상태 문자열을 보여줄지"
    ; 가리키는 포인터 변수다.
    ;
    ; 처음 시작할 때는 아직 아무 방향키도 누르지 않았으므로
    ; "WAITING FOR KEY" 문자열을 가리키게 만든다.
    ; --------------------------------------------------------

    call draw_screen
    ; --------------------------------------------------------
    ; 현재 화면 전체를 그리는 서브루틴 호출.
    ;
    ; 이 시점에서는:
    ;   - 화면 지우기
    ;   - 제목 출력
    ;   - 안내 문구 출력
    ;   - 상태 문자열 출력
    ; 을 한 번에 수행한다.
    ; --------------------------------------------------------



main_loop:
; ------------------------------------------------------------
; 반복 루프의 시작점.
;
; 여기서 계속:
;   1) 키 입력 받기
;   2) 어떤 키인지 판별
;   3) 상태 문자열 포인터 변경
;   4) 화면 다시 그림
;   5) 다시 루프
; 의 흐름으로 돈다.
; ------------------------------------------------------------

    mov ah, 00h
    ; --------------------------------------------------------
    ; BIOS 키보드 인터럽트 int 16h 의 기능 번호 준비.
    ;
    ; AH=00h :
    ;   키 하나를 입력받을 때까지 기다린다.
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키를 하나 입력받는다.
    ;
    ; 보통 결과:
    ;   AL = ASCII 코드
    ;   AH = 스캔 코드
    ;
    ; ESC 같은 키는 AL로 비교 가능하고,
    ; 방향키 같은 특수키는 AL=00h 이고 AH를 본다.
    ; --------------------------------------------------------


    cmp al, 1Bh
    ; --------------------------------------------------------
    ; ESC 키인지 확인.
    ; 1Bh는 ESC의 ASCII 코드다.
    ; --------------------------------------------------------

    je exit_program
    ; --------------------------------------------------------
    ; ESC면 종료 루틴으로 이동.
    ; --------------------------------------------------------


    cmp al, 00h
    ; --------------------------------------------------------
    ; 방향키 같은 특수키는 AL=00h 로 오는 경우가 많다.
    ; 따라서 AL이 00h인지 검사한다.
    ; --------------------------------------------------------

    jne set_other
    ; --------------------------------------------------------
    ; AL이 00h가 아니면 방향키 계열이 아니라고 보고
    ; OTHER 상태로 처리한다.
    ; --------------------------------------------------------


    cmp ah, 48h
    ; --------------------------------------------------------
    ; AH=48h 이면 위 방향키(Up Arrow) 스캔 코드
    ; --------------------------------------------------------

    je set_up

    cmp ah, 50h
    ; --------------------------------------------------------
    ; AH=50h 이면 아래 방향키(Down Arrow) 스캔 코드
    ; --------------------------------------------------------

    je set_down

    cmp ah, 4Bh
    ; --------------------------------------------------------
    ; AH=4Bh 이면 왼쪽 방향키(Left Arrow) 스캔 코드
    ; --------------------------------------------------------

    je set_left

    cmp ah, 4Dh
    ; --------------------------------------------------------
    ; AH=4Dh 이면 오른쪽 방향키(Right Arrow) 스캔 코드
    ; --------------------------------------------------------

    je set_right

    jmp set_other
    ; --------------------------------------------------------
    ; 여기까지 어떤 방향키에도 안 걸렸으면
    ; 그냥 OTHER 상태로 처리.
    ; --------------------------------------------------------



set_up:
; ------------------------------------------------------------
; 상태 문자열을 "UP"으로 바꾼다.
; ------------------------------------------------------------

    mov word [current_msg_ptr], state_up
    ; --------------------------------------------------------
    ; current_msg_ptr가 이제 state_up 문자열을 가리키게 한다.
    ; --------------------------------------------------------

    jmp redraw_and_continue
    ; --------------------------------------------------------
    ; 상태 바꿨으니 화면 다시 그리는 공통 구간으로 이동
    ; --------------------------------------------------------


set_down:
; ------------------------------------------------------------
; 상태 문자열을 "DOWN"으로 바꾼다.
; ------------------------------------------------------------

    mov word [current_msg_ptr], state_down
    jmp redraw_and_continue


set_left:
; ------------------------------------------------------------
; 상태 문자열을 "LEFT"로 바꾼다.
; ------------------------------------------------------------

    mov word [current_msg_ptr], state_left
    jmp redraw_and_continue


set_right:
; ------------------------------------------------------------
; 상태 문자열을 "RIGHT"로 바꾼다.
; ------------------------------------------------------------

    mov word [current_msg_ptr], state_right
    jmp redraw_and_continue


set_other:
; ------------------------------------------------------------
; 방향키/ESC가 아닌 키는 OTHER 상태로 바꾼다.
; ------------------------------------------------------------

    mov word [current_msg_ptr], state_other
    ; --------------------------------------------------------
    ; 상태 문자열 포인터를 OTHER로 변경
    ; --------------------------------------------------------



redraw_and_continue:
; ------------------------------------------------------------
; 상태가 바뀐 뒤에 공통으로 오는 구간.
; 화면을 다시 그리고 루프로 돌아간다.
; ------------------------------------------------------------

    call draw_screen
    ; --------------------------------------------------------
    ; 화면 전체 다시 그림
    ; --------------------------------------------------------

    jmp main_loop
    ; --------------------------------------------------------
    ; 다시 입력 대기 루프로 복귀
    ; --------------------------------------------------------



exit_program:
; ------------------------------------------------------------
; ESC를 눌렀을 때 들어오는 종료 처리 구간.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AX=0003h 는 보통 80x25 텍스트 모드를 설정한다.
    ;
    ; 사실 현재도 텍스트 모드이긴 하지만,
    ; 종료 직전에 화면을 정돈하는 의미로 다시 설정한다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 서비스 호출
    ; 텍스트 화면 모드로 맞춤
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 문자열 출력 기능 준비
    ; --------------------------------------------------------

    mov dx, exit_msg
    ; --------------------------------------------------------
    ; 종료 메시지 주소 준비
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; "EXIT" 출력
    ; --------------------------------------------------------

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 프로그램 종료 준비
    ;
    ; AH=4Ch : 종료
    ; AL=00h : 종료 코드 0
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 프로그램 종료
    ; --------------------------------------------------------



draw_screen:
; ------------------------------------------------------------
; 화면 전체를 다시 그리는 서브루틴
;
; 이 루틴이 하는 일:
;   1) 텍스트 화면을 초기 상태로 만든다
;   2) 제목 출력
;   3) 사용 안내 출력
;   4) 현재 상태 출력
;
; draw_screen을 호출할 때마다
; 이전에 쌓인 출력이 없어지고,
; "현재 상태만 깔끔하게 보이는 화면"이 만들어진다.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AX=0003h 는 80x25 컬러 텍스트 모드를 설정한다.
    ;
    ; 이걸 다시 호출하면 화면이 사실상 초기화되면서
    ; 텍스트 화면이 깔끔해진다.
    ;
    ; 지금은 별도의 커서 위치 제어나 부분 지우기 대신
    ; 가장 단순하게 "모드 재설정 = 화면 초기화" 방식으로 간다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 서비스 호출
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 문자열 출력 기능 준비
    ; --------------------------------------------------------

    mov dx, title_msg
    ; --------------------------------------------------------
    ; 제목 문자열 주소 준비
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 제목 출력
    ; --------------------------------------------------------

    mov ah, 09h
    mov dx, help_msg
    int 21h
    ; --------------------------------------------------------
    ; 사용 안내 문구 출력
    ; --------------------------------------------------------

    mov ah, 09h
    mov dx, label_msg
    int 21h
    ; --------------------------------------------------------
    ; "CURRENT STATE: " 라벨 출력
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; 상태 문자열 출력 기능 준비
    ; --------------------------------------------------------

    mov dx, [current_msg_ptr]
    ; --------------------------------------------------------
    ; current_msg_ptr 변수 안에 들어있는 문자열 주소를 DX로 가져온다.
    ;
    ; 즉 현재 상태가 UP이면 state_up 주소가 들어 있고,
    ; RIGHT면 state_right 주소가 들어 있다.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 현재 상태 문자열 출력
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; draw_screen 서브루틴 종료
    ; 호출한 곳으로 복귀
    ; --------------------------------------------------------



current_msg_ptr dw 0
; ------------------------------------------------------------
; 현재 상태 문자열의 주소를 저장하는 변수.
;
; dw = define word
; 16비트 값 하나를 저장할 공간을 만든다.
;
; 지금은 .COM 프로그램이고 문자열 주소(오프셋)를
; 16비트 값으로 저장하면 충분하다.
; ------------------------------------------------------------

title_msg db 'DOS INPUT TEST', 13, 10, '$'
; ------------------------------------------------------------
; 화면 상단 제목
; 13,10은 줄바꿈
; 끝의 '$'는 DOS 문자열 출력 종료 표시
; ------------------------------------------------------------

help_msg db 'ARROW KEYS = CHANGE STATE / ESC = QUIT', 13, 10, 13, 10, '$'
; ------------------------------------------------------------
; 사용 안내 문구
; 두 번 줄바꿈해서 아래와 간격을 조금 띄운다.
; ------------------------------------------------------------

label_msg db 'CURRENT STATE: $'
; ------------------------------------------------------------
; 현재 상태 문자열 앞에 붙는 라벨
; ------------------------------------------------------------

state_wait db 'WAITING FOR KEY$'
; ------------------------------------------------------------
; 프로그램 시작 직후 기본 상태 문자열
; ------------------------------------------------------------

state_up db 'UP$'
; ------------------------------------------------------------
; 위 방향키 상태 문자열
; ------------------------------------------------------------

state_down db 'DOWN$'
; ------------------------------------------------------------
; 아래 방향키 상태 문자열
; ------------------------------------------------------------

state_left db 'LEFT$'
; ------------------------------------------------------------
; 왼쪽 방향키 상태 문자열
; ------------------------------------------------------------

state_right db 'RIGHT$'
; ------------------------------------------------------------
; 오른쪽 방향키 상태 문자열
; ------------------------------------------------------------

state_other db 'OTHER KEY$'
; ------------------------------------------------------------
; 그 외 키 상태 문자열
; ------------------------------------------------------------

exit_msg db 'EXIT$'
; ------------------------------------------------------------
; 종료 시 출력할 메시지
; ------------------------------------------------------------