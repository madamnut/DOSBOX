; ============================================================
; game.asm
;
; 목적:
;   1) VGA Mode 13h (320x200, 256색) 그래픽 모드로 들어간다
;   2) 화면 전체를 한 가지 색으로 채운다
;   3) 키 입력을 계속 기다린다
;   4) ESC 키를 누르면 텍스트 모드로 복귀하고 종료한다
;
; 이번 단계의 핵심:
;   이제부터는 텍스트 모드가 아니라
;   그래픽 모드에서 "화면 메모리"를 직접 다룬다.
;
;   Mode 13h 에서는:
;     - 해상도: 320 x 200
;     - 색상 수: 256
;     - 픽셀 1개 = 1바이트
;
;   총 픽셀 수:
;     320 * 200 = 64000
;
;   따라서 VGA 메모리 A000:0000 부터 시작하는
;   64000바이트를 채우면 화면 전체 색을 한 번에 바꿀 수 있다.
;
; 이번 버전에서는:
;   - 화면을 색 하나로 칠한 뒤
;   - ESC만 종료 키로 인정한다
;   - 다른 키는 무시한다
; ============================================================


org 100h
; ------------------------------------------------------------
; .COM 프로그램은 DOS가 보통 메모리 오프셋 0100h부터 적재한다.
; 따라서 어셈블러에게 이 프로그램의 기준 주소를 100h로 잡으라고 알려준다.
; ------------------------------------------------------------


start:
; ------------------------------------------------------------
; 프로그램 시작 지점
; ------------------------------------------------------------

    mov ax, 0013h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AX=0013h 는 VGA 그래픽 모드 13h 설정을 뜻한다.
    ;
    ; 즉:
    ;   320x200
    ;   256색
    ; 그래픽 모드로 전환하라는 의미다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 서비스 호출.
    ; 실제로 화면이 텍스트 모드에서 그래픽 모드 13h로 바뀐다.
    ; --------------------------------------------------------


    mov ax, 0A000h
    ; --------------------------------------------------------
    ; VGA Mode 13h의 프레임버퍼(화면 메모리) 세그먼트는
    ; 일반적으로 A000h 이다.
    ;
    ; 즉 화면 메모리를 건드리려면
    ; A000:0000 영역으로 접근해야 한다.
    ; --------------------------------------------------------

    mov es, ax
    ; --------------------------------------------------------
    ; ES 레지스터에 A000h를 넣는다.
    ;
    ; 이제 ES:DI 형태로 접근하면
    ; VGA 비디오 메모리에 쓸 수 있다.
    ; --------------------------------------------------------

    xor di, di
    ; --------------------------------------------------------
    ; DI를 0으로 만든다.
    ;
    ; 즉 쓰기 시작 위치를
    ;   A000:0000
    ; 으로 맞춘다.
    ; --------------------------------------------------------

    mov cx, 32000
    ; --------------------------------------------------------
    ; 화면 전체 픽셀 수는 64000개이고,
    ; 픽셀 하나가 1바이트이므로 총 64000바이트다.
    ;
    ; 아래에서 stosw를 사용할 것이므로
    ; 한 번에 2바이트씩 쓴다.
    ;
    ; 따라서 64000 / 2 = 32000 번 반복하면
    ; 화면 전체를 다 채울 수 있다.
    ; --------------------------------------------------------

    mov ax, 1F1Fh
    ; --------------------------------------------------------
    ; AX에 화면에 쓸 색 번호를 두 번 넣는다.
    ;
    ; 여기서 1Fh는 색 인덱스 값이다.
    ; AX=1F1Fh 로 두면:
    ;   상위 바이트도 1Fh
    ;   하위 바이트도 1Fh
    ;
    ; 즉 stosw 한 번 할 때
    ; 동일한 색 번호 2개를 연속으로 쓰게 된다.
    ; --------------------------------------------------------

    rep stosw
    ; --------------------------------------------------------
    ; stosw:
    ;   AX 값을 ES:DI에 기록하고 DI를 2 증가시킨다.
    ;
    ; rep:
    ;   CX가 0이 될 때까지 반복한다.
    ;
    ; 즉 이 한 줄로:
    ;   화면 메모리 전체 64000바이트를
    ;   색 번호 1Fh로 꽉 채운다.
    ;
    ; 결과적으로 화면 전체가 한 색으로 덮인다.
    ; --------------------------------------------------------



wait_for_esc:
; ------------------------------------------------------------
; ESC를 기다리는 반복 루프.
;
; 이번 버전에서는:
;   - ESC를 누르면 종료
;   - 다른 키는 무시하고 계속 대기
; ------------------------------------------------------------

    mov ah, 00h
    ; --------------------------------------------------------
    ; BIOS 키보드 인터럽트 int 16h 기능 준비.
    ;
    ; AH=00h:
    ;   키 하나를 입력받을 때까지 기다린다.
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키 하나를 입력받는다.
    ;
    ; 입력 결과는 보통:
    ;   AL = ASCII 코드
    ;   AH = 스캔 코드
    ;
    ; 이번에는 ESC만 판단하면 되므로
    ; AL만 보면 충분하다.
    ; --------------------------------------------------------

    cmp al, 1Bh
    ; --------------------------------------------------------
    ; ESC 키의 ASCII 코드 1Bh 와 비교한다.
    ;
    ; 즉:
    ;   "방금 누른 키가 ESC인가?"
    ; 를 검사한다.
    ; --------------------------------------------------------

    jne wait_for_esc
    ; --------------------------------------------------------
    ; jne = Jump if Not Equal
    ;
    ; ESC가 아니면 다시 wait_for_esc 라벨로 돌아가서
    ; 계속 키 입력을 기다린다.
    ;
    ; 즉:
    ;   ESC 외의 모든 키는 그냥 무시
    ; --------------------------------------------------------



exit_program:
; ------------------------------------------------------------
; ESC를 눌렀을 때 여기로 와서 종료 처리한다.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AX=0003h 는 80x25 텍스트 모드 설정이다.
    ;
    ; 그래픽 모드에서 끝내면 DOS 화면이 깨져 보일 수 있으므로,
    ; 종료 전에 텍스트 모드로 되돌린다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 서비스 호출.
    ; 화면을 다시 텍스트 모드로 복귀시킨다.
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 문자열 출력 기능 준비.
    ;
    ; AH=09h:
    ;   '$'로 끝나는 문자열 출력
    ; --------------------------------------------------------

    mov dx, exit_msg
    ; --------------------------------------------------------
    ; 종료 메시지 문자열 주소를 DX에 넣는다.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 텍스트 모드로 돌아온 뒤
    ; "EXIT" 메시지를 출력한다.
    ; --------------------------------------------------------

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 프로그램 종료 기능 준비.
    ;
    ; AH=4Ch : 종료 기능
    ; AL=00h : 종료 코드 0
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; DOS 종료 서비스 호출.
    ; 프로그램이 끝나고 DOS 프롬프트로 돌아간다.
    ; --------------------------------------------------------



exit_msg db 'EXIT$'
; ------------------------------------------------------------
; 종료 시 출력할 문자열.
;
; DOS int 21h / AH=09h 문자열 출력은
; '$'를 만날 때까지 출력하므로
; 끝에 반드시 '$'를 붙인다.
; ------------------------------------------------------------