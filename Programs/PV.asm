; ============================================================
; PrintAsciiz
;
; 목적:
;   0(null) 종료 문자열을 한 글자씩 출력
;
; 입력:
;   DS:DX = ASCIIZ 문자열
; ============================================================

PrintAsciiz:
; ------------------------------------------------------------
; null 종료 문자열 출력
; ------------------------------------------------------------

    push ax
    push dx
    push si
    ; --------------------------------------------------------
    ; 레지스터 보존
    ; --------------------------------------------------------

    mov si, dx
    ; --------------------------------------------------------
    ; SI로 문자열 순회
    ; --------------------------------------------------------

.next_char:
    lodsb
    ; --------------------------------------------------------
    ; 다음 문자 읽기
    ; --------------------------------------------------------

    test al, al
    ; --------------------------------------------------------
    ; 0이면 종료
    ; --------------------------------------------------------

    jz .done

    mov dl, al
    ; --------------------------------------------------------
    ; DOS 문자 출력용으로 DL에 복사
    ; --------------------------------------------------------

    mov ah, 02h
    ; --------------------------------------------------------
    ; DOS 문자 1개 출력
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 출력
    ; --------------------------------------------------------

    jmp .next_char
    ; --------------------------------------------------------
    ; 다음 문자
    ; --------------------------------------------------------

.done:
    pop si
    pop dx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    ret



