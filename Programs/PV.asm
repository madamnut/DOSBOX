; ============================================================
; ApplyPalette
;
; 목적:
;   PaletteBuffer(768 bytes)를 VGA DAC에 적용한다
;
; VGA 포트:
;   03C8h = 시작 색 인덱스
;   03C9h = RGB 데이터 입력
;
; 주의:
;   파이썬에서 저장한 팔레트는 보통 0~255 범위이다
;   VGA DAC는 0~63 범위를 쓰므로 4로 나눠야 한다
; ============================================================

ApplyPalette:
; ------------------------------------------------------------
; VGA DAC에 팔레트 적용
; ------------------------------------------------------------

    push ax
    push cx
    push dx
    push si
    ; --------------------------------------------------------
    ; 레지스터 보존
    ; --------------------------------------------------------

    mov dx, 03C8h
    ; --------------------------------------------------------
    ; 시작 색 인덱스 포트
    ; --------------------------------------------------------

    xor al, al
    ; --------------------------------------------------------
    ; 0번 색부터 쓰기 시작
    ; --------------------------------------------------------

    out dx, al
    ; --------------------------------------------------------
    ; 시작 색 = 0
    ; --------------------------------------------------------

    mov dx, 03C9h
    ; --------------------------------------------------------
    ; VGA DAC 데이터 포트
    ; --------------------------------------------------------

    lea si, [PaletteBuffer]
    ; --------------------------------------------------------
    ; 팔레트 버퍼 시작 주소
    ; --------------------------------------------------------

    mov cx, 768
    ; --------------------------------------------------------
    ; 총 768바이트 출력
    ; --------------------------------------------------------


.next_byte:
    lodsb
    ; --------------------------------------------------------
    ; 다음 팔레트 바이트를 AL로 읽는다
    ; --------------------------------------------------------

    shr al, 1
    shr al, 1
    ; --------------------------------------------------------
    ; 0~255 -> 0~63
    ; --------------------------------------------------------

    out dx, al
    ; --------------------------------------------------------
    ; VGA DAC로 출력
    ; --------------------------------------------------------

    loop .next_byte
    ; --------------------------------------------------------
    ; 768바이트 모두 처리할 때까지 반복
    ; --------------------------------------------------------

    pop si
    pop dx
    pop cx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    ret



; ============================================================
; DrawPaletteGrid
;
; 목적:
;   256색 인덱스를 16x16 격자로 화면에 그린다
;
; 배치:
;   - 총 16행
;   - 총 16열
;   - 각 칸 크기:
;       가로 20픽셀
;       세로 10픽셀
;
; 계산:
;   색 인덱스 = 행 * 16 + 열
;   x = 열 * 20
;   y = 행 * 10
;
; 구현 방식:
;   - ES = A000h 로 맞춘다
;   - 각 행마다 시작 오프셋 = y * 320
;   - 각 칸마다 해당 색 인덱스를 20바이트 반복 저장
;   - 한 칸 높이 10줄을 채운다
; ============================================================

DrawPaletteGrid:
; ------------------------------------------------------------
; 256색 격자 전체를 그림
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push bp
    push es
    ; --------------------------------------------------------
    ; 레지스터/세그먼트 보존
    ; --------------------------------------------------------

    mov ax, 0A000h
    ; --------------------------------------------------------
    ; VGA VRAM 세그먼트
    ; --------------------------------------------------------

    mov es, ax
    ; --------------------------------------------------------
    ; ES = A000h
    ; --------------------------------------------------------

    xor dh, dh
    ; --------------------------------------------------------
    ; DH를 행 번호(0~15)로 사용
    ; 처음은 0행
    ; --------------------------------------------------------


.row_loop:
; ------------------------------------------------------------
; 한 행(16칸)을 처리하는 루프
; ------------------------------------------------------------

    cmp dh, 16
    ; --------------------------------------------------------
    ; 16행 모두 끝났는지 확인
    ; --------------------------------------------------------

    jae .done
    ; --------------------------------------------------------
    ; 16행 완료 시 종료
    ; --------------------------------------------------------

    xor dl, dl
    ; --------------------------------------------------------
    ; DL을 열 번호(0~15)로 사용
    ; 각 새 행 시작 시 0열부터 시작
    ; --------------------------------------------------------


.col_loop:
; ------------------------------------------------------------
; 현재 행에서 16개 칸을 순서대로 그림
; ------------------------------------------------------------

    cmp dl, 16
    ; --------------------------------------------------------
    ; 현재 행에서 16열 모두 끝났는지 확인
    ; --------------------------------------------------------

    jae .next_row
    ; --------------------------------------------------------
    ; 한 행 완료 -> 다음 행으로
    ; --------------------------------------------------------

    mov al, dh
    ; --------------------------------------------------------
    ; AL = 현재 행 번호
    ; --------------------------------------------------------

    shl al, 1
    shl al, 1
    shl al, 1
    shl al, 1
    ; --------------------------------------------------------
    ; AL = 행 * 16
    ; 즉 현재 행의 첫 색 인덱스
    ; --------------------------------------------------------

    add al, dl
    ; --------------------------------------------------------
    ; AL = 행*16 + 열
    ; 즉 현재 칸의 색 인덱스
    ; --------------------------------------------------------

    call DrawCell
    ; --------------------------------------------------------
    ; AL에 들어있는 색 인덱스로
    ; (DH,DL) 위치의 칸 하나를 그린다
    ; --------------------------------------------------------

    inc dl
    ; --------------------------------------------------------
    ; 다음 열로 이동
    ; --------------------------------------------------------

    jmp .col_loop
    ; --------------------------------------------------------
    ; 계속 같은 행 처리
    ; --------------------------------------------------------


.next_row:
; ------------------------------------------------------------
; 다음 행으로 이동
; ------------------------------------------------------------

    inc dh
    ; --------------------------------------------------------
    ; 행 번호 증가
    ; --------------------------------------------------------

    jmp .row_loop
    ; --------------------------------------------------------
    ; 다음 행 처리
    ; --------------------------------------------------------


.done:
; ------------------------------------------------------------
; 전체 256칸 그리기 완료
; ------------------------------------------------------------

    pop es
    pop bp
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터/세그먼트 복구
    ; --------------------------------------------------------

    ret



; ============================================================
; DrawCell
;
; 목적:
;   현재 (행,열)에 해당하는 색 칸 하나를 그린다
;
; 입력:
;   AL = 색 인덱스
;   DH = 행 번호 (0~15)
;   DL = 열 번호 (0~15)
;
; 출력:
;   없음
;
; 칸 크기:
;   폭  = 20
;   높이 = 10
;
; 계산:
;   x = 열 * 20
;   y = 행 * 10
;   line_offset = y * 320
;   start_offset = line_offset + x
;
; 구현:
;   10줄 반복:
;     시작 주소에 색 인덱스를 20바이트 채움
;     다음 줄로 가려면 +320
; ============================================================

DrawCell:
; ------------------------------------------------------------
; 색 칸 하나를 그림
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push di
    push bp
    ; --------------------------------------------------------
    ; 내부 사용 레지스터 보존
    ; --------------------------------------------------------

    mov bl, al
    ; --------------------------------------------------------
    ; 색 인덱스를 BL에 백업
    ; 아래 계산 중 AL을 다시 쓸 수 있으므로 보관
    ; --------------------------------------------------------

    xor ax, ax
    ; --------------------------------------------------------
    ; AX 초기화
    ; --------------------------------------------------------

    mov al, dh
    ; --------------------------------------------------------
    ; AL = 행 번호
    ; --------------------------------------------------------

    shl ax, 1
    ; --------------------------------------------------------
    ; AX = 행 * 2
    ; 워드 테이블 인덱싱을 위해 2를 곱한다
    ; --------------------------------------------------------

    mov si, ax
    ; --------------------------------------------------------
    ; SI = CellYTable 인덱스 오프셋
    ; --------------------------------------------------------

    mov ax, [CellYTable + si]
    ; --------------------------------------------------------
    ; AX = y 좌표
    ; 즉 행 * 10 값
    ; --------------------------------------------------------

    mov bp, ax
    ; --------------------------------------------------------
    ; BP = y
    ; --------------------------------------------------------

    mov ax, bp
    ; --------------------------------------------------------
    ; AX = y
    ; --------------------------------------------------------

    shl ax, 6
    ; --------------------------------------------------------
    ; AX = y * 64
    ; --------------------------------------------------------

    mov di, ax
    ; --------------------------------------------------------
    ; DI = y * 64
    ; --------------------------------------------------------

    mov ax, bp
    ; --------------------------------------------------------
    ; AX = y
    ; --------------------------------------------------------

    shl ax, 8
    ; --------------------------------------------------------
    ; AX = y * 256
    ; --------------------------------------------------------

    add di, ax
    ; --------------------------------------------------------
    ; DI = y*64 + y*256 = y*320
    ; 즉 현재 칸의 첫 줄 시작 오프셋
    ; --------------------------------------------------------

    xor ax, ax
    ; --------------------------------------------------------
    ; AX 초기화
    ; --------------------------------------------------------

    mov al, dl
    ; --------------------------------------------------------
    ; AL = 열 번호
    ; --------------------------------------------------------

    mov bp, ax
    ; --------------------------------------------------------
    ; BP = 열 번호
    ; --------------------------------------------------------

    shl ax, 4
    ; --------------------------------------------------------
    ; AX = 열 * 16
    ; --------------------------------------------------------

    shl bp, 2
    ; --------------------------------------------------------
    ; BP = 열 * 4
    ; --------------------------------------------------------

    add ax, bp
    ; --------------------------------------------------------
    ; AX = 열*16 + 열*4 = 열*20
    ; 즉 x 좌표
    ; --------------------------------------------------------

    add di, ax
    ; --------------------------------------------------------
    ; DI = y*320 + x
    ; 현재 칸의 첫 번째 줄 시작 오프셋 완성
    ; --------------------------------------------------------

    mov bh, 10
    ; --------------------------------------------------------
    ; 칸 높이 = 10줄
    ; BH를 남은 줄 수 카운터로 사용
    ; --------------------------------------------------------


.line_loop:
; ------------------------------------------------------------
; 한 줄씩 20픽셀 칠하는 루프
; ------------------------------------------------------------

    mov al, bl
    ; --------------------------------------------------------
    ; 색 인덱스를 AL에 복원
    ; --------------------------------------------------------

    mov cx, 20
    ; --------------------------------------------------------
    ; 한 줄 폭 = 20픽셀
    ; --------------------------------------------------------

    rep stosb
    ; --------------------------------------------------------
    ; ES:DI 위치에 AL 값을 20번 저장
    ; 즉 현재 줄을 같은 색으로 20픽셀 채운다
    ;
    ; 주의:
    ; rep stosb 이후 DI는 20만큼 증가한 상태가 된다
    ; --------------------------------------------------------

    add di, 300
    ; --------------------------------------------------------
    ; 방금 20픽셀을 채웠으므로
    ; 현재 DI는 줄 끝 다음 위치에 있다
    ;
    ; 화면 한 줄은 320바이트이므로
    ; 다음 줄의 같은 x 위치로 가려면
    ; 320 - 20 = 300 을 더하면 된다
    ; --------------------------------------------------------

    dec bh
    ; --------------------------------------------------------
    ; 남은 줄 수 1 감소
    ; --------------------------------------------------------

    jnz .line_loop
    ; --------------------------------------------------------
    ; 10줄 모두 칠할 때까지 반복
    ; --------------------------------------------------------

    pop bp
    pop di
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    ret



