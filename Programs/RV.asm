; ============================================================
; PrintAsciiz
;
; 목적:
;   0(null) 종료 문자열을 한 글자씩 출력한다
;
; 입력:
;   DS:DX = ASCIIZ 문자열
; ============================================================

PrintAsciiz:
; ------------------------------------------------------------
; null 종료 문자열 출력
; ------------------------------------------------------------

    push ax
    push dx
    push si
    ; --------------------------------------------------------
    ; 레지스터 보존
    ; --------------------------------------------------------

    mov si, dx
    ; --------------------------------------------------------
    ; SI로 문자열 순회
    ; --------------------------------------------------------

.next_char:
    lodsb
    ; --------------------------------------------------------
    ; 다음 문자 읽기
    ; --------------------------------------------------------

    test al, al
    ; --------------------------------------------------------
    ; 0이면 끝
    ; --------------------------------------------------------

    jz .done
    ; --------------------------------------------------------
    ; 종료
    ; --------------------------------------------------------

    mov dl, al
    ; --------------------------------------------------------
    ; 문자 출력용으로 DL에 복사
    ; --------------------------------------------------------

    mov ah, 02h
    ; --------------------------------------------------------
    ; DOS 문자 1개 출력 기능
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 문자 출력
    ; --------------------------------------------------------

    jmp .next_char
    ; --------------------------------------------------------
    ; 다음 문자 처리
    ; --------------------------------------------------------

.done:
    pop si
    pop dx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    ret



