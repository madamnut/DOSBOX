; ============================================================
; rawview.asm
;
; 목적:
;   1) DOS에서 명령행 인자 하나를 받는다
;   2) 예: RAWVIEW HELLO
;   3) 그러면 현재 디렉터리에서
;        HELLO.RAW
;        HELLO.PAL
;      두 파일을 자동으로 찾는다
;   4) HELLO.PAL 을 읽어서 VGA 팔레트에 적용한다
;   5) HELLO.RAW 를 읽어서 Mode 13h 화면에 표시한다
;   6) ESC를 누르면 텍스트 모드로 복귀하고 종료한다
;
; 사용법:
;   RAWVIEW HELLO
;
; 전제:
;   - 인자는 확장자 없는 base 이름만 받는다
;   - base 이름은 최대 8글자까지 허용한다 (DOS 8.3 기준)
;   - 현재 디렉터리에 HELLO.RAW 와 HELLO.PAL 이 있어야 한다
;   - HELLO.RAW 는 정확히 64000바이트여야 한다
;   - HELLO.PAL 은 정확히 768바이트여야 한다
;
; 예:
;   RAWVIEW HELLO
;   -> HELLO.RAW
;   -> HELLO.PAL
;
; 핵심 흐름:
;   1) PSP 명령행 인자 영역에서 첫 번째 토큰을 파싱한다
;   2) 그 문자열을 BaseNameBuffer 에 저장한다
;   3) BaseNameBuffer + ".RAW" -> RawFileName 생성
;   4) BaseNameBuffer + ".PAL" -> PalFileName 생성
;   5) PalFileName 읽기
;   6) Mode 13h 진입
;   7) 팔레트 적용
;   8) RawFileName 읽어서 VRAM에 출력
;   9) ESC 대기 후 종료
; ============================================================

org 100h
; ------------------------------------------------------------
; .COM 프로그램 시작 오프셋은 0100h
; ------------------------------------------------------------

jmp start
; ------------------------------------------------------------
; 데이터 영역을 건너뛰고 실행 시작 지점으로 이동
; ------------------------------------------------------------


; ============================================================
; 데이터 영역
; ============================================================

MsgUsage db 13,10,'Usage: RAWVIEW NAME',13,10
         db 'Example: RAWVIEW HELLO',13,10,'$'
; ------------------------------------------------------------
; 사용법 안내 메시지
; ------------------------------------------------------------

MsgNameTooLong db 13,10,'Error: name must be 1..8 characters.$'
; ------------------------------------------------------------
; 인자 길이가 0이거나 8자를 초과했을 때 출력할 메시지
; ------------------------------------------------------------

MsgPalError db 13,10,'Failed to load PAL file: $'
; ------------------------------------------------------------
; PAL 파일 로딩 실패 메시지 앞부분
; 뒤에 실제 파일명을 이어서 출력하기 위해 앞부분만 둔다.
; ------------------------------------------------------------

MsgRawError db 13,10,'Failed to load RAW file: $'
; ------------------------------------------------------------
; RAW 파일 로딩 실패 메시지 앞부분
; ------------------------------------------------------------

MsgBadPalSize db 13,10,'Error: PAL file must be exactly 768 bytes.$'
; ------------------------------------------------------------
; PAL 파일 크기 이상 메시지
; ------------------------------------------------------------

MsgBadRawSize db 13,10,'Error: RAW file must be exactly 64000 bytes.$'
; ------------------------------------------------------------
; RAW 파일 크기 이상 메시지
; ------------------------------------------------------------

MsgTooManyArgs db 13,10,'Error: only one name argument is allowed.$'
; ------------------------------------------------------------
; 인자를 2개 이상 넣었을 때의 메시지
; 예: RAWVIEW HELLO TEST
; ------------------------------------------------------------

BaseNameBuffer db 9 dup(0)
; ------------------------------------------------------------
; 사용자가 입력한 base 이름 저장 버퍼
; 최대 8글자 + 끝의 0(null) = 9바이트
; ------------------------------------------------------------

RawFileName db 13 dup(0)
; ------------------------------------------------------------
; 생성될 RAW 파일명 저장 버퍼
; 최대 "12345678.RAW",0
; 즉 8 + 1 + 3 + 1 = 13바이트
; ------------------------------------------------------------

PalFileName db 13 dup(0)
; ------------------------------------------------------------
; 생성될 PAL 파일명 저장 버퍼
; 최대 "12345678.PAL",0
; ------------------------------------------------------------

PaletteBuffer db 768 dup(0)
; ------------------------------------------------------------
; PAL 파일 768바이트를 읽어둘 버퍼
; ------------------------------------------------------------

ReadBuffer db 4096 dup(0)
; ------------------------------------------------------------
; RAW 파일을 조금씩 읽어둘 임시 버퍼
; ------------------------------------------------------------

FileHandle dw 0
; ------------------------------------------------------------
; DOS 파일 핸들 저장용
; ------------------------------------------------------------

BytesLeft dw 0
; ------------------------------------------------------------
; RAW 파일에서 아직 읽어야 할 남은 바이트 수
; ------------------------------------------------------------

ChunkSize dw 0
; ------------------------------------------------------------
; 이번 루프에서 실제로 읽을 크기
; ------------------------------------------------------------

LastReadSize dw 0
; ------------------------------------------------------------
; LoadExactFile 내부에서 실제 읽은 바이트 수를 보관
; 파일 크기 이상 여부를 호출자 쪽에서 구분하는 데 사용
; ------------------------------------------------------------



; ============================================================
; 프로그램 시작
; ============================================================

start:
; ------------------------------------------------------------
; 프로그램 시작 지점
; ------------------------------------------------------------

    push cs
    pop ds
    ; --------------------------------------------------------
    ; DS를 프로그램 세그먼트로 맞춘다
    ; --------------------------------------------------------

    push cs
    pop es
    ; --------------------------------------------------------
    ; ES도 프로그램 세그먼트로 맞춘다
    ; --------------------------------------------------------


    call ParseCommandName
    ; --------------------------------------------------------
    ; 명령행 첫 번째 인자를 파싱해서 BaseNameBuffer 에 저장한다
    ;
    ; 성공:
    ;   CF=0
    ;
    ; 실패:
    ;   CF=1
    ; --------------------------------------------------------

    jc show_usage
    ; --------------------------------------------------------
    ; 인자가 없거나 형식이 잘못되면 사용법 출력
    ; --------------------------------------------------------


    call BuildFileNames
    ; --------------------------------------------------------
    ; BaseNameBuffer 내용을 바탕으로
    ; RawFileName = "<base>.RAW"
    ; PalFileName = "<base>.PAL"
    ; 을 만든다.
    ; --------------------------------------------------------


    lea dx, [PalFileName]
    ; --------------------------------------------------------
    ; PAL 파일명 주소를 DX에 넣는다
    ; --------------------------------------------------------

    lea bx, [PaletteBuffer]
    ; --------------------------------------------------------
    ; PAL 파일 내용을 읽어둘 버퍼 주소를 BX에 넣는다
    ; --------------------------------------------------------

    mov cx, 768
    ; --------------------------------------------------------
    ; PAL 파일은 정확히 768바이트여야 한다
    ; --------------------------------------------------------

    call LoadExactFile
    ; --------------------------------------------------------
    ; PAL 파일을 정확히 768바이트 읽으려고 시도한다
    ; --------------------------------------------------------

    jc pal_error
    ; --------------------------------------------------------
    ; 실패 시 pal_error 처리
    ; --------------------------------------------------------

    cmp word [LastReadSize], 768
    ; --------------------------------------------------------
    ; 실제로 읽은 바이트 수가 768인지 확인
    ; --------------------------------------------------------

    jne pal_size_error
    ; --------------------------------------------------------
    ; 768이 아니면 크기 이상
    ; --------------------------------------------------------


    mov ax, 0013h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h
    ; VGA Mode 13h 진입
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 그래픽 모드 진입
    ; --------------------------------------------------------


    call ApplyPalette
    ; --------------------------------------------------------
    ; PaletteBuffer 내용을 VGA DAC에 적용
    ; --------------------------------------------------------


    call LoadImageToVRAM
    ; --------------------------------------------------------
    ; RawFileName 파일을 열고 64000바이트를 읽어서
    ; A000:0000 화면 메모리에 복사
    ; --------------------------------------------------------

    jc raw_error_graphics
    ; --------------------------------------------------------
    ; 실패 시 그래픽 모드 에러 처리
    ; --------------------------------------------------------



main_loop:
; ------------------------------------------------------------
; ESC 키를 누를 때까지 대기
; ------------------------------------------------------------

    mov ah, 01h
    ; --------------------------------------------------------
    ; BIOS 키보드 확인 기능
    ; 키가 없으면 ZF=1
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키보드 상태 확인
    ; --------------------------------------------------------

    jz main_loop
    ; --------------------------------------------------------
    ; 아직 입력된 키가 없으면 계속 대기
    ; --------------------------------------------------------

    mov ah, 00h
    ; --------------------------------------------------------
    ; 실제 키 하나를 읽는 기능
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키 입력
    ; ASCII 코드는 AL
    ; --------------------------------------------------------

    cmp al, 27
    ; --------------------------------------------------------
    ; ESC = 27 = 1Bh
    ; --------------------------------------------------------

    jne main_loop
    ; --------------------------------------------------------
    ; ESC가 아니면 다시 대기
    ; --------------------------------------------------------



exit_to_text:
; ------------------------------------------------------------
; 정상 종료
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; 텍스트 모드 80x25 로 복귀
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 실제 텍스트 모드 복귀
    ; --------------------------------------------------------

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 정상 종료
    ; AH=4Ch
    ; AL=00h
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 프로그램 종료
    ; --------------------------------------------------------



show_usage:
; ------------------------------------------------------------
; 사용법 출력 후 종료
; ------------------------------------------------------------

    mov dx, MsgUsage
    call PrintString

    mov ax, 4C01h
    int 21h



pal_error:
; ------------------------------------------------------------
; PAL 파일 로딩 실패
; ------------------------------------------------------------

    mov dx, MsgPalError
    call PrintString
    ; --------------------------------------------------------
    ; "Failed to load PAL file: " 출력
    ; --------------------------------------------------------

    mov dx, PalFileName
    call PrintAsciiz
    ; --------------------------------------------------------
    ; 실제 생성된 파일명 출력
    ; --------------------------------------------------------

    call PrintCRLF
    ; --------------------------------------------------------
    ; 줄바꿈
    ; --------------------------------------------------------

    mov ax, 4C02h
    int 21h



pal_size_error:
; ------------------------------------------------------------
; PAL 파일 크기 이상
; ------------------------------------------------------------

    mov dx, MsgBadPalSize
    call PrintString

    mov ax, 4C03h
    int 21h



raw_error_graphics:
; ------------------------------------------------------------
; 그래픽 모드 상태에서 RAW 파일 에러 발생
; 먼저 텍스트 모드로 복귀해야 메시지가 정상적으로 보인다
; ------------------------------------------------------------

    mov ax, 0003h
    int 10h
    ; --------------------------------------------------------
    ; 텍스트 모드 복귀
    ; --------------------------------------------------------


raw_error:
; ------------------------------------------------------------
; RAW 파일 로딩 실패
; ------------------------------------------------------------

    mov dx, MsgRawError
    call PrintString
    ; --------------------------------------------------------
    ; "Failed to load RAW file: " 출력
    ; --------------------------------------------------------

    mov dx, RawFileName
    call PrintAsciiz
    ; --------------------------------------------------------
    ; 실제 생성된 파일명 출력
    ; --------------------------------------------------------

    call PrintCRLF
    ; --------------------------------------------------------
    ; 줄바꿈
    ; --------------------------------------------------------

    mov ax, 4C04h
    int 21h



raw_size_error_graphics:
; ------------------------------------------------------------
; 그래픽 모드 상태에서 RAW 파일 크기 이상
; ------------------------------------------------------------

    mov ax, 0003h
    int 10h
    ; --------------------------------------------------------
    ; 텍스트 모드 복귀
    ; --------------------------------------------------------


raw_size_error:
; ------------------------------------------------------------
; RAW 파일 크기 이상 메시지 출력
; ------------------------------------------------------------

    mov dx, MsgBadRawSize
    call PrintString

    mov ax, 4C05h
    int 21h



; ============================================================
; ParseCommandName
;
; 목적:
;   PSP의 명령행 인자 영역에서 첫 번째 인자를 읽는다
;
; 입력 예:
;   RAWVIEW HELLO
;
; 결과:
;   BaseNameBuffer = "HELLO",0
;
; 실패 조건:
;   - 인자가 없음
;   - 이름 길이가 8자를 초과함
;   - 두 번째 인자가 추가로 존재함
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;
; PSP 명령행 구조:
;   offset 80h : 명령행 길이(바이트 수)
;   offset 81h : 실제 문자들
;   마지막엔 보통 CR(0Dh)이 온다
; ============================================================

ParseCommandName:
; ------------------------------------------------------------
; 명령행 인자에서 첫 번째 토큰 하나만 읽는다
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push es
    ; --------------------------------------------------------
    ; 내부 사용 레지스터 보존
    ; --------------------------------------------------------

    mov ax, ds
    ; --------------------------------------------------------
    ; 현재 프로그램 세그먼트 값을 AX에 보관
    ; --------------------------------------------------------

    mov es, ax
    ; --------------------------------------------------------
    ; ES를 프로그램 세그먼트로 맞춘다
    ; BaseNameBuffer는 ES:DI로 접근할 것이다
    ; --------------------------------------------------------

    mov si, 81h
    ; --------------------------------------------------------
    ; PSP의 명령행 텍스트 시작 위치
    ; --------------------------------------------------------

    lea di, [BaseNameBuffer]
    ; --------------------------------------------------------
    ; 파싱 결과 저장 목적지
    ; --------------------------------------------------------

    xor bx, bx
    ; --------------------------------------------------------
    ; BX를 문자 개수 카운터로 사용
    ; 처음엔 0
    ; --------------------------------------------------------


.skip_spaces:
; ------------------------------------------------------------
; 앞쪽 공백 건너뛰기
; ------------------------------------------------------------

    mov al, [si]
    ; --------------------------------------------------------
    ; 현재 명령행 문자 읽기
    ; --------------------------------------------------------

    cmp al, 13
    ; --------------------------------------------------------
    ; 곧바로 CR이면 인자가 없는 것
    ; --------------------------------------------------------

    je .fail
    ; --------------------------------------------------------
    ; 실패
    ; --------------------------------------------------------

    cmp al, ' '
    ; --------------------------------------------------------
    ; 공백인지 확인
    ; --------------------------------------------------------

    jne .read_name
    ; --------------------------------------------------------
    ; 공백이 아니면 이름 읽기 단계로 이동
    ; --------------------------------------------------------

    inc si
    ; --------------------------------------------------------
    ; 공백이면 다음 문자로 이동
    ; --------------------------------------------------------

    jmp .skip_spaces
    ; --------------------------------------------------------
    ; 계속 공백 스킵
    ; --------------------------------------------------------


.read_name:
; ------------------------------------------------------------
; 첫 번째 인자 읽기
; ------------------------------------------------------------

    mov al, [si]
    ; --------------------------------------------------------
    ; 현재 문자 읽기
    ; --------------------------------------------------------

    cmp al, ' '
    ; --------------------------------------------------------
    ; 공백이면 이름 끝
    ; --------------------------------------------------------

    je .end_name
    ; --------------------------------------------------------
    ; 이름 읽기 종료
    ; --------------------------------------------------------

    cmp al, 13
    ; --------------------------------------------------------
    ; CR이면 이름 끝
    ; --------------------------------------------------------

    je .end_name
    ; --------------------------------------------------------
    ; 이름 읽기 종료
    ; --------------------------------------------------------

    cmp bx, 8
    ; --------------------------------------------------------
    ; 이미 8글자를 채웠는지 확인
    ; --------------------------------------------------------

    jae .fail
    ; --------------------------------------------------------
    ; 9글자 이상이면 실패
    ; --------------------------------------------------------

    mov [di], al
    ; --------------------------------------------------------
    ; 이름 문자 1개 저장
    ; --------------------------------------------------------

    inc di
    ; --------------------------------------------------------
    ; 저장 위치 다음 칸으로 이동
    ; --------------------------------------------------------

    inc si
    ; --------------------------------------------------------
    ; 명령행 입력 위치도 다음 칸으로 이동
    ; --------------------------------------------------------

    inc bx
    ; --------------------------------------------------------
    ; 문자 수 증가
    ; --------------------------------------------------------

    jmp .read_name
    ; --------------------------------------------------------
    ; 계속 이름 읽기
    ; --------------------------------------------------------


.end_name:
; ------------------------------------------------------------
; 첫 번째 이름 읽기 완료
; ------------------------------------------------------------

    cmp bx, 0
    ; --------------------------------------------------------
    ; 이름 길이가 0이면 실패
    ; --------------------------------------------------------

    je .fail
    ; --------------------------------------------------------
    ; 실패
    ; --------------------------------------------------------

    mov byte [di], 0
    ; --------------------------------------------------------
    ; BaseNameBuffer 끝에 null 추가
    ; --------------------------------------------------------


.skip_tail_spaces:
; ------------------------------------------------------------
; 첫 번째 인자 뒤 공백 건너뛰기
; 추가 인자가 있는지 검사하기 위한 단계
; ------------------------------------------------------------

    mov al, [si]
    ; --------------------------------------------------------
    ; 현재 문자 읽기
    ; --------------------------------------------------------

    cmp al, 13
    ; --------------------------------------------------------
    ; CR이면 첫 번째 인자만 존재한 것
    ; 성공
    ; --------------------------------------------------------

    je .success
    ; --------------------------------------------------------
    ; 성공 처리
    ; --------------------------------------------------------

    cmp al, ' '
    ; --------------------------------------------------------
    ; 공백이면 계속 건너뜀
    ; --------------------------------------------------------

    jne .too_many_args
    ; --------------------------------------------------------
    ; 공백도 아니고 CR도 아니면
    ; 두 번째 인자가 있다는 뜻
    ; --------------------------------------------------------

    inc si
    ; --------------------------------------------------------
    ; 다음 문자로 이동
    ; --------------------------------------------------------

    jmp .skip_tail_spaces
    ; --------------------------------------------------------
    ; 계속 확인
    ; --------------------------------------------------------


.too_many_args:
; ------------------------------------------------------------
; 두 번째 인자가 발견된 경우
; ------------------------------------------------------------

    mov dx, MsgTooManyArgs
    call PrintString
    ; --------------------------------------------------------
    ; "인자는 하나만 허용" 메시지 출력
    ; --------------------------------------------------------

    stc
    ; --------------------------------------------------------
    ; 실패 표시
    ; --------------------------------------------------------

    jmp .done
    ; --------------------------------------------------------
    ; 정리 후 복귀
    ; --------------------------------------------------------


.success:
; ------------------------------------------------------------
; 정상 파싱 성공
; ------------------------------------------------------------

    clc
    ; --------------------------------------------------------
    ; 성공 표시
    ; --------------------------------------------------------

    jmp .done
    ; --------------------------------------------------------
    ; 정리 후 복귀
    ; --------------------------------------------------------


.fail:
; ------------------------------------------------------------
; 공통 실패 경로
; ------------------------------------------------------------

    stc
    ; --------------------------------------------------------
    ; 실패 표시
    ; --------------------------------------------------------


.done:
; ------------------------------------------------------------
; 레지스터 복구 후 복귀
; ------------------------------------------------------------

    pop es
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ret



; ============================================================
; BuildFileNames
;
; 목적:
;   BaseNameBuffer 내용을 이용해
;     RawFileName = "<base>.RAW"
;     PalFileName = "<base>.PAL"
;   을 만든다
;
; 예:
;   BaseNameBuffer = "HELLO",0
;
; 결과:
;   RawFileName = "HELLO.RAW",0
;   PalFileName = "HELLO.PAL",0
; ============================================================

BuildFileNames:
; ------------------------------------------------------------
; base 이름에 확장자를 붙여 실제 파일명을 만든다
; ------------------------------------------------------------

    push ax
    push si
    push di
    ; --------------------------------------------------------
    ; 내부 사용 레지스터 보존
    ; --------------------------------------------------------


    lea si, [BaseNameBuffer]
    lea di, [RawFileName]
    call CopyAsciiz
    ; --------------------------------------------------------
    ; BaseNameBuffer 내용을 RawFileName으로 복사
    ; 복사 후 DI는 문자열 끝의 null 위치를 가리킨다
    ; --------------------------------------------------------

    dec di
    ; --------------------------------------------------------
    ; 방금 복사한 null 위치를 한 칸 뒤로 물린다
    ; 즉 문자열 끝 위치로 되돌린다
    ; --------------------------------------------------------

    mov al, '.'
    stosb
    ; --------------------------------------------------------
    ; '.' 추가
    ; --------------------------------------------------------

    mov al, 'R'
    stosb
    mov al, 'A'
    stosb
    mov al, 'W'
    stosb
    ; --------------------------------------------------------
    ; "RAW" 추가
    ; --------------------------------------------------------

    xor al, al
    stosb
    ; --------------------------------------------------------
    ; null 종료
    ; --------------------------------------------------------


    lea si, [BaseNameBuffer]
    lea di, [PalFileName]
    call CopyAsciiz
    ; --------------------------------------------------------
    ; BaseNameBuffer 내용을 PalFileName으로 복사
    ; --------------------------------------------------------

    dec di
    ; --------------------------------------------------------
    ; 끝의 null 위치를 한 칸 되돌림
    ; --------------------------------------------------------

    mov al, '.'
    stosb
    ; --------------------------------------------------------
    ; '.' 추가
    ; --------------------------------------------------------

    mov al, 'P'
    stosb
    mov al, 'A'
    stosb
    mov al, 'L'
    stosb
    ; --------------------------------------------------------
    ; "PAL" 추가
    ; --------------------------------------------------------

    xor al, al
    stosb
    ; --------------------------------------------------------
    ; null 종료
    ; --------------------------------------------------------


    pop di
    pop si
    pop ax
    ret



; ============================================================
; CopyAsciiz
;
; 목적:
;   0으로 끝나는 문자열을 복사한다
;
; 입력:
;   DS:SI = 원본 문자열
;   ES:DI = 목적지 문자열
;
; 출력:
;   DI는 복사된 끝의 null 다음 위치를 가리킨다
; ============================================================

CopyAsciiz:
; ------------------------------------------------------------
; ASCIIZ 문자열 복사
; ------------------------------------------------------------

.copy_loop:
    lodsb
    ; --------------------------------------------------------
    ; DS:SI 에서 1바이트 읽고 SI 증가
    ; --------------------------------------------------------

    stosb
    ; --------------------------------------------------------
    ; AL 값을 ES:DI 에 저장하고 DI 증가
    ; --------------------------------------------------------

    test al, al
    ; --------------------------------------------------------
    ; 방금 저장한 문자가 0인지 검사
    ; --------------------------------------------------------

    jne .copy_loop
    ; --------------------------------------------------------
    ; 0이 아니면 계속 복사
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; 0까지 복사했으면 종료
    ; --------------------------------------------------------



; ============================================================
; LoadExactFile
;
; 목적:
;   파일을 열고 "최대 CX 바이트까지" 읽어서 버퍼에 저장한다
;
; 입력:
;   DS:DX = 파일명
;   ES:BX = 목적지 버퍼
;   CX    = 읽을 요청 크기
;
; 출력:
;   CF=0 : 파일 열기/읽기/닫기 자체는 성공
;   CF=1 : 파일 열기 또는 읽기 또는 닫기 실패
;
; 추가 출력:
;   LastReadSize = 실제 읽은 바이트 수
;
; 설명:
;   파일 크기가 정확히 기대값인지 여부는 호출자 쪽에서
;   LastReadSize 와 비교하여 판단한다.
;
;   예:
;     PAL은 LastReadSize 가 768이어야 정상
;
; 주의:
;   DOS 파일 읽기는 DS:DX 버퍼를 사용한다.
;   그런데 목적지는 ES:BX로 들어오므로,
;   이 루틴 안에서 잠깐 DS를 ES로 바꾼다.
; ============================================================

LoadExactFile:
; ------------------------------------------------------------
; 파일 열고 읽고 닫는 일반 루틴
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push si
    push di
    ; --------------------------------------------------------
    ; 레지스터 보존
    ; --------------------------------------------------------

    mov ax, 3D00h
    ; --------------------------------------------------------
    ; DOS 파일 열기
    ; AH=3Dh
    ; AL=00h = 읽기 전용
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 열기 수행
    ; 성공 시 AX = 파일 핸들
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 실패 시 즉시 에러
    ; --------------------------------------------------------

    mov [FileHandle], ax
    ; --------------------------------------------------------
    ; 파일 핸들 저장
    ; --------------------------------------------------------

    mov si, ds
    ; --------------------------------------------------------
    ; 원래 DS 보관
    ; --------------------------------------------------------

    push es
    pop ds
    ; --------------------------------------------------------
    ; DS를 ES로 바꿔 목적지 버퍼 세그먼트에 맞춘다
    ; --------------------------------------------------------

    mov dx, bx
    ; --------------------------------------------------------
    ; DS:DX = 목적지 버퍼
    ; --------------------------------------------------------

    mov bx, [cs:FileHandle]
    ; --------------------------------------------------------
    ; 파일 핸들을 BX에 다시 로드
    ; DS가 바뀌었으므로 CS 세그먼트 지정자를 사용
    ; --------------------------------------------------------

    mov ah, 3Fh
    ; --------------------------------------------------------
    ; DOS 파일 읽기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 읽기 수행
    ; 성공 시 AX = 실제 읽은 바이트 수
    ; --------------------------------------------------------

    jc .read_fail
    ; --------------------------------------------------------
    ; 읽기 실패
    ; --------------------------------------------------------

    mov [cs:LastReadSize], ax
    ; --------------------------------------------------------
    ; 실제 읽은 바이트 수 저장
    ; --------------------------------------------------------

    mov ds, si
    ; --------------------------------------------------------
    ; DS 복구
    ; --------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 닫을 파일 핸들
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; DOS 파일 닫기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 수행
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 닫기 실패
    ; --------------------------------------------------------

    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    clc
    ; --------------------------------------------------------
    ; 성공
    ; --------------------------------------------------------

    ret


.read_fail:
; ------------------------------------------------------------
; 읽기 실패 시 DS부터 복구
; ------------------------------------------------------------

    mov ds, si
    ; --------------------------------------------------------
    ; DS 복구
    ; --------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 파일은 열려 있으니 닫기 시도
    ; --------------------------------------------------------

    mov ah, 3Eh
    int 21h
    ; --------------------------------------------------------
    ; 닫기 시도
    ; --------------------------------------------------------

.fail:
; ------------------------------------------------------------
; 공통 실패 경로
; ------------------------------------------------------------

    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    stc
    ; --------------------------------------------------------
    ; 실패
    ; --------------------------------------------------------

    ret



; ============================================================
; ApplyPalette
;
; 목적:
;   PaletteBuffer(768 bytes)를 VGA DAC에 적용한다
;
; VGA 포트:
;   03C8h = 시작 색 인덱스
;   03C9h = RGB 데이터 입력
;
; 주의:
;   파이썬에서 만든 팔레트는 0~255 범위라고 가정
;   VGA DAC는 0~63 범위이므로 4로 나눠서 출력한다
; ============================================================

ApplyPalette:
; ------------------------------------------------------------
; VGA DAC에 256색 팔레트 적용
; ------------------------------------------------------------

    push ax
    push cx
    push dx
    push si
    ; --------------------------------------------------------
    ; 레지스터 보존
    ; --------------------------------------------------------

    mov dx, 03C8h
    ; --------------------------------------------------------
    ; 시작 색 인덱스 포트
    ; --------------------------------------------------------

    xor al, al
    ; --------------------------------------------------------
    ; 0번 색부터 시작
    ; --------------------------------------------------------

    out dx, al
    ; --------------------------------------------------------
    ; 시작 색 인덱스 = 0
    ; --------------------------------------------------------

    mov dx, 03C9h
    ; --------------------------------------------------------
    ; RGB 데이터 포트
    ; --------------------------------------------------------

    lea si, [PaletteBuffer]
    ; --------------------------------------------------------
    ; 팔레트 버퍼 시작 주소
    ; --------------------------------------------------------

    mov cx, 768
    ; --------------------------------------------------------
    ; 총 768바이트 출력
    ; --------------------------------------------------------


.next_byte:
    lodsb
    ; --------------------------------------------------------
    ; 다음 팔레트 바이트를 AL로 읽음
    ; --------------------------------------------------------

    shr al, 1
    shr al, 1
    ; --------------------------------------------------------
    ; 0~255 -> 0~63 축소
    ; --------------------------------------------------------

    out dx, al
    ; --------------------------------------------------------
    ; VGA DAC에 출력
    ; --------------------------------------------------------

    loop .next_byte
    ; --------------------------------------------------------
    ; 768바이트 전부 처리할 때까지 반복
    ; --------------------------------------------------------

    pop si
    pop dx
    pop cx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    ret



; ============================================================
; LoadImageToVRAM
;
; 목적:
;   RawFileName 파일을 열고
;   정확히 64000바이트를 읽어서 A000:0000 에 복사한다
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;
; 설명:
;   - 4096바이트씩 나눠 읽는다
;   - 마지막 조각은 남은 만큼만 읽는다
;   - 마지막에 정확히 64000바이트를 모두 채웠는지 확인한다
; ============================================================

LoadImageToVRAM:
; ------------------------------------------------------------
; RAW 파일을 열고 화면 메모리에 복사
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push ds
    push es
    ; --------------------------------------------------------
    ; 레지스터/세그먼트 보존
    ; --------------------------------------------------------

    lea dx, [RawFileName]
    ; --------------------------------------------------------
    ; 열 RAW 파일 이름
    ; --------------------------------------------------------

    mov ax, 3D00h
    ; --------------------------------------------------------
    ; DOS 파일 열기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 열기
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 실패
    ; --------------------------------------------------------

    mov [FileHandle], ax
    ; --------------------------------------------------------
    ; 파일 핸들 저장
    ; --------------------------------------------------------

    mov ax, 0A000h
    ; --------------------------------------------------------
    ; VGA Mode 13h VRAM 세그먼트
    ; --------------------------------------------------------

    mov es, ax
    ; --------------------------------------------------------
    ; ES = A000h
    ; --------------------------------------------------------

    xor di, di
    ; --------------------------------------------------------
    ; 화면 메모리 시작 오프셋 = 0
    ; --------------------------------------------------------

    mov word [BytesLeft], 64000
    ; --------------------------------------------------------
    ; 총 64000바이트를 채워야 한다
    ; --------------------------------------------------------


.read_loop:
    cmp word [BytesLeft], 0
    ; --------------------------------------------------------
    ; 아직 남은 바이트가 있는지 확인
    ; --------------------------------------------------------

    je .success_close
    ; --------------------------------------------------------
    ; 0이면 성공적으로 다 채운 것
    ; --------------------------------------------------------

    mov ax, [BytesLeft]
    ; --------------------------------------------------------
    ; 남은 바이트 수
    ; --------------------------------------------------------

    cmp ax, 4096
    ; --------------------------------------------------------
    ; 4096보다 큰지 확인
    ; --------------------------------------------------------

    jbe .use_rest
    ; --------------------------------------------------------
    ; 4096 이하라면 남은 만큼만 읽는다
    ; --------------------------------------------------------

    mov ax, 4096
    ; --------------------------------------------------------
    ; 4096보다 크면 이번에는 4096 읽는다
    ; --------------------------------------------------------

.use_rest:
    mov [ChunkSize], ax
    ; --------------------------------------------------------
    ; 이번에 읽을 크기 저장
    ; --------------------------------------------------------

    push cs
    pop ds
    ; --------------------------------------------------------
    ; ReadBuffer가 있는 프로그램 세그먼트로 DS 맞춤
    ; --------------------------------------------------------

    lea dx, [ReadBuffer]
    ; --------------------------------------------------------
    ; 읽기 버퍼 주소
    ; --------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 파일 핸들
    ; --------------------------------------------------------

    mov cx, [ChunkSize]
    ; --------------------------------------------------------
    ; 이번에 읽을 바이트 수
    ; --------------------------------------------------------

    mov ah, 3Fh
    ; --------------------------------------------------------
    ; DOS 파일 읽기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; ReadBuffer로 읽기
    ; --------------------------------------------------------

    jc .close_fail
    ; --------------------------------------------------------
    ; 읽기 실패
    ; --------------------------------------------------------

    cmp ax, [ChunkSize]
    ; --------------------------------------------------------
    ; 실제 읽은 바이트 수가 기대값과 같은지 확인
    ; --------------------------------------------------------

    jne .size_fail
    ; --------------------------------------------------------
    ; 다르면 RAW 크기가 부족하거나 이상함
    ; --------------------------------------------------------

    push cs
    pop ds
    ; --------------------------------------------------------
    ; 복사 원본인 ReadBuffer 접근을 위해 DS 복구
    ; --------------------------------------------------------

    lea si, [ReadBuffer]
    ; --------------------------------------------------------
    ; 원본 버퍼 시작 주소
    ; --------------------------------------------------------

    mov cx, [ChunkSize]
    ; --------------------------------------------------------
    ; 복사 크기
    ; --------------------------------------------------------

    rep movsb
    ; --------------------------------------------------------
    ; DS:SI -> ES:DI 로 복사
    ; 즉 ReadBuffer -> VRAM
    ; --------------------------------------------------------

    mov ax, [BytesLeft]
    ; --------------------------------------------------------
    ; 남은 바이트 수
    ; --------------------------------------------------------

    sub ax, [ChunkSize]
    ; --------------------------------------------------------
    ; 이번에 처리한 만큼 감소
    ; --------------------------------------------------------

    mov [BytesLeft], ax
    ; --------------------------------------------------------
    ; 다시 저장
    ; --------------------------------------------------------

    jmp .read_loop
    ; --------------------------------------------------------
    ; 다음 조각 처리
    ; --------------------------------------------------------


.success_close:
    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 닫을 파일 핸들
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; DOS 파일 닫기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 닫기 수행
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 닫기 실패
    ; --------------------------------------------------------

    pop es
    pop ds
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터/세그먼트 복구
    ; --------------------------------------------------------

    clc
    ; --------------------------------------------------------
    ; 성공
    ; --------------------------------------------------------

    ret


.size_fail:
; ------------------------------------------------------------
; 읽기 자체는 됐지만 기대한 크기만큼 못 읽은 경우
; RAW 파일 크기 이상으로 본다
; ------------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 파일 핸들
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; 닫기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 시도
    ; --------------------------------------------------------

    pop es
    pop ds
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    jmp raw_size_error_graphics
    ; --------------------------------------------------------
    ; 별도 크기 이상 에러 처리로 이동
    ; --------------------------------------------------------


.close_fail:
; ------------------------------------------------------------
; 파일은 열려 있으므로 닫고 실패 처리
; ------------------------------------------------------------

    mov bx, [FileHandle]
    mov ah, 3Eh
    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 시도
    ; --------------------------------------------------------

.fail:
; ------------------------------------------------------------
; 공통 실패 경로
; ------------------------------------------------------------

    pop es
    pop ds
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 레지스터 복구
    ; --------------------------------------------------------

    stc
    ; --------------------------------------------------------
    ; 실패
    ; --------------------------------------------------------

    ret



