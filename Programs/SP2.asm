; ============================================================
; SP2.ASM
;
; 목적:
;   1) DOS에서 명령행 인자로 WAV 파일명을 받는다
;   2) 예: SP2 SOUND.WAV
;   3) WAV 헤더를 검사한다
;   4) 지원 형식이면 Sound Blaster 8-bit DMA single-cycle 방식으로 재생한다
;   5) 재생 완료 IRQ를 기다린 뒤 종료한다
;
; 현재 버전의 지원 조건:
;   - RIFF/WAVE
;   - 기본 44바이트 WAV 헤더
;   - PCM (AudioFormat = 1)
;   - Mono (Channels = 1)
;   - 8-bit (BitsPerSample = 8)
;   - 8000 Hz (SampleRate = 8000)
;   - data chunk 가 44바이트 헤더 바로 뒤에 있는 기본형
;
; 하드웨어 가정:
;   - Sound Blaster base port = 220h
;   - IRQ = 7
;   - DMA = 1
;
; 주의:
;   - 이 코드는 "첫 DMA 버전"이다
;   - WAV data 크기는 최대 32768 바이트까지만 지원한다
;   - data chunk를 통째로 메모리에 읽어서 single-cycle DMA로 한 번 재생한다
;   - 더 큰 파일/연속 재생/루프 재생은 다음 단계에서 auto-init DMA로 확장 가능
;
; 빌드:
;   nasm -f bin SP2.ASM -o SP2.COM
;
; 사용법:
;   SP2 SOUND.WAV
; ============================================================

org 100h
jmp start


; ============================================================
; 상수 정의
; ============================================================

SB_BASE         equ 0220h
; ------------------------------------------------------------
; Sound Blaster 기본 베이스 포트
; DOSBox 기본 설정에 흔한 220h 를 가정한다
; ------------------------------------------------------------

SB_RESET        equ SB_BASE + 06h
; ------------------------------------------------------------
; DSP Reset 포트
; ------------------------------------------------------------

SB_READ         equ SB_BASE + 0Ah
; ------------------------------------------------------------
; DSP Data Read 포트
; ------------------------------------------------------------

SB_WRITE        equ SB_BASE + 0Ch
; ------------------------------------------------------------
; DSP Write 포트
; 동시에 write buffer status 확인에도 사용한다
; bit7 = 1 이면 아직 쓸 수 없음
; bit7 = 0 이면 써도 됨
; ------------------------------------------------------------

SB_RSTATUS      equ SB_BASE + 0Eh
; ------------------------------------------------------------
; DSP Read Status 포트
; bit7 = 1 이면 읽을 데이터가 있음
; 또한 8-bit IRQ acknowledge 용으로 읽는다
; ------------------------------------------------------------

PIC1_MASK       equ 021h
; ------------------------------------------------------------
; PIC 마스터 마스크 포트
; IRQ0~7 마스크 제어
; ------------------------------------------------------------

PIC1_EOI        equ 020h
; ------------------------------------------------------------
; PIC 마스터 EOI 포트
; ------------------------------------------------------------

DMA_MASK_REG    equ 00Ah
; ------------------------------------------------------------
; 8237 DMA Single Mask Register
; ------------------------------------------------------------

DMA_MODE_REG    equ 00Bh
; ------------------------------------------------------------
; 8237 DMA Mode Register
; ------------------------------------------------------------

DMA_CLEAR_FF    equ 00Ch
; ------------------------------------------------------------
; 8237 DMA Flip-Flop Clear
; 주소/카운트 low/high 순서를 초기화하기 위해 사용
; ------------------------------------------------------------

DMA_CH1_ADDR    equ 002h
; ------------------------------------------------------------
; DMA channel 1 base/current address port
; ------------------------------------------------------------

DMA_CH1_COUNT   equ 003h
; ------------------------------------------------------------
; DMA channel 1 count port
; ------------------------------------------------------------

DMA_CH1_PAGE    equ 083h
; ------------------------------------------------------------
; DMA channel 1 page register
; ------------------------------------------------------------

IRQ7_VECTOR     equ 0Fh
; ------------------------------------------------------------
; IRQ7의 인터럽트 벡터 번호
; IRQ0=08h 이므로 IRQ7=0Fh
; ------------------------------------------------------------

MAX_DATA_SIZE   equ 55000
; ------------------------------------------------------------
; 이번 버전에서 지원하는 최대 WAV data 크기
; .COM 프로그램에서 단순하고 안전하게 가기 위한 제한
; ------------------------------------------------------------

TIME_CONST_8KHZ equ 083h
; ------------------------------------------------------------
; 8000 Hz 용 DSP time constant
; ------------------------------------------------------------


; ============================================================
; 메시지 문자열
; ============================================================

MsgUsage db 13,10,'Usage: SP2 SOUND.WAV',13,10,'$'
; ------------------------------------------------------------
; 사용법 메시지
; ------------------------------------------------------------

MsgTooManyArgs db 13,10,'Error: only one filename is allowed.$'
; ------------------------------------------------------------
; 인자를 2개 이상 넣었을 때의 메시지
; ------------------------------------------------------------

MsgFileOpenError db 13,10,'File open error: $'
; ------------------------------------------------------------
; 파일 열기 실패 메시지 앞부분
; ------------------------------------------------------------

MsgReadError db 13,10,'File read error.$'
; ------------------------------------------------------------
; 파일 읽기 실패 메시지
; ------------------------------------------------------------

MsgNotWave db 13,10,'Error: not a supported WAV file.$'
; ------------------------------------------------------------
; WAV 구조가 아니거나 지원 형식이 아닐 때
; ------------------------------------------------------------

MsgNeedPCM8Mono8k db 13,10,'Need PCM 8-bit mono 8000Hz WAV.$'
; ------------------------------------------------------------
; 현재 지원 형식이 아닐 때
; ------------------------------------------------------------

MsgTooLarge db 13,10,'Error: WAV data too large for this SP2 version.$'
; ------------------------------------------------------------
; 이번 버전 최대 크기 초과
; ------------------------------------------------------------

MsgDMABoundary db 13,10,'Error: playback buffer crosses 64K DMA boundary.$'
; ------------------------------------------------------------
; DMA 버퍼가 64K 경계를 넘으면 8-bit DMA 한 블록 재생이 곤란하다
; ------------------------------------------------------------

MsgSBError db 13,10,'Sound Blaster not detected at 220h.$'
; ------------------------------------------------------------
; Sound Blaster DSP 초기화 실패 메시지
; ------------------------------------------------------------

MsgDone db 13,10,'Done.$'
; ------------------------------------------------------------
; 재생 완료 메시지
; ------------------------------------------------------------


; ============================================================
; 데이터 영역
; ============================================================

FileNameBuffer db 13 dup(0)
; ------------------------------------------------------------
; DOS 8.3 파일명을 담을 버퍼
; 최대 12자 + null
; 예: SOUND.WAV,0
; ------------------------------------------------------------

HeaderBuffer db 44 dup(0)
; ------------------------------------------------------------
; 기본 WAV 헤더 44바이트를 읽어둘 버퍼
; ------------------------------------------------------------

PlaybackBuffer db MAX_DATA_SIZE dup(0)
; ------------------------------------------------------------
; WAV data chunk를 통째로 읽어둘 재생 버퍼
; 이번 버전은 단발 DMA 재생이므로 한 번에 이 버퍼에 적재한다
; ------------------------------------------------------------

FileHandle dw 0
; ------------------------------------------------------------
; 열린 파일 핸들
; ------------------------------------------------------------

BytesRead dw 0
; ------------------------------------------------------------
; 마지막 파일 읽기에서 실제 읽은 바이트 수
; ------------------------------------------------------------

DataSizeLo dw 0
DataSizeHi dw 0
; ------------------------------------------------------------
; WAV data chunk 크기 (dword)
; 현재 버전에서는 hi=0 이어야 하고 lo<=32768 이어야 한다
; ------------------------------------------------------------

PlaySize dw 0
; ------------------------------------------------------------
; 실제 재생할 바이트 수
; 즉 WAV data chunk 크기 (최대 32768)
; ------------------------------------------------------------

DMAOffset dw 0
; ------------------------------------------------------------
; DMA 전송 시작 오프셋의 low 16비트
; ------------------------------------------------------------

DMAPage db 0
; ------------------------------------------------------------
; DMA page register에 넣을 상위 주소 바이트
; ------------------------------------------------------------

PlaybackDone db 0
; ------------------------------------------------------------
; IRQ7 핸들러가 재생 완료 시 1로 바꾸는 플래그
; ------------------------------------------------------------

OldIntOff dw 0
OldIntSeg dw 0
; ------------------------------------------------------------
; 원래 IRQ7 벡터 저장용
; ------------------------------------------------------------

OldPICMask db 0
; ------------------------------------------------------------
; 원래 PIC 마스크 값 저장용
; 종료 시 복원한다
; ------------------------------------------------------------


; ============================================================
; 프로그램 시작
; ============================================================

start:
    push cs
    pop ds
    ; --------------------------------------------------------
    ; DS를 프로그램 세그먼트로 맞춘다
    ; --------------------------------------------------------

    push cs
    pop es
    ; --------------------------------------------------------
    ; ES도 프로그램 세그먼트로 맞춘다
    ; --------------------------------------------------------

    call ParseFileName
    ; --------------------------------------------------------
    ; 명령행에서 파일명 하나를 읽어 FileNameBuffer에 저장
    ; --------------------------------------------------------

    jc show_usage
    ; --------------------------------------------------------
    ; 인자가 없거나 형식이 잘못되면 사용법 출력
    ; --------------------------------------------------------

    call OpenWaveFile
    ; --------------------------------------------------------
    ; WAV 파일 열기
    ; --------------------------------------------------------

    jc file_open_error
    ; --------------------------------------------------------
    ; 파일 열기 실패
    ; --------------------------------------------------------

    call ReadAndValidateHeader
    ; --------------------------------------------------------
    ; WAV 헤더 44바이트를 읽고 형식 검사
    ; --------------------------------------------------------

    jc wav_error
    ; --------------------------------------------------------
    ; 헤더/형식 오류
    ; --------------------------------------------------------

    call CheckDataSize
    ; --------------------------------------------------------
    ; 현재 버전이 지원 가능한 크기인지 검사
    ; --------------------------------------------------------

    jc size_error
    ; --------------------------------------------------------
    ; data 크기 초과
    ; --------------------------------------------------------

    call ReadWaveData
    ; --------------------------------------------------------
    ; WAV data chunk를 PlaybackBuffer에 통째로 읽는다
    ; --------------------------------------------------------

    jc read_error
    ; --------------------------------------------------------
    ; 파일 읽기 실패
    ; --------------------------------------------------------

    call CloseFile
    ; --------------------------------------------------------
    ; 재생 전에 파일 닫기
    ; --------------------------------------------------------

    call PrepareDMABufferAddress
    ; --------------------------------------------------------
    ; PlaybackBuffer의 실제 물리 주소를 계산하고
    ; 64K DMA 경계를 넘는지 확인한다
    ; --------------------------------------------------------

    jc dma_boundary_error
    ; --------------------------------------------------------
    ; DMA 경계 에러
    ; --------------------------------------------------------

    call SB_Init
    ; --------------------------------------------------------
    ; Sound Blaster DSP 초기화
    ; --------------------------------------------------------

    jc sb_error
    ; --------------------------------------------------------
    ; DSP 응답 없음
    ; --------------------------------------------------------

    call InstallIRQ7Handler
    ; --------------------------------------------------------
    ; IRQ7 인터럽트 핸들러 설치
    ; --------------------------------------------------------

    call UnmaskIRQ7
    ; --------------------------------------------------------
    ; PIC에서 IRQ7 허용
    ; --------------------------------------------------------

    call SB_SpeakerOn
    ; --------------------------------------------------------
    ; DSP 스피커 ON
    ; --------------------------------------------------------

    call ProgramDMAForPlayback
    ; --------------------------------------------------------
    ; DMA channel 1을 PlaybackBuffer 대상으로 설정
    ; --------------------------------------------------------

    call SB_Set8kTimeConstant
    ; --------------------------------------------------------
    ; 8000Hz 재생용 time constant 설정
    ; --------------------------------------------------------

    mov byte [PlaybackDone], 0
    ; --------------------------------------------------------
    ; 완료 플래그 초기화
    ; --------------------------------------------------------

    call SB_StartSingleCycleDMA
    ; --------------------------------------------------------
    ; DSP에 8-bit single-cycle DMA playback 시작 명령 전송
    ; --------------------------------------------------------

.wait_done:
    cmp byte [PlaybackDone], 1
    je .play_done
    ; --------------------------------------------------------
    ; IRQ7 핸들러가 완료 플래그를 1로 바꿀 때까지 대기
    ; --------------------------------------------------------

    ; 재생 중 키보드 입력을 그냥 무시하고 완료만 기다린다
    jmp .wait_done

.play_done:
    call SB_SpeakerOff
    ; --------------------------------------------------------
    ; 재생이 끝났으므로 스피커 OFF
    ; --------------------------------------------------------

    call RestorePICMask
    ; --------------------------------------------------------
    ; PIC 마스크 원복
    ; --------------------------------------------------------

    call UninstallIRQ7Handler
    ; --------------------------------------------------------
    ; 원래 IRQ7 벡터 복원
    ; --------------------------------------------------------

    mov dx, MsgDone
    call PrintString
    ; --------------------------------------------------------
    ; 완료 메시지 출력
    ; --------------------------------------------------------

    mov ax, 4C00h
    int 21h
    ; --------------------------------------------------------
    ; 정상 종료
    ; --------------------------------------------------------


show_usage:
    mov dx, MsgUsage
    call PrintString
    mov ax, 4C01h
    int 21h
    ; --------------------------------------------------------
    ; 사용법 오류 종료
    ; --------------------------------------------------------


file_open_error:
    mov dx, MsgFileOpenError
    call PrintString
    mov dx, FileNameBuffer
    call PrintAsciiz
    call PrintCRLF
    mov ax, 4C02h
    int 21h
    ; --------------------------------------------------------
    ; 파일 열기 실패 종료
    ; --------------------------------------------------------


wav_error:
    call CloseFile
    mov ax, 4C03h
    int 21h
    ; --------------------------------------------------------
    ; WAV 형식 오류 종료
    ; --------------------------------------------------------


size_error:
    call CloseFile
    mov dx, MsgTooLarge
    call PrintString
    mov ax, 4C04h
    int 21h
    ; --------------------------------------------------------
    ; 크기 초과 종료
    ; --------------------------------------------------------


read_error:
    call CloseFile
    mov dx, MsgReadError
    call PrintString
    mov ax, 4C05h
    int 21h
    ; --------------------------------------------------------
    ; WAV data 읽기 실패 종료
    ; --------------------------------------------------------


dma_boundary_error:
    mov dx, MsgDMABoundary
    call PrintString
    mov ax, 4C06h
    int 21h
    ; --------------------------------------------------------
    ; DMA boundary 오류 종료
    ; --------------------------------------------------------


sb_error:
    call RestorePICMask
    call UninstallIRQ7Handler
    mov dx, MsgSBError
    call PrintString
    mov ax, 4C07h
    int 21h
    ; --------------------------------------------------------
    ; SB 초기화 실패 종료
    ; --------------------------------------------------------


; ============================================================
; ParseFileName
;
; 목적:
;   PSP 명령행에서 파일명 하나를 받아 FileNameBuffer에 저장
;
; 형식:
;   SP2 SOUND.WAV
;
; 제한:
;   - 파일명 하나만 받는다
;   - 최대 12글자 (8.3 형식 기준)
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

ParseFileName:
    push ax
    push bx
    push si
    push di

    mov si, 81h
    ; --------------------------------------------------------
    ; PSP 명령행 시작 위치
    ; --------------------------------------------------------

    lea di, [FileNameBuffer]
    ; --------------------------------------------------------
    ; 저장 버퍼 시작
    ; --------------------------------------------------------

    xor bx, bx
    ; --------------------------------------------------------
    ; BX = 저장 글자 수
    ; --------------------------------------------------------

.skip_spaces:
    mov al, [si]
    cmp al, 13
    je .fail
    cmp al, ' '
    jne .read_name
    inc si
    jmp .skip_spaces
    ; --------------------------------------------------------
    ; 앞쪽 공백 제거
    ; --------------------------------------------------------

.read_name:
    mov al, [si]
    cmp al, ' '
    je .end_name
    cmp al, 13
    je .end_name

    cmp bx, 12
    jae .fail
    ; --------------------------------------------------------
    ; 8.3 기준 파일명 최대 12자만 허용
    ; --------------------------------------------------------

    mov [di], al
    inc di
    inc si
    inc bx
    jmp .read_name

.end_name:
    cmp bx, 0
    je .fail
    ; --------------------------------------------------------
    ; 글자 수 0이면 실패
    ; --------------------------------------------------------

    mov byte [di], 0
    ; --------------------------------------------------------
    ; null 종료
    ; --------------------------------------------------------

.skip_tail_spaces:
    mov al, [si]
    cmp al, 13
    je .success
    cmp al, ' '
    jne .too_many_args
    inc si
    jmp .skip_tail_spaces
    ; --------------------------------------------------------
    ; 뒤쪽 공백 제거
    ; 공백 아닌 문자가 더 나오면 두 번째 인자라고 보고 실패
    ; --------------------------------------------------------

.too_many_args:
    mov dx, MsgTooManyArgs
    call PrintString
    stc
    jmp .done

.success:
    clc
    jmp .done

.fail:
    stc

.done:
    pop di
    pop si
    pop bx
    pop ax
    ret


; ============================================================
; OpenWaveFile
;
; 목적:
;   FileNameBuffer의 파일을 읽기 전용으로 연다
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

OpenWaveFile:
    mov dx, FileNameBuffer
    mov ax, 3D00h
    int 21h
    jc .fail

    mov [FileHandle], ax
    clc
    ret

.fail:
    stc
    ret


; ============================================================
; CloseFile
;
; 목적:
;   현재 열린 파일이 있으면 닫는다
; ============================================================

CloseFile:
    push ax
    push bx

    mov bx, [FileHandle]
    cmp bx, 0
    je .done
    ; --------------------------------------------------------
    ; 핸들이 0이면 열려 있지 않다고 본다
    ; --------------------------------------------------------

    mov ah, 3Eh
    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기
    ; --------------------------------------------------------

    mov word [FileHandle], 0

.done:
    pop bx
    pop ax
    ret


; ============================================================
; ReadFileBlock
;
; 목적:
;   현재 열린 파일에서 CX 바이트를 읽는다
;
; 입력:
;   CX    = 읽을 바이트 수
;   DS:DX = 버퍼 주소
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;   BytesRead = 실제 읽은 바이트 수
; ============================================================

ReadFileBlock:
    push ax
    push bx

    mov bx, [FileHandle]
    mov ah, 3Fh
    int 21h
    jc .fail

    mov [BytesRead], ax
    clc
    jmp .done

.fail:
    stc

.done:
    pop bx
    pop ax
    ret


; ============================================================
; ReadAndValidateHeader
;
; 목적:
;   WAV 헤더 44바이트를 읽고 현재 버전이 지원하는 형식인지 검사
;
; 지원 형식:
;   - "RIFF"
;   - "WAVE"
;   - "fmt "
;   - fmt chunk size = 16
;   - AudioFormat = 1 (PCM)
;   - Channels = 1
;   - SampleRate = 8000
;   - BitsPerSample = 8
;   - "data"
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;   DataSizeLo/Hi = data chunk 크기
; ============================================================

ReadAndValidateHeader:
    push ax
    push cx
    push dx
    push si

    mov dx, HeaderBuffer
    mov cx, 44
    call ReadFileBlock
    jc .read_fail

    cmp word [BytesRead], 44
    jne .bad_wave
    ; --------------------------------------------------------
    ; 44바이트를 못 읽으면 기본형 WAV로 보기 어렵다
    ; --------------------------------------------------------

    lea si, [HeaderBuffer]

    ; "RIFF"
    cmp byte [si+0], 'R'
    jne .bad_wave
    cmp byte [si+1], 'I'
    jne .bad_wave
    cmp byte [si+2], 'F'
    jne .bad_wave
    cmp byte [si+3], 'F'
    jne .bad_wave

    ; "WAVE"
    cmp byte [si+8], 'W'
    jne .bad_wave
    cmp byte [si+9], 'A'
    jne .bad_wave
    cmp byte [si+10], 'V'
    jne .bad_wave
    cmp byte [si+11], 'E'
    jne .bad_wave

    ; "fmt "
    cmp byte [si+12], 'f'
    jne .bad_wave
    cmp byte [si+13], 'm'
    jne .bad_wave
    cmp byte [si+14], 't'
    jne .bad_wave
    cmp byte [si+15], ' '
    jne .bad_wave

    ; fmt chunk size = 16
    cmp word [si+16], 16
    jne .bad_format
    cmp word [si+18], 0
    jne .bad_format

    ; AudioFormat = 1 (PCM)
    cmp word [si+20], 1
    jne .bad_format

    ; Channels = 1 (mono)
    cmp word [si+22], 1
    jne .bad_format

    ; SampleRate = 8000
    cmp word [si+24], 8000
    jne .bad_format
    cmp word [si+26], 0
    jne .bad_format

    ; BitsPerSample = 8
    cmp word [si+34], 8
    jne .bad_format

    ; "data"
    cmp byte [si+36], 'd'
    jne .bad_wave
    cmp byte [si+37], 'a'
    jne .bad_wave
    cmp byte [si+38], 't'
    jne .bad_wave
    cmp byte [si+39], 'a'
    jne .bad_wave

    mov ax, [si+40]
    mov [DataSizeLo], ax
    mov ax, [si+42]
    mov [DataSizeHi], ax
    ; --------------------------------------------------------
    ; data chunk 크기 저장
    ; --------------------------------------------------------

    clc
    jmp .done

.read_fail:
    mov dx, MsgReadError
    call PrintString
    stc
    jmp .done

.bad_wave:
    mov dx, MsgNotWave
    call PrintString
    stc
    jmp .done

.bad_format:
    mov dx, MsgNeedPCM8Mono8k
    call PrintString
    stc

.done:
    pop si
    pop dx
    pop cx
    pop ax
    ret


; ============================================================
; CheckDataSize
;
; 목적:
;   현재 버전에서 지원 가능한 WAV data 크기인지 검사
;
; 조건:
;   - high word = 0
;   - low word <= MAX_DATA_SIZE
;   - low word > 0
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;   PlaySize = 실제 재생할 바이트 수
; ============================================================

CheckDataSize:
    mov ax, [DataSizeHi]
    or ax, ax
    jnz .fail
    ; --------------------------------------------------------
    ; 65535 초과는 이번 버전에서 지원 안 함
    ; --------------------------------------------------------

    mov ax, [DataSizeLo]
    or ax, ax
    jz .fail
    ; --------------------------------------------------------
    ; data 길이 0은 무의미하므로 실패
    ; --------------------------------------------------------

    cmp ax, MAX_DATA_SIZE
    ja .fail
    ; --------------------------------------------------------
    ; 이번 버전의 최대 버퍼 크기 초과
    ; --------------------------------------------------------

    mov [PlaySize], ax
    clc
    ret

.fail:
    stc
    ret


; ============================================================
; ReadWaveData
;
; 목적:
;   WAV data chunk 전체를 PlaybackBuffer에 읽는다
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

ReadWaveData:
    push cx
    push dx

    mov dx, PlaybackBuffer
    mov cx, [PlaySize]
    call ReadFileBlock
    jc .fail

    cmp word [BytesRead], cx
    jne .fail
    ; --------------------------------------------------------
    ; 요청한 바이트 수만큼 정확히 못 읽었으면 실패
    ; --------------------------------------------------------

    clc
    jmp .done

.fail:
    stc

.done:
    pop dx
    pop cx
    ret


; ============================================================
; PrepareDMABufferAddress
;
; 목적:
;   PlaybackBuffer의 실제 물리 주소를 계산해 DMAOffset/DMAPage에 저장한다
;   그리고 PlaySize 범위가 같은 64K 물리 페이지 안에 모두 들어가는지 확인한다
;
; 8-bit DMA 단발 블록 전송은 64K 경계를 넘기면 곤란하므로 검사 필요
;
; 계산:
;   physical = DS * 16 + offset
;   DMA page   = physical >> 16
;   DMA offset = physical & 0FFFFh
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

PrepareDMABufferAddress:
    push ax
    push bx
    push dx

    mov ax, ds
    ; --------------------------------------------------------
    ; 현재 세그먼트
    ; --------------------------------------------------------

    mov dx, ax
    shr dx, 12
    ; --------------------------------------------------------
    ; DX에 물리 주소 상위 page 후보를 만든다
    ; segment << 4 이므로 segment >> 12 가 page 기여분
    ; --------------------------------------------------------

    shl ax, 4
    ; --------------------------------------------------------
    ; AX = segment * 16 = physical base low 16비트
    ; --------------------------------------------------------

    add ax, PlaybackBuffer
    ; --------------------------------------------------------
    ; AX += PlaybackBuffer offset
    ; --------------------------------------------------------

    jnc .no_carry
    inc dx
    ; --------------------------------------------------------
    ; low 16비트 덧셈 carry가 나면 page 증가
    ; --------------------------------------------------------

.no_carry:
    mov [DMAOffset], ax
    mov [DMAPage], dl
    ; --------------------------------------------------------
    ; 계산된 DMA 시작 주소 저장
    ; --------------------------------------------------------

    mov bx, [PlaySize]
    dec bx
    ; --------------------------------------------------------
    ; 마지막 바이트 위치를 보기 위해 size-1
    ; --------------------------------------------------------

    add ax, bx
    jc .cross_fail
    ; --------------------------------------------------------
    ; 시작 offset + size-1 에서 carry가 나면
    ; 같은 64K page를 벗어난 것
    ; --------------------------------------------------------

    clc
    jmp .done

.cross_fail:
    stc

.done:
    pop dx
    pop bx
    pop ax
    ret


; ============================================================
; SB_Init
;
; 목적:
;   Sound Blaster DSP reset 후 존재 여부 확인
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;
; 절차:
;   1) reset 포트에 1
;   2) 짧게 대기
;   3) reset 포트에 0
;   4) status bit7 대기
;   5) read 포트에서 AAh 확인
; ============================================================

SB_Init:
    push ax
    push cx
    push dx

    mov dx, SB_RESET
    mov al, 1
    out dx, al
    ; --------------------------------------------------------
    ; DSP reset 시작
    ; --------------------------------------------------------

    mov cx, 500
.delay1:
    loop .delay1
    ; --------------------------------------------------------
    ; 아주 짧은 대기
    ; --------------------------------------------------------

    xor al, al
    out dx, al
    ; --------------------------------------------------------
    ; DSP reset 해제
    ; --------------------------------------------------------

    mov dx, SB_RSTATUS
    mov cx, 1000
.wait_data:
    in al, dx
    test al, 80h
    jnz .have_data
    loop .wait_data
    jmp .fail
    ; --------------------------------------------------------
    ; 읽을 데이터가 준비될 때까지 대기
    ; --------------------------------------------------------

.have_data:
    mov dx, SB_READ
    in al, dx
    cmp al, 0AAh
    jne .fail
    ; --------------------------------------------------------
    ; reset 성공 응답은 0AAh
    ; --------------------------------------------------------

    clc
    jmp .done

.fail:
    stc

.done:
    pop dx
    pop cx
    pop ax
    ret


; ============================================================
; SB_WaitWriteReady
;
; 목적:
;   DSP write buffer가 비워질 때까지 기다린다
; ============================================================

SB_WaitWriteReady:
    push ax
    push dx

    mov dx, SB_WRITE
.wait_loop:
    in al, dx
    test al, 80h
    jnz .wait_loop
    ; --------------------------------------------------------
    ; bit7=1 이면 아직 busy
    ; bit7=0 이 되면 써도 됨
    ; --------------------------------------------------------

    pop dx
    pop ax
    ret


; ============================================================
; SB_WriteDSP
;
; 목적:
;   AL 값을 DSP 명령/데이터로 쓴다
; ============================================================

SB_WriteDSP:
    push dx

    call SB_WaitWriteReady
    mov dx, SB_WRITE
    out dx, al
    ; --------------------------------------------------------
    ; AL을 DSP write 포트로 전송
    ; --------------------------------------------------------

    pop dx
    ret


; ============================================================
; SB_SpeakerOn / SB_SpeakerOff
;
; 목적:
;   DSP 스피커 on/off
; ============================================================

SB_SpeakerOn:
    mov al, 0D1h
    call SB_WriteDSP
    ret

SB_SpeakerOff:
    mov al, 0D3h
    call SB_WriteDSP
    ret


; ============================================================
; SB_Set8kTimeConstant
;
; 목적:
;   8000 Hz 재생용 time constant를 설정한다
; ============================================================

SB_Set8kTimeConstant:
    mov al, 40h
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; DSP command 40h = set time constant
    ; --------------------------------------------------------

    mov al, TIME_CONST_8KHZ
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; 8000Hz용 time constant = 83h
    ; --------------------------------------------------------

    ret


; ============================================================
; SB_StartSingleCycleDMA
;
; 목적:
;   DSP에 8-bit single-cycle DMA playback 명령을 보낸다
;
; 절차:
;   1) command 14h 전송
;   2) block length-1 의 low byte
;   3) block length-1 의 high byte
; ============================================================

SB_StartSingleCycleDMA:
    push ax
    push bx

    mov al, 14h
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; DSP command 14h = 8-bit single-cycle DMA playback
    ; --------------------------------------------------------

    mov ax, [PlaySize]
    dec ax
    ; --------------------------------------------------------
    ; DSP length는 size-1을 보낸다
    ; --------------------------------------------------------

    mov bl, al
    mov al, bl
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; low byte
    ; --------------------------------------------------------

    mov al, ah
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; high byte
    ; --------------------------------------------------------

    pop bx
    pop ax
    ret


; ============================================================
; ProgramDMAForPlayback
;
; 목적:
;   DMA channel 1을 PlaybackBuffer 대상으로 설정한다
;
; 채널:
;   - 8-bit DMA channel 1
;
; 설정 순서:
;   1) channel 1 mask
;   2) clear flip-flop
;   3) mode register = single-cycle playback
;   4) address low/high
;   5) count low/high
;   6) page
;   7) channel 1 unmask
; ============================================================

ProgramDMAForPlayback:
    push ax
    push dx

    mov dx, DMA_MASK_REG
    mov al, 05h
    out dx, al
    ; --------------------------------------------------------
    ; channel 1 mask
    ; 04h = set mask bit
    ; +01h = channel 1
    ; --------------------------------------------------------

    mov dx, DMA_CLEAR_FF
    xor al, al
    out dx, al
    ; --------------------------------------------------------
    ; address/count low-high 순서 리셋
    ; --------------------------------------------------------

    mov dx, DMA_MODE_REG
    mov al, 49h
    out dx, al
    ; --------------------------------------------------------
    ; mode = channel 1, single transfer, address increment,
    ; read from memory (device playback)
    ; --------------------------------------------------------

    mov dx, DMA_CH1_ADDR
    mov al, [DMAOffset]
    out dx, al
    mov al, [DMAOffset + 1]
    out dx, al
    ; --------------------------------------------------------
    ; DMA address low/high
    ; --------------------------------------------------------

    mov dx, DMA_CLEAR_FF
    xor al, al
    out dx, al
    ; --------------------------------------------------------
    ; count 설정 전 flip-flop 다시 리셋
    ; --------------------------------------------------------

    mov dx, DMA_CH1_COUNT
    mov ax, [PlaySize]
    dec ax
    ; --------------------------------------------------------
    ; DMA count도 size-1
    ; --------------------------------------------------------

    out dx, al
    mov al, ah
    out dx, al
    ; --------------------------------------------------------
    ; DMA count low/high
    ; --------------------------------------------------------

    mov dx, DMA_CH1_PAGE
    mov al, [DMAPage]
    out dx, al
    ; --------------------------------------------------------
    ; DMA page register
    ; --------------------------------------------------------

    mov dx, DMA_MASK_REG
    mov al, 01h
    out dx, al
    ; --------------------------------------------------------
    ; channel 1 unmask
    ; 00h = clear mask bit
    ; +01h = channel 1
    ; --------------------------------------------------------

    pop dx
    pop ax
    ret


; ============================================================
; InstallIRQ7Handler
;
; 목적:
;   IRQ7 벡터(int 0Fh)를 우리 핸들러로 바꾼다
;   원래 벡터는 OldIntOff/OldIntSeg에 저장한다
; ============================================================

InstallIRQ7Handler:
    push ax
    push es

    cli
    ; --------------------------------------------------------
    ; 벡터 수정 중 인터럽트 금지
    ; --------------------------------------------------------

    xor ax, ax
    mov es, ax
    ; --------------------------------------------------------
    ; 인터럽트 벡터 테이블은 0000:0000에 있다
    ; --------------------------------------------------------

    mov ax, [es:IRQ7_VECTOR*4]
    mov [OldIntOff], ax
    ; --------------------------------------------------------
    ; 기존 오프셋 저장
    ; --------------------------------------------------------

    mov ax, [es:IRQ7_VECTOR*4 + 2]
    mov [OldIntSeg], ax
    ; --------------------------------------------------------
    ; 기존 세그먼트 저장
    ; --------------------------------------------------------

    mov word [es:IRQ7_VECTOR*4], IRQ7_Handler
    mov word [es:IRQ7_VECTOR*4 + 2], cs
    ; --------------------------------------------------------
    ; 새 핸들러 설치
    ; --------------------------------------------------------

    sti
    ; --------------------------------------------------------
    ; 인터럽트 복원
    ; --------------------------------------------------------

    pop es
    pop ax
    ret


; ============================================================
; UninstallIRQ7Handler
;
; 목적:
;   원래 IRQ7 벡터를 복원한다
; ============================================================

UninstallIRQ7Handler:
    push ax
    push es

    cli

    xor ax, ax
    mov es, ax

    mov ax, [OldIntOff]
    mov [es:IRQ7_VECTOR*4], ax

    mov ax, [OldIntSeg]
    mov [es:IRQ7_VECTOR*4 + 2], ax

    sti

    pop es
    pop ax
    ret


; ============================================================
; UnmaskIRQ7
;
; 목적:
;   PIC에서 IRQ7을 허용한다
;   원래 마스크 값은 OldPICMask에 저장한다
; ============================================================

UnmaskIRQ7:
    push ax

    in al, PIC1_MASK
    mov [OldPICMask], al
    ; --------------------------------------------------------
    ; 기존 PIC 마스크 저장
    ; --------------------------------------------------------

    and al, 07Fh
    ; --------------------------------------------------------
    ; bit7 클리어 -> IRQ7 허용
    ; --------------------------------------------------------

    out PIC1_MASK, al

    pop ax
    ret


; ============================================================
; RestorePICMask
;
; 목적:
;   PIC 마스크를 원래 값으로 복원한다
; ============================================================

RestorePICMask:
    push ax

    mov al, [OldPICMask]
    out PIC1_MASK, al

    pop ax
    ret


; ============================================================
; IRQ7_Handler
;
; 목적:
;   재생 완료 시 Sound Blaster 8-bit IRQ를 처리한다
;
; 작업:
;   1) PlaybackDone = 1
;   2) SB 8-bit IRQ acknowledge
;   3) PIC EOI
;   4) IRET
; ============================================================

IRQ7_Handler:
    push ax
    push dx
    push ds

    push cs
    pop ds
    ; --------------------------------------------------------
    ; 우리 데이터에 접근하기 위해 DS를 CS로 맞춘다
    ; --------------------------------------------------------

    mov byte [PlaybackDone], 1
    ; --------------------------------------------------------
    ; 메인 루프에 재생 완료 알림
    ; --------------------------------------------------------

    mov dx, SB_RSTATUS
    in al, dx
    ; --------------------------------------------------------
    ; 8-bit SB IRQ acknowledge
    ; --------------------------------------------------------

    mov al, 20h
    out PIC1_EOI, al
    ; --------------------------------------------------------
    ; PIC에 EOI 전송
    ; --------------------------------------------------------

    pop ds
    pop dx
    pop ax
    iret


; ============================================================
; PrintString
;
; 목적:
;   '$' 종료 문자열을 출력한다
;
; 입력:
;   DS:DX = '$' 종료 문자열
; ============================================================

PrintString:
    mov ah, 09h
    int 21h
    ret


; ============================================================
; PrintAsciiz
;
; 목적:
;   0(null) 종료 문자열을 한 글자씩 출력한다
;
; 입력:
;   DS:DX = ASCIIZ 문자열
; ============================================================

PrintAsciiz:
    push ax
    push dx
    push si

    mov si, dx

.next_char:
    lodsb
    test al, al
    jz .done

    mov dl, al
    mov ah, 02h
    int 21h
    jmp .next_char

.done:
    pop si
    pop dx
    pop ax
    ret


; ============================================================
; PrintCRLF
;
; 목적:
;   줄바꿈(CR/LF) 출력
; ============================================================

PrintCRLF:
    push ax
    push dx

    mov dl, 13
    mov ah, 02h
    int 21h

    mov dl, 10
    mov ah, 02h
    int 21h

    pop dx
    pop ax
    ret