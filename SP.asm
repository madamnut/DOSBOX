; ============================================================
; SP.ASM
;
; 목적:
;   1) DOS에서 명령행 인자로 WAV 파일명을 받는다
;   2) 예: SP SOUND.WAV
;   3) WAV 헤더를 검사한다
;   4) 지원 형식이면 Sound Blaster DSP direct DAC 방식으로 재생한다
;   5) 재생 후 종료한다
;
; 현재 버전의 지원 조건:
;   - RIFF/WAVE
;   - 기본 44바이트 WAV 헤더
;   - PCM (AudioFormat = 1)
;   - Mono (Channels = 1)
;   - 8-bit (BitsPerSample = 8)
;   - 8000 Hz (SampleRate = 8000)
;
; 주의:
;   - 이 코드는 "첫 실험용"이다
;   - DOSBox에서 Sound Blaster가 켜져 있다는 전제
;   - direct DAC + 간단한 지연 루프 방식이라
;     긴 파일 / 고품질 재생 / 정확한 타이밍을 위한 최종형은 아니다
;   - 더 제대로 하려면 다음 단계는 DMA/IRQ 방식으로 가야 한다
;
; 사용법:
;   SP SOUND.WAV
;
; 빌드:
;   nasm -f bin SP.ASM -o SP.COM
; ============================================================

org 100h
jmp start


; ============================================================
; 상수 정의
; ============================================================

SB_BASE        equ 0220h
; ------------------------------------------------------------
; Sound Blaster 기본 베이스 포트
; DOSBox 기본 설정이 보통 220h 이므로 이것으로 가정
; ------------------------------------------------------------

SB_RESET       equ SB_BASE + 6
; ------------------------------------------------------------
; DSP Reset 포트
; ------------------------------------------------------------

SB_READ        equ SB_BASE + 0Ah
; ------------------------------------------------------------
; DSP Data Read 포트
; ------------------------------------------------------------

SB_WRITE       equ SB_BASE + 0Ch
; ------------------------------------------------------------
; DSP Write 포트
; 동시에 Write Buffer Status도 이 포트로 확인한다
; bit7 = 1 이면 아직 못 씀
; bit7 = 0 이면 써도 됨
; ------------------------------------------------------------

SB_READSTATUS  equ SB_BASE + 0Eh
; ------------------------------------------------------------
; DSP Read Buffer Status 포트
; bit7 = 1 이면 읽을 데이터가 있음
; ------------------------------------------------------------

DELAY_CONST    equ 33
; ------------------------------------------------------------
; direct DAC 샘플 간 간단 지연 상수
;
; 이 값은 DOSBox 환경에 따라 약간 조정이 필요할 수 있다.
; 지금은 8000Hz 근처 실험용 값으로 둔다.
;
; 소리가 너무 빠르면 값을 늘리고,
; 너무 느리면 값을 줄인다.
; ------------------------------------------------------------

BUFFER_SIZE    equ 1024
; ------------------------------------------------------------
; WAV 데이터를 조금씩 읽을 버퍼 크기
; .COM 프로그램이므로 너무 크게 잡지 않는다
; ------------------------------------------------------------


; ============================================================
; 메시지 문자열
; ============================================================

MsgUsage db 13,10,'Usage: SP SOUND.WAV',13,10,'$'
; ------------------------------------------------------------
; 사용법 메시지
; ------------------------------------------------------------

MsgFileOpenError db 13,10,'File open error: $'
; ------------------------------------------------------------
; 파일 열기 실패 메시지 앞부분
; ------------------------------------------------------------

MsgReadError db 13,10,'File read error.$'
; ------------------------------------------------------------
; 파일 읽기 실패 메시지
; ------------------------------------------------------------

MsgNotWave db 13,10,'Error: not a supported WAV file.$'
; ------------------------------------------------------------
; WAV 구조가 아니거나 지원 형식이 아닐 때
; ------------------------------------------------------------

MsgNeedPCM8Mono8k db 13,10,'Need PCM 8-bit mono 8000Hz WAV.$'
; ------------------------------------------------------------
; 현재 지원 형식이 아닐 때
; ------------------------------------------------------------

MsgSBError db 13,10,'Sound Blaster not detected at 220h.$'
; ------------------------------------------------------------
; Sound Blaster DSP 초기화 실패 메시지
; ------------------------------------------------------------

MsgPlayDone db 13,10,'Done.$'
; ------------------------------------------------------------
; 재생 완료 메시지
; ------------------------------------------------------------


; ============================================================
; 데이터 버퍼
; ============================================================

FileNameBuffer db 13 dup(0)
; ------------------------------------------------------------
; DOS 8.3 파일명을 담을 버퍼
; 예: SOUND.WAV + null
; 최대 12자 + 0 = 13바이트
; ------------------------------------------------------------

HeaderBuffer db 44 dup(0)
; ------------------------------------------------------------
; 기본 WAV 헤더 44바이트를 읽어둘 버퍼
; ------------------------------------------------------------

DataBuffer db BUFFER_SIZE dup(0)
; ------------------------------------------------------------
; 샘플 데이터를 조금씩 읽어둘 버퍼
; ------------------------------------------------------------

FileHandle dw 0
; ------------------------------------------------------------
; 열린 WAV 파일 핸들 저장
; ------------------------------------------------------------

BytesRead dw 0
; ------------------------------------------------------------
; 마지막 읽기에서 실제로 읽은 바이트 수
; ------------------------------------------------------------

DataBytesLeftLo dw 0
DataBytesLeftHi dw 0
; ------------------------------------------------------------
; WAV data chunk의 남은 길이 저장
; dword를 hi/lo 두 워드로 나눠 저장
; ------------------------------------------------------------


; ============================================================
; 시작
; ============================================================

start:
    push cs
    pop ds
    ; --------------------------------------------------------
    ; DS를 프로그램 세그먼트로 맞춘다
    ; --------------------------------------------------------

    push cs
    pop es
    ; --------------------------------------------------------
    ; ES도 프로그램 세그먼트로 맞춘다
    ; --------------------------------------------------------

    call ParseFileName
    ; --------------------------------------------------------
    ; 명령행에서 파일명 하나를 읽어 FileNameBuffer에 저장
    ; 실패 시 CF=1
    ; --------------------------------------------------------

    jc show_usage
    ; --------------------------------------------------------
    ; 파일명이 없거나 형식이 이상하면 사용법 출력
    ; --------------------------------------------------------

    call OpenWavFile
    ; --------------------------------------------------------
    ; WAV 파일 열기
    ; --------------------------------------------------------

    jc file_open_error
    ; --------------------------------------------------------
    ; 파일 열기 실패
    ; --------------------------------------------------------

    call ReadAndValidateHeader
    ; --------------------------------------------------------
    ; 44바이트 헤더 읽기 + 형식 검사
    ; --------------------------------------------------------

    jc wav_error
    ; --------------------------------------------------------
    ; 헤더 이상 / 지원 안 되는 형식
    ; --------------------------------------------------------

    call SB_Init
    ; --------------------------------------------------------
    ; Sound Blaster DSP 초기화
    ; --------------------------------------------------------

    jc sb_error
    ; --------------------------------------------------------
    ; SB 감지 실패
    ; --------------------------------------------------------

    call SB_SpeakerOn
    ; --------------------------------------------------------
    ; DSP 스피커 ON
    ; --------------------------------------------------------

    call PlayWavData
    ; --------------------------------------------------------
    ; 실제 WAV 데이터 재생
    ; --------------------------------------------------------

    call SB_SpeakerOff
    ; --------------------------------------------------------
    ; 재생 끝나면 스피커 OFF
    ; --------------------------------------------------------

    call CloseFile
    ; --------------------------------------------------------
    ; 파일 닫기
    ; --------------------------------------------------------

    mov dx, MsgPlayDone
    call PrintString

    mov ax, 4C00h
    int 21h
    ; --------------------------------------------------------
    ; 정상 종료
    ; --------------------------------------------------------


show_usage:
    mov dx, MsgUsage
    call PrintString
    mov ax, 4C01h
    int 21h
    ; --------------------------------------------------------
    ; 사용법 오류 종료
    ; --------------------------------------------------------


file_open_error:
    mov dx, MsgFileOpenError
    call PrintString
    mov dx, FileNameBuffer
    call PrintAsciiz
    call PrintCRLF
    mov ax, 4C02h
    int 21h
    ; --------------------------------------------------------
    ; 파일 열기 실패 종료
    ; --------------------------------------------------------


wav_error:
    call CloseFile
    ; --------------------------------------------------------
    ; 파일이 열려 있으면 닫는다
    ; --------------------------------------------------------
    mov ax, 4C03h
    int 21h
    ; --------------------------------------------------------
    ; WAV 형식 오류 종료
    ; --------------------------------------------------------


sb_error:
    call CloseFile
    mov dx, MsgSBError
    call PrintString
    mov ax, 4C04h
    int 21h
    ; --------------------------------------------------------
    ; Sound Blaster 초기화 실패 종료
    ; --------------------------------------------------------


; ============================================================
; ParseFileName
;
; 목적:
;   PSP 명령행에서 파일명 하나를 받아 FileNameBuffer에 저장
;
; 형식:
;   SP SOUND.WAV
;
; 제한:
;   - 파일명 하나만 받는다
;   - 최대 12글자(8.3 형식 기준: 8 + '.' + 3)
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

ParseFileName:
    push ax
    push bx
    push si
    push di

    mov si, 81h
    ; --------------------------------------------------------
    ; PSP 명령행 시작 위치
    ; --------------------------------------------------------

    lea di, [FileNameBuffer]
    ; --------------------------------------------------------
    ; 저장 버퍼 시작 주소
    ; --------------------------------------------------------

    xor bx, bx
    ; --------------------------------------------------------
    ; BX = 저장한 글자 수
    ; --------------------------------------------------------

.skip_spaces:
    mov al, [si]
    cmp al, 13
    je .fail
    cmp al, ' '
    jne .read_name
    inc si
    jmp .skip_spaces
    ; --------------------------------------------------------
    ; 앞쪽 공백 제거
    ; --------------------------------------------------------

.read_name:
    mov al, [si]
    cmp al, ' '
    je .end_name
    cmp al, 13
    je .end_name

    cmp bx, 12
    jae .fail
    ; --------------------------------------------------------
    ; 최대 12자까지만 허용
    ; --------------------------------------------------------

    mov [di], al
    inc di
    inc si
    inc bx
    jmp .read_name

.end_name:
    cmp bx, 0
    je .fail
    ; --------------------------------------------------------
    ; 글자 수 0이면 실패
    ; --------------------------------------------------------

    mov byte [di], 0
    ; --------------------------------------------------------
    ; null 종료
    ; --------------------------------------------------------

.skip_tail_spaces:
    mov al, [si]
    cmp al, 13
    je .success
    cmp al, ' '
    jne .fail
    inc si
    jmp .skip_tail_spaces
    ; --------------------------------------------------------
    ; 뒤쪽에 두 번째 인자가 있으면 실패
    ; --------------------------------------------------------

.success:
    clc
    jmp .done

.fail:
    stc

.done:
    pop di
    pop si
    pop bx
    pop ax
    ret


; ============================================================
; OpenWavFile
;
; 목적:
;   FileNameBuffer의 파일을 읽기 전용으로 연다
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

OpenWavFile:
    mov dx, FileNameBuffer
    mov ax, 3D00h
    int 21h
    jc .fail

    mov [FileHandle], ax
    clc
    ret

.fail:
    stc
    ret


; ============================================================
; CloseFile
;
; 목적:
;   현재 열린 파일이 있으면 닫는다
; ============================================================

CloseFile:
    push ax
    push bx

    mov bx, [FileHandle]
    cmp bx, 0
    je .done
    ; --------------------------------------------------------
    ; 핸들이 0이면 열린 파일 없다고 간주
    ; --------------------------------------------------------

    mov ah, 3Eh
    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기
    ; --------------------------------------------------------

    mov word [FileHandle], 0

.done:
    pop bx
    pop ax
    ret


; ============================================================
; ReadExact
;
; 목적:
;   현재 열린 파일에서 CX 바이트를 읽는다
;
; 입력:
;   CX    = 읽을 바이트 수
;   DS:DX = 버퍼 주소
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;   BytesRead = 실제 읽은 바이트 수
; ============================================================

ReadExact:
    push ax
    push bx

    mov bx, [FileHandle]
    mov ah, 3Fh
    int 21h
    jc .fail

    mov [BytesRead], ax
    clc
    jmp .done

.fail:
    stc

.done:
    pop bx
    pop ax
    ret


; ============================================================
; ReadAndValidateHeader
;
; 목적:
;   WAV 헤더 44바이트를 읽고 지원 형식인지 검사
;
; 현재 지원 형식:
;   - "RIFF"
;   - "WAVE"
;   - "fmt "
;   - fmt size = 16
;   - PCM = 1
;   - Channels = 1
;   - SampleRate = 8000
;   - BitsPerSample = 8
;   - "data"
;
; 출력:
;   CF=0 성공
;   CF=1 실패
; ============================================================

ReadAndValidateHeader:
    push ax
    push bx
    push cx
    push dx
    push si

    mov dx, HeaderBuffer
    mov cx, 44
    call ReadExact
    jc .read_fail

    cmp word [BytesRead], 44
    jne .bad_wave
    ; --------------------------------------------------------
    ; 헤더 44바이트를 못 읽었으면 기본 WAV 형식 아님
    ; --------------------------------------------------------

    lea si, [HeaderBuffer]

    ; "RIFF"
    cmp byte [si+0], 'R'
    jne .bad_wave
    cmp byte [si+1], 'I'
    jne .bad_wave
    cmp byte [si+2], 'F'
    jne .bad_wave
    cmp byte [si+3], 'F'
    jne .bad_wave

    ; "WAVE"
    cmp byte [si+8], 'W'
    jne .bad_wave
    cmp byte [si+9], 'A'
    jne .bad_wave
    cmp byte [si+10], 'V'
    jne .bad_wave
    cmp byte [si+11], 'E'
    jne .bad_wave

    ; "fmt "
    cmp byte [si+12], 'f'
    jne .bad_wave
    cmp byte [si+13], 'm'
    jne .bad_wave
    cmp byte [si+14], 't'
    jne .bad_wave
    cmp byte [si+15], ' '
    jne .bad_wave

    ; fmt chunk size = 16
    cmp word [si+16], 16
    jne .bad_format
    cmp word [si+18], 0
    jne .bad_format

    ; AudioFormat = 1 (PCM)
    cmp word [si+20], 1
    jne .bad_format

    ; Channels = 1 (mono)
    cmp word [si+22], 1
    jne .bad_format

    ; SampleRate = 8000
    cmp word [si+24], 8000
    jne .bad_format
    cmp word [si+26], 0
    jne .bad_format

    ; BitsPerSample = 8
    cmp word [si+34], 8
    jne .bad_format

    ; "data"
    cmp byte [si+36], 'd'
    jne .bad_wave
    cmp byte [si+37], 'a'
    jne .bad_wave
    cmp byte [si+38], 't'
    jne .bad_wave
    cmp byte [si+39], 'a'
    jne .bad_wave

    ; data chunk size 저장
    mov ax, [si+40]
    mov [DataBytesLeftLo], ax
    mov ax, [si+42]
    mov [DataBytesLeftHi], ax

    clc
    jmp .done

.read_fail:
    mov dx, MsgReadError
    call PrintString
    stc
    jmp .done

.bad_wave:
    mov dx, MsgNotWave
    call PrintString
    stc
    jmp .done

.bad_format:
    mov dx, MsgNeedPCM8Mono8k
    call PrintString
    stc

.done:
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ret


; ============================================================
; SB_Init
;
; 목적:
;   Sound Blaster DSP를 reset하고 존재 여부 확인
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;
; 절차:
;   1) reset 포트에 1
;   2) 잠깐 대기
;   3) reset 포트에 0
;   4) read status bit7 대기
;   5) read port에서 AAh 확인
; ============================================================

SB_Init:
    push ax
    push cx
    push dx

    mov dx, SB_RESET
    mov al, 1
    out dx, al
    ; --------------------------------------------------------
    ; DSP reset 시작
    ; --------------------------------------------------------

    mov cx, 500
.delay1:
    loop .delay1
    ; --------------------------------------------------------
    ; 아주 짧은 대기
    ; --------------------------------------------------------

    mov al, 0
    out dx, al
    ; --------------------------------------------------------
    ; DSP reset 해제
    ; --------------------------------------------------------

    mov dx, SB_READSTATUS
    mov cx, 1000
.wait_data:
    in al, dx
    test al, 80h
    jnz .have_data
    loop .wait_data
    jmp .fail
    ; --------------------------------------------------------
    ; DSP가 응답할 때까지 대기
    ; --------------------------------------------------------

.have_data:
    mov dx, SB_READ
    in al, dx
    cmp al, 0AAh
    jne .fail
    ; --------------------------------------------------------
    ; DSP reset 성공 시 AAh 반환
    ; --------------------------------------------------------

    clc
    jmp .done

.fail:
    stc

.done:
    pop dx
    pop cx
    pop ax
    ret


; ============================================================
; SB_WaitWriteReady
;
; 목적:
;   DSP write buffer가 비워질 때까지 기다린다
; ============================================================

SB_WaitWriteReady:
    push ax
    push dx

    mov dx, SB_WRITE
.wait_loop:
    in al, dx
    test al, 80h
    jnz .wait_loop
    ; --------------------------------------------------------
    ; bit7=1 이면 아직 못 씀
    ; bit7=0 이면 써도 됨
    ; --------------------------------------------------------

    pop dx
    pop ax
    ret


; ============================================================
; SB_WriteDSP
;
; 목적:
;   AL 값을 DSP 명령/데이터로 쓴다
; ============================================================

SB_WriteDSP:
    push dx

    call SB_WaitWriteReady
    mov dx, SB_WRITE
    out dx, al
    ; --------------------------------------------------------
    ; AL을 DSP write 포트로 전송
    ; --------------------------------------------------------

    pop dx
    ret


; ============================================================
; SB_SpeakerOn / SB_SpeakerOff
;
; 목적:
;   DSP 스피커 온오프
; ============================================================

SB_SpeakerOn:
    mov al, 0D1h
    call SB_WriteDSP
    ret

SB_SpeakerOff:
    mov al, 0D3h
    call SB_WriteDSP
    ret


; ============================================================
; PlayWavData
;
; 목적:
;   WAV data chunk를 조금씩 읽으면서 샘플을 direct DAC로 출력
;
; 주의:
;   이 방식은 direct DAC + 소프트 지연 루프라
;   정밀한 최종형은 아니다.
;   하지만 DOSBox 실험용 첫 버전으로는 구조가 단순하다.
; ============================================================

PlayWavData:
    push ax
    push bx
    push cx
    push dx
    push si

.next_block:
    ; --------------------------------------------------------
    ; 남은 데이터가 0이면 종료
    ; --------------------------------------------------------
    mov ax, [DataBytesLeftLo]
    mov dx, [DataBytesLeftHi]
    or ax, dx
    jz .done

    ; --------------------------------------------------------
    ; 이번에 읽을 크기 결정
    ; min(남은 길이, BUFFER_SIZE)
    ; --------------------------------------------------------
    mov ax, [DataBytesLeftHi]
    or ax, ax
    jnz .full_buffer
    ; --------------------------------------------------------
    ; 상위워드가 0이면 남은 크기가 65535 이하
    ; --------------------------------------------------------

    mov ax, [DataBytesLeftLo]
    cmp ax, BUFFER_SIZE
    jbe .use_rest

.full_buffer:
    mov cx, BUFFER_SIZE
    jmp .read_block

.use_rest:
    mov cx, ax

.read_block:
    mov dx, DataBuffer
    call ReadExact
    jc .read_fail

    mov ax, [BytesRead]
    or ax, ax
    jz .done
    ; --------------------------------------------------------
    ; EOF 성격으로 0바이트면 종료
    ; --------------------------------------------------------

    ; --------------------------------------------------------
    ; 남은 데이터 길이 감소
    ; --------------------------------------------------------
    mov bx, ax
    ; BX = 이번에 읽은 실제 바이트 수

    mov ax, [DataBytesLeftLo]
    sub ax, bx
    mov [DataBytesLeftLo], ax

    mov ax, [DataBytesLeftHi]
    sbb ax, 0
    mov [DataBytesLeftHi], ax

    ; --------------------------------------------------------
    ; 버퍼 내 샘플들을 하나씩 direct DAC로 보냄
    ; --------------------------------------------------------
    lea si, [DataBuffer]
    mov cx, bx

.sample_loop:
    lodsb
    ; --------------------------------------------------------
    ; AL = 다음 샘플 (unsigned 8-bit PCM)
    ; --------------------------------------------------------

    push cx
    ; --------------------------------------------------------
    ; loop 카운터 보존
    ; --------------------------------------------------------

    mov ah, al
    ; --------------------------------------------------------
    ; 샘플 바이트를 AH에 백업
    ; 아래에서 DSP 명령 10h를 먼저 보내야 하므로 백업
    ; --------------------------------------------------------

    mov al, 10h
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; Direct DAC write 명령
    ; --------------------------------------------------------

    mov al, ah
    call SB_WriteDSP
    ; --------------------------------------------------------
    ; 실제 샘플 1바이트 출력
    ; --------------------------------------------------------

    call SampleDelay
    ; --------------------------------------------------------
    ; 다음 샘플 전 간단 지연
    ; 8000Hz 근처 재생 속도를 흉내냄
    ; --------------------------------------------------------

    pop cx
    loop .sample_loop

    jmp .next_block

.read_fail:
    mov dx, MsgReadError
    call PrintString

.done:
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ret


; ============================================================
; SampleDelay
;
; 목적:
;   샘플 사이 간단한 지연
;
; 설명:
;   direct DAC 자체는 자동 샘플링 클럭이 아니므로
;   샘플을 너무 빨리 밀어넣으면 소리가 비정상적으로 빨라진다.
;   그래서 매우 단순한 지연 루프를 넣는다.
;
; 주의:
;   이 값은 DOSBox 설정/속도에 따라 조금 조정될 수 있다.
; ============================================================

SampleDelay:
    push cx

    mov cx, DELAY_CONST
