; ============================================================
; PrintAsciiz
;
; 목적:
;   0(null) 종료 문자열을 한 글자씩 출력
;
; 입력:
;   DS:DX = ASCIIZ 문자열
; ============================================================

PrintAsciiz:
    push ax
    push dx
    push si

    mov si, dx

.next_char:
    lodsb
    test al, al
    jz .done

    mov dl, al
    mov ah, 02h
    int 21h
    jmp .next_char

.done:
    pop si
    pop dx
    pop ax
    ret


; ============================================================
; PrintCRLF
;
; 목적:
;   줄바꿈 출력
; ============================================================

PrintCRLF:
    push ax
    push dx

    mov dl, 13
    mov ah, 02h
    int 21h

    mov dl, 10
    mov ah, 02h
    int 21h

    pop dx
    pop ax
    ret