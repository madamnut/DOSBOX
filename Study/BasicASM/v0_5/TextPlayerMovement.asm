; ============================================================
; game.asm
;
; 목적:
;   1) 텍스트 화면에서 플레이어를 '@' 문자 하나로 표현한다
;   2) 방향키를 누르면 플레이어 위치가 바뀐다
;   3) ESC를 누르면 종료한다
;
; 이번 단계의 핵심:
;   이제부터는 단순히 "어떤 키가 눌렸는지 출력"하는 걸 넘어서,
;   실제로 화면상의 어떤 대상 하나를 움직이기 시작한다.
;
;   즉, 아주 원시적인 게임 구조가 시작된다:
;
;     입력 받기
;     좌표 변경
;     화면 다시 그리기
;     반복
;
;   여기서 '@' 문자가 플레이어라고 생각하면 된다.
;
; 중요한 구현 방식:
;   이번 예제는 텍스트 모드에서 BIOS 커서 이동 기능과
;   DOS 문자열 출력을 이용해서 화면에 글자를 찍는다.
;
;   아직 VGA 그래픽 모드는 아니다.
;   즉 "픽셀"이 아니라 "문자 칸" 단위로 움직인다.
;
; 빌드:
;   nasm -f bin game.asm -o game.com
;
; 실행:
;   DOSBox에서 game 또는 game.com
; ============================================================


org 100h
; ------------------------------------------------------------
; .COM 프로그램은 DOS가 보통 오프셋 0100h부터 적재한다.
; 따라서 어셈블러에게 주소 계산 기준을 100h로 잡으라고 알려준다.
; ------------------------------------------------------------


start:
; ------------------------------------------------------------
; 프로그램 시작 지점.
;
; 먼저 플레이어의 초기 위치를 설정하고,
; 첫 화면을 그린 다음,
; 반복 루프로 들어간다.
; ------------------------------------------------------------

    mov byte [player_x], 40
    ; --------------------------------------------------------
    ; 플레이어의 초기 X 좌표(열 번호)를 40으로 설정.
    ;
    ; 텍스트 화면은 보통 80열 x 25행이라고 생각하면 된다.
    ; 따라서 X=40이면 대략 화면 가로 중앙 근처다.
    ; --------------------------------------------------------

    mov byte [player_y], 12
    ; --------------------------------------------------------
    ; 플레이어의 초기 Y 좌표(행 번호)를 12로 설정.
    ;
    ; 25행 기준으로 대략 세로 중앙 근처다.
    ; --------------------------------------------------------

    call draw_screen
    ; --------------------------------------------------------
    ; 초기 화면을 한 번 그림.
    ; 이 시점에는 플레이어가 (40,12) 근처에 보인다.
    ; --------------------------------------------------------



main_loop:
; ------------------------------------------------------------
; 메인 반복 루프.
;
; 앞으로 프로그램은 계속 여기로 돌아온다.
;
; 흐름:
;   1) 키 입력 대기
;   2) ESC인지 확인
;   3) 방향키인지 확인
;   4) 플레이어 좌표 변경
;   5) 화면 다시 그림
;   6) 다시 루프
; ------------------------------------------------------------

    mov ah, 00h
    ; --------------------------------------------------------
    ; BIOS 키보드 인터럽트 int 16h 준비.
    ;
    ; AH=00h :
    ;   키 하나를 입력받을 때까지 기다린다.
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키 하나 입력받기.
    ;
    ; 일반적으로:
    ;   AL = ASCII 코드
    ;   AH = 스캔 코드
    ;
    ; ESC는 AL로 검사 가능.
    ; 방향키는 보통 AL=00h 이고 AH를 봐야 한다.
    ; --------------------------------------------------------


    cmp al, 1Bh
    ; --------------------------------------------------------
    ; ESC의 ASCII 코드(1Bh)와 비교.
    ; 즉 "ESC 눌렀나?" 확인.
    ; --------------------------------------------------------

    je exit_program
    ; --------------------------------------------------------
    ; ESC라면 종료 루틴으로 이동.
    ; --------------------------------------------------------


    cmp al, 00h
    ; --------------------------------------------------------
    ; 방향키 같은 특수키는 보통 AL=00h.
    ; 따라서 특수키인지 먼저 확인.
    ; --------------------------------------------------------

    jne main_loop
    ; --------------------------------------------------------
    ; AL이 00h가 아니면 방향키가 아니라고 보고,
    ; 그냥 아무것도 하지 않고 루프 처음으로 돌아간다.
    ;
    ; 즉 이번 예제에서는 문자키 같은 건 무시한다.
    ; --------------------------------------------------------


    cmp ah, 48h
    ; --------------------------------------------------------
    ; 위 방향키(Up Arrow) 스캔 코드인지 검사.
    ; --------------------------------------------------------

    je move_up

    cmp ah, 50h
    ; --------------------------------------------------------
    ; 아래 방향키(Down Arrow) 스캔 코드인지 검사.
    ; --------------------------------------------------------

    je move_down

    cmp ah, 4Bh
    ; --------------------------------------------------------
    ; 왼쪽 방향키(Left Arrow) 스캔 코드인지 검사.
    ; --------------------------------------------------------

    je move_left

    cmp ah, 4Dh
    ; --------------------------------------------------------
    ; 오른쪽 방향키(Right Arrow) 스캔 코드인지 검사.
    ; --------------------------------------------------------

    je move_right

    jmp main_loop
    ; --------------------------------------------------------
    ; 방향키 4종이 아니면 그냥 무시하고 루프로 복귀.
    ; --------------------------------------------------------



move_up:
; ------------------------------------------------------------
; 위 방향키를 눌렀을 때 플레이어를 위로 이동.
; ------------------------------------------------------------

    cmp byte [player_y], 3
    ; --------------------------------------------------------
    ; 현재 Y 좌표가 3인지 확인.
    ;
    ; 왜 3인가?
    ; 화면 윗부분에는 제목/안내 문구를 출력하고 있어서
    ; 그 아래쪽부터 플레이어가 움직이게 하려는 것이다.
    ;
    ; 즉 Y=3보다 위로는 못 올라가게 막는다.
    ; --------------------------------------------------------

    jbe redraw_and_continue
    ; --------------------------------------------------------
    ; jbe = Jump if Below or Equal
    ;
    ; 현재 Y가 3 이하이면 더 위로 못 가게 하고,
    ; 그냥 화면만 다시 그린 뒤 루프로 돌아간다.
    ; --------------------------------------------------------

    dec byte [player_y]
    ; --------------------------------------------------------
    ; Y 좌표를 1 감소시킨다.
    ;
    ; 텍스트 화면에서 Y가 작아질수록 위쪽으로 이동한 것.
    ; --------------------------------------------------------

    jmp redraw_and_continue
    ; --------------------------------------------------------
    ; 좌표 변경 후 화면 다시 그림.
    ; --------------------------------------------------------



move_down:
; ------------------------------------------------------------
; 아래 방향키를 눌렀을 때 플레이어를 아래로 이동.
; ------------------------------------------------------------

    cmp byte [player_y], 24
    ; --------------------------------------------------------
    ; Y가 24인지 검사.
    ;
    ; 텍스트 화면은 보통 0~24 행을 쓴다고 생각할 수 있다.
    ; 따라서 24를 하한선(최하단)으로 잡는다.
    ; --------------------------------------------------------

    jae redraw_and_continue
    ; --------------------------------------------------------
    ; jae = Jump if Above or Equal
    ;
    ; 이미 24 이상이면 더 아래로 못 내려가게 한다.
    ; --------------------------------------------------------

    inc byte [player_y]
    ; --------------------------------------------------------
    ; Y 좌표를 1 증가시킨다.
    ; 아래로 한 칸 이동.
    ; --------------------------------------------------------

    jmp redraw_and_continue



move_left:
; ------------------------------------------------------------
; 왼쪽 방향키를 눌렀을 때 플레이어를 왼쪽으로 이동.
; ------------------------------------------------------------

    cmp byte [player_x], 0
    ; --------------------------------------------------------
    ; X가 0인지 검사.
    ; 텍스트 화면의 가장 왼쪽을 0열로 생각.
    ; --------------------------------------------------------

    jbe redraw_and_continue
    ; --------------------------------------------------------
    ; 이미 0 이하이면 더 왼쪽으로 못 가게 막는다.
    ; --------------------------------------------------------

    dec byte [player_x]
    ; --------------------------------------------------------
    ; X를 1 감소시켜 왼쪽으로 한 칸 이동.
    ; --------------------------------------------------------

    jmp redraw_and_continue



move_right:
; ------------------------------------------------------------
; 오른쪽 방향키를 눌렀을 때 플레이어를 오른쪽으로 이동.
; ------------------------------------------------------------

    cmp byte [player_x], 79
    ; --------------------------------------------------------
    ; X가 79인지 검사.
    ;
    ; 텍스트 화면 80열 기준이면
    ; 열 인덱스는 0~79 범위로 볼 수 있다.
    ; --------------------------------------------------------

    jae redraw_and_continue
    ; --------------------------------------------------------
    ; 이미 맨 오른쪽이면 더 못 가게 막는다.
    ; --------------------------------------------------------

    inc byte [player_x]
    ; --------------------------------------------------------
    ; X를 1 증가시켜 오른쪽으로 한 칸 이동.
    ; --------------------------------------------------------

    jmp redraw_and_continue



redraw_and_continue:
; ------------------------------------------------------------
; 좌표가 바뀌었든 안 바뀌었든,
; 현재 상태를 바탕으로 화면을 다시 그린다.
; ------------------------------------------------------------

    call draw_screen
    ; --------------------------------------------------------
    ; 제목, 안내, 좌표 정보, 플레이어 문자를 포함한
    ; 전체 화면을 다시 그림.
    ; --------------------------------------------------------

    jmp main_loop
    ; --------------------------------------------------------
    ; 다시 입력 대기 루프로 돌아감.
    ; --------------------------------------------------------



exit_program:
; ------------------------------------------------------------
; ESC 눌렀을 때 종료 처리.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; BIOS 비디오 서비스에서
    ; 80x25 텍스트 모드 재설정.
    ;
    ; 화면을 깔끔한 텍스트 상태로 맞춘다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 호출
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 문자열 출력 기능 준비.
    ; --------------------------------------------------------

    mov dx, exit_msg
    ; --------------------------------------------------------
    ; 종료 메시지 주소 준비.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; "EXIT" 출력.
    ; --------------------------------------------------------

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 종료 기능 준비.
    ;
    ; AH=4Ch : 프로그램 종료
    ; AL=00h : 종료 코드 0
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 프로그램 종료.
    ; --------------------------------------------------------



draw_screen:
; ------------------------------------------------------------
; 화면 전체를 다시 그리는 서브루틴.
;
; 이 루틴이 하는 일:
;   1) 텍스트 모드 재설정으로 화면 초기화
;   2) 제목 출력
;   3) 안내 문구 출력
;   4) 현재 좌표 출력
;   5) 플레이어 위치로 커서를 이동
;   6) '@' 문자 출력
;
; 지금은 간단함을 위해
; "화면 전체를 날리고 다시 그리는 방식"을 사용한다.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; 80x25 텍스트 모드로 재설정.
    ; 결과적으로 화면이 거의 초기화되는 효과를 준다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 호출.
    ; --------------------------------------------------------

    mov ah, 09h
    mov dx, title_msg
    int 21h
    ; --------------------------------------------------------
    ; 제목 출력.
    ; --------------------------------------------------------

    mov ah, 09h
    mov dx, help_msg
    int 21h
    ; --------------------------------------------------------
    ; 안내 문구 출력.
    ; --------------------------------------------------------

    mov ah, 09h
    mov dx, coord_label_msg
    int 21h
    ; --------------------------------------------------------
    ; "X=" 라벨까지 포함된 좌표 안내 앞부분 출력.
    ; --------------------------------------------------------

    mov al, [player_x]
    ; --------------------------------------------------------
    ; 현재 플레이어 X 좌표 값을 AL에 가져온다.
    ; --------------------------------------------------------

    call print_two_digits
    ; --------------------------------------------------------
    ; AL 안의 0~99 값을 두 자리 십진수처럼 출력한다.
    ;
    ; 예:
    ;   5  -> 05
    ;   12 -> 12
    ;   40 -> 40
    ; --------------------------------------------------------

    mov ah, 09h
    mov dx, coord_mid_msg
    int 21h
    ; --------------------------------------------------------
    ; ", Y=" 같은 중간 문자열 출력.
    ; --------------------------------------------------------

    mov al, [player_y]
    ; --------------------------------------------------------
    ; 현재 플레이어 Y 좌표 값을 AL에 가져온다.
    ; --------------------------------------------------------

    call print_two_digits
    ; --------------------------------------------------------
    ; Y 좌표도 두 자리 숫자로 출력.
    ; --------------------------------------------------------

    mov ah, 02h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AH=02h 는 "커서 위치 설정" 기능이다.
    ; --------------------------------------------------------

    mov bh, 00h
    ; --------------------------------------------------------
    ; 페이지 번호.
    ; 일반적인 텍스트 화면에서 보통 0 페이지 사용.
    ; --------------------------------------------------------

    mov dh, [player_y]
    ; --------------------------------------------------------
    ; DH = 커서의 행(row)
    ; 즉 플레이어의 Y 좌표를 행으로 사용.
    ; --------------------------------------------------------

    mov dl, [player_x]
    ; --------------------------------------------------------
    ; DL = 커서의 열(column)
    ; 즉 플레이어의 X 좌표를 열로 사용.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; BIOS 비디오 서비스 호출.
    ; 커서를 플레이어 좌표 위치로 이동시킨다.
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 문자열 출력 기능 준비.
    ; --------------------------------------------------------

    mov dx, player_char_msg
    ; --------------------------------------------------------
    ; '@' 문자가 들어 있는 문자열 주소 준비.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 현재 커서 위치에 '@' 출력.
    ;
    ; 즉 플레이어가 화면에 보이는 부분이다.
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; draw_screen 종료.
    ; 호출한 곳으로 돌아간다.
    ; --------------------------------------------------------



print_two_digits:
; ------------------------------------------------------------
; AL에 들어 있는 0~99 값을
; 두 자리 십진수 문자로 출력하는 서브루틴.
;
; 예:
;   AL= 0  -> "00"
;   AL= 7  -> "07"
;   AL=12  -> "12"
;   AL=40  -> "40"
;
; 왜 필요하냐?
;   현재 좌표를 보기 좋게 화면에 출력하기 위해서다.
;
; 동작 원리:
;   1) 10으로 나눠서
;      - 몫 = 십의 자리
;      - 나머지 = 일의 자리
;   2) 각각에 '0'을 더해서 ASCII 숫자로 만든다
;   3) DOS의 문자 출력 기능으로 한 글자씩 출력한다
; ------------------------------------------------------------

    xor ah, ah
    ; --------------------------------------------------------
    ; 8비트 나눗셈(div r/m8)을 하기 전,
    ; AX 전체를 dividend로 사용하므로
    ; AH를 0으로 만들어 AX 값을 정리한다.
    ;
    ; 즉 현재 AX는:
    ;   AH = 0
    ;   AL = 출력할 값
    ; 이 상태가 된다.
    ; --------------------------------------------------------

    mov bl, 10
    ; --------------------------------------------------------
    ; 10으로 나눌 것이므로 BL에 10 저장.
    ; --------------------------------------------------------

    div bl
    ; --------------------------------------------------------
    ; AX를 BL(10)로 나눈다.
    ;
    ; 8비트 나눗셈 결과:
    ;   AL = 몫  (십의 자리)
    ;   AH = 나머지 (일의 자리)
    ;
    ; 예를 들어 AX=40이면:
    ;   AL=4
    ;   AH=0
    ; --------------------------------------------------------

    mov [digit_tens], al
    ; --------------------------------------------------------
    ; 십의 자리 숫자값 임시 저장.
    ; --------------------------------------------------------

    mov [digit_ones], ah
    ; --------------------------------------------------------
    ; 일의 자리 숫자값 임시 저장.
    ; --------------------------------------------------------

    mov dl, [digit_tens]
    ; --------------------------------------------------------
    ; 십의 자리 값을 DL로 가져옴.
    ; --------------------------------------------------------

    add dl, '0'
    ; --------------------------------------------------------
    ; 숫자값 0~9를 ASCII 문자 '0'~'9'로 변환.
    ;
    ; 예:
    ;   4 + '0' = 문자 '4'
    ; --------------------------------------------------------

    mov ah, 02h
    ; --------------------------------------------------------
    ; DOS int 21h 에서 AH=02h 는
    ; DL에 들어 있는 한 문자를 출력하는 기능.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 십의 자리 한 글자 출력.
    ; --------------------------------------------------------

    mov dl, [digit_ones]
    ; --------------------------------------------------------
    ; 일의 자리 숫자값을 DL로 가져옴.
    ; --------------------------------------------------------

    add dl, '0'
    ; --------------------------------------------------------
    ; ASCII 숫자 문자로 변환.
    ; --------------------------------------------------------

    mov ah, 02h
    ; --------------------------------------------------------
    ; 한 글자 출력 기능 준비.
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 일의 자리 출력.
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; print_two_digits 종료.
    ; --------------------------------------------------------



player_x db 0
; ------------------------------------------------------------
; 플레이어의 현재 X 좌표(열)를 저장하는 변수.
; 0~79 범위를 기대한다.
; ------------------------------------------------------------

player_y db 0
; ------------------------------------------------------------
; 플레이어의 현재 Y 좌표(행)를 저장하는 변수.
; 0~24 범위를 기대한다.
; ------------------------------------------------------------

digit_tens db 0
; ------------------------------------------------------------
; 두 자리 숫자 출력 서브루틴에서
; 십의 자리를 잠깐 저장하는 임시 변수.
; ------------------------------------------------------------

digit_ones db 0
; ------------------------------------------------------------
; 두 자리 숫자 출력 서브루틴에서
; 일의 자리를 잠깐 저장하는 임시 변수.
; ------------------------------------------------------------

title_msg db 'DOS TEXT PLAYER MOVE', 13, 10, '$'
; ------------------------------------------------------------
; 화면 제목.
; ------------------------------------------------------------

help_msg db 'ARROW KEYS = MOVE @ / ESC = QUIT', 13, 10, '$'
; ------------------------------------------------------------
; 사용 안내 문구.
; ------------------------------------------------------------

coord_label_msg db 'X=$'
; ------------------------------------------------------------
; 좌표 출력용 앞부분 문자열.
; ------------------------------------------------------------

coord_mid_msg db ', Y=$'
; ------------------------------------------------------------
; 좌표 출력용 중간 문자열.
; ------------------------------------------------------------

player_char_msg db '@$'
; ------------------------------------------------------------
; 플레이어를 나타내는 문자열.
; '@' 하나만 출력하면 되지만,
; int 21h / AH=09h 출력 규칙에 맞게 '$'로 끝낸다.
; ------------------------------------------------------------

exit_msg db 'EXIT$'
; ------------------------------------------------------------
; 종료 메시지.
; ------------------------------------------------------------