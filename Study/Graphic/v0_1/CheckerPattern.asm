; ============================================================
; game.asm
;
; 목적:
;   1) VGA Mode 13h (320x200, 256색) 그래픽 모드로 들어간다
;   2) 화면 전체를 1픽셀 단위 흰색/검정색 체커보드 패턴으로 채운다
;   3) ESC 키를 누를 때까지 화면을 유지한다
;   4) ESC를 누르면 텍스트 모드로 복귀하고 종료한다
;
; 이번 단계의 핵심:
;   이전에는 8x8 블록 단위의 큰 격자를 만들려 했지만,
;   이번에는 진짜 "픽셀 하나 단위"로 흰색/검정색이 번갈아 나오게 한다.
;
; 즉 각 픽셀마다:
;   (x + y)가 짝수면 흰색
;   (x + y)가 홀수면 검정색
;
; 화면 설정:
;   Mode 13h
;   - 해상도: 320 x 200
;   - 색상: 256색
;   - 픽셀 1개 = 1바이트
;
; 색상:
;   - 검정색 = 00h
;   - 흰색   = 0Fh
;
; 종료:
;   - ESC만 종료 키
;   - 다른 키는 무시
; ============================================================


org 100h
; ------------------------------------------------------------
; .COM 프로그램은 DOS가 보통 메모리 오프셋 0100h부터 적재한다.
; 따라서 어셈블러에게 기준 주소를 100h로 잡으라고 알려준다.
; ------------------------------------------------------------


start:
; ------------------------------------------------------------
; 프로그램 시작 지점
; ------------------------------------------------------------

    mov ax, 0013h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AX=0013h 는 VGA Mode 13h 설정이다.
    ;
    ; 즉:
    ;   320x200
    ;   256색
    ; 그래픽 모드로 전환한다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 실제로 그래픽 모드 13h로 진입한다.
    ; --------------------------------------------------------


    mov ax, 0A000h
    ; --------------------------------------------------------
    ; VGA Mode 13h의 비디오 메모리 세그먼트는 A000h 이다.
    ; --------------------------------------------------------

    mov es, ax
    ; --------------------------------------------------------
    ; ES를 A000h로 맞춘다.
    ;
    ; 이제 ES:DI에 값을 쓰면 화면 메모리에 직접 쓰는 것이다.
    ; --------------------------------------------------------


    xor bx, bx
    ; --------------------------------------------------------
    ; BX를 0으로 만든다.
    ;
    ; 여기서는 BX를 현재 y 좌표처럼 사용한다.
    ; 즉:
    ;   BX = 현재 줄 번호
    ; --------------------------------------------------------



y_loop:
; ------------------------------------------------------------
; y = 0 부터 199까지, 각 화면 줄을 처리하는 바깥 루프
; ------------------------------------------------------------

    cmp bx, 200
    ; --------------------------------------------------------
    ; 현재 y가 200에 도달했는지 검사.
    ; Mode 13h의 세로 해상도는 200이므로
    ; 유효한 y 범위는 0 ~ 199 이다.
    ; --------------------------------------------------------

    jae draw_done
    ; --------------------------------------------------------
    ; y가 200 이상이면 화면 전체를 다 그린 것이므로 그리기 종료.
    ; --------------------------------------------------------


    mov ax, bx
    ; --------------------------------------------------------
    ; 현재 y 값을 AX로 복사한다.
    ; 이후 이 값을 이용해 "현재 줄의 시작 오프셋"을 계산할 것이다.
    ; --------------------------------------------------------

    mov dx, 320
    ; --------------------------------------------------------
    ; 한 줄의 픽셀 수는 320개다.
    ; 따라서 줄 시작 위치는 y * 320 이다.
    ; --------------------------------------------------------

    mul dx
    ; --------------------------------------------------------
    ; AX * DX 수행.
    ;
    ; 현재:
    ;   AX = y
    ;   DX = 320
    ;
    ; 결과:
    ;   DX:AX = y * 320
    ;
    ; 여기서는 값이 충분히 작으므로 AX만 사용하면 된다.
    ; --------------------------------------------------------

    mov di, ax
    ; --------------------------------------------------------
    ; 현재 줄(y)의 시작 화면 메모리 오프셋을 DI에 저장한다.
    ;
    ; 즉:
    ;   ES:DI = (0, y) 픽셀 위치
    ; --------------------------------------------------------


    xor cx, cx
    ; --------------------------------------------------------
    ; CX를 0으로 만든다.
    ;
    ; 여기서는 CX를 현재 x 좌표처럼 사용한다.
    ; 즉:
    ;   CX = 현재 열 번호
    ; --------------------------------------------------------



x_loop:
; ------------------------------------------------------------
; x = 0 부터 319까지, 한 줄 안의 각 픽셀을 처리하는 안쪽 루프
; ------------------------------------------------------------

    cmp cx, 320
    ; --------------------------------------------------------
    ; 현재 x가 320에 도달했는지 검사.
    ; Mode 13h의 가로 해상도는 320이므로
    ; 유효한 x 범위는 0 ~ 319 이다.
    ; --------------------------------------------------------

    jae next_row
    ; --------------------------------------------------------
    ; x가 320 이상이면 현재 줄 처리가 끝난 것이므로
    ; 다음 y 줄로 넘어간다.
    ; --------------------------------------------------------


    mov ax, cx
    ; --------------------------------------------------------
    ; 현재 x 값을 AX로 복사한다.
    ; --------------------------------------------------------

    add ax, bx
    ; --------------------------------------------------------
    ; AX = x + y
    ;
    ; 이 값의 짝/홀을 이용해서 흰색/검정색을 결정할 것이다.
    ;
    ; 체커보드 규칙:
    ;   x + y 가 짝수 -> 흰색
    ;   x + y 가 홀수 -> 검정색
    ; --------------------------------------------------------

    and ax, 1
    ; --------------------------------------------------------
    ; AX의 최하위 비트만 남긴다.
    ;
    ; 결과:
    ;   AX = 0 이면 짝수
    ;   AX = 1 이면 홀수
    ; --------------------------------------------------------

    cmp ax, 0
    ; --------------------------------------------------------
    ; 짝수인지 검사
    ; --------------------------------------------------------

    je pixel_white
    ; --------------------------------------------------------
    ; 짝수면 흰색 픽셀 처리로 이동
    ; --------------------------------------------------------


pixel_black:
; ------------------------------------------------------------
; 현재 픽셀을 검정색으로 칠하는 구간
; ------------------------------------------------------------

    mov al, 00h
    ; --------------------------------------------------------
    ; Mode 13h 팔레트에서 00h는 기본적으로 검정색이다.
    ; --------------------------------------------------------

    jmp write_pixel
    ; --------------------------------------------------------
    ; 색 값을 준비했으니 실제 화면 메모리에 쓰는 공통 구간으로 이동
    ; --------------------------------------------------------


pixel_white:
; ------------------------------------------------------------
; 현재 픽셀을 흰색으로 칠하는 구간
; ------------------------------------------------------------

    mov al, 0Fh
    ; --------------------------------------------------------
    ; 0Fh는 전통적으로 밝은 흰색 계열로 보기 쉬운 값이다.
    ; --------------------------------------------------------



write_pixel:
; ------------------------------------------------------------
; AL에 들어 있는 색 값을 실제 현재 픽셀 위치에 기록하는 공통 구간
; ------------------------------------------------------------

    mov [es:di], al
    ; --------------------------------------------------------
    ; 현재 화면 메모리 위치 ES:DI에 색 값을 기록한다.
    ;
    ; 이 1바이트가 바로 현재 픽셀 하나의 색이 된다.
    ; --------------------------------------------------------

    inc di
    ; --------------------------------------------------------
    ; 다음 픽셀 위치로 이동.
    ;
    ; Mode 13h는 1픽셀 = 1바이트 이므로
    ; 다음 픽셀은 메모리에서도 바로 다음 바이트다.
    ; --------------------------------------------------------

    inc cx
    ; --------------------------------------------------------
    ; x 좌표도 1 증가시킨다.
    ; --------------------------------------------------------

    jmp x_loop
    ; --------------------------------------------------------
    ; 다음 픽셀 처리로 반복
    ; --------------------------------------------------------



next_row:
; ------------------------------------------------------------
; 한 줄의 픽셀 320개를 모두 처리한 뒤 다음 줄로 넘어가는 구간
; ------------------------------------------------------------

    inc bx
    ; --------------------------------------------------------
    ; y 좌표를 1 증가시켜 다음 줄로 이동
    ; --------------------------------------------------------

    jmp y_loop
    ; --------------------------------------------------------
    ; 다음 줄 처리 시작
    ; --------------------------------------------------------



draw_done:
; ------------------------------------------------------------
; 화면 전체를 다 그린 뒤 ESC를 기다리는 루프
; ------------------------------------------------------------


wait_for_esc:
; ------------------------------------------------------------
; ESC가 눌릴 때까지 키 입력을 계속 기다린다.
; 다른 키는 무시한다.
; ------------------------------------------------------------

    mov ah, 00h
    ; --------------------------------------------------------
    ; BIOS 키보드 인터럽트 int 16h 준비
    ;
    ; AH=00h:
    ;   키 하나를 입력받을 때까지 대기
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키 하나를 입력받는다.
    ;
    ; AL = ASCII 코드
    ; 이번엔 ESC만 확인하면 되므로 AL만 보면 된다.
    ; --------------------------------------------------------

    cmp al, 1Bh
    ; --------------------------------------------------------
    ; ESC의 ASCII 코드 1Bh 와 비교
    ; --------------------------------------------------------

    jne wait_for_esc
    ; --------------------------------------------------------
    ; ESC가 아니면 다시 기다린다.
    ; --------------------------------------------------------



exit_program:
; ------------------------------------------------------------
; ESC를 눌렀을 때 종료 처리
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h 에서
    ; AX=0003h 는 80x25 텍스트 모드 복귀를 의미한다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 그래픽 모드에서 텍스트 모드로 복귀
    ; --------------------------------------------------------

    mov ah, 09h
    ; --------------------------------------------------------
    ; DOS 문자열 출력 기능 준비
    ; --------------------------------------------------------

    mov dx, exit_msg
    ; --------------------------------------------------------
    ; 종료 메시지 주소 준비
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; "EXIT" 출력
    ; --------------------------------------------------------

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 종료 기능 준비
    ;
    ; AH=4Ch : 종료
    ; AL=00h : 종료 코드 0
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 프로그램 종료
    ; --------------------------------------------------------



exit_msg db 'EXIT$'
; ------------------------------------------------------------
; 종료 메시지 문자열
;
; DOS int 21h / AH=09h 문자열 출력은
; '$'를 만날 때까지 출력하므로 끝에 '$'를 붙인다.
; ------------------------------------------------------------