; ============================================================
; hello.asm
;
; 목적:
;   1) DOS에서 팔레트 파일(HELLO.PAL)을 연다
;   2) DOS에서 이미지 파일(HELLO.RAW)을 연다
;   3) VGA Mode 13h (320x200, 256색) 로 들어간다
;   4) HELLO.PAL 의 768바이트를 VGA 팔레트에 적용한다
;   5) HELLO.RAW 의 64000바이트를 화면 메모리(A000:0000)에 출력한다
;   6) ESC를 누르면 텍스트 모드로 복귀하고 DOS로 정상 종료한다
;
; 전제:
;   - HELLO.RAW 는 반드시 320x200, 256색, 헤더 없는 RAW 파일이어야 한다
;   - HELLO.RAW 크기는 정확히 64000바이트여야 한다
;   - HELLO.PAL 은 256색 * RGB 3바이트 = 총 768바이트여야 한다
;   - HELLO.PAL 의 RGB 값은 파이썬에서 저장된 0~255 범위라고 가정한다
;   - VGA DAC는 0~63 범위를 쓰므로, 출력 전에 4로 나눠야 한다
;
; 핵심 구조:
;   1) 먼저 HELLO.PAL 을 프로그램 메모리의 PaletteBuffer 로 읽는다
;   2) Mode 13h 로 들어간다
;   3) PaletteBuffer 의 내용을 VGA 포트 3C8h / 3C9h 로 출력한다
;   4) HELLO.RAW 는 4096바이트씩 나눠 읽어서 A000:0000 으로 복사한다
;   5) ESC를 기다린 뒤 텍스트 모드로 돌아가 DOS 종료 함수로 끝낸다
;
; 왜 RAW를 한 번에 안 읽나?
;   .COM 프로그램은 메모리가 넉넉하지 않다.
;   64000바이트 전체를 별도 버퍼에 두는 것보다,
;   작은 버퍼(4096바이트)로 조금씩 읽어서 VRAM에 복사하는 게 안전하다.
; ============================================================

org 100h
; ------------------------------------------------------------
; .COM 프로그램 기준 시작 오프셋을 0100h로 맞춘다.
; DOS는 .COM 파일을 메모리에 올릴 때 보통 100h부터 코드가 시작된다.
; ------------------------------------------------------------

jmp start
; ------------------------------------------------------------
; 데이터 영역보다 먼저 실행 코드로 점프한다.
; .COM에서는 코드와 데이터가 한 세그먼트 안에 섞여 있으므로
; 보통 시작 부분에서 main 루틴으로 건너뛴다.
; ------------------------------------------------------------


; ============================================================
; 데이터 영역
; ============================================================

ImageFileName db 'HELLO.RAW',0
; ------------------------------------------------------------
; 열 이미지 파일 이름
; DOS int 21h / AH=3Dh 파일 열기 함수는
; 0으로 끝나는 문자열(ASCIIZ)을 사용한다.
; ------------------------------------------------------------

PaletteFileName db 'HELLO.PAL',0
; ------------------------------------------------------------
; 열 팔레트 파일 이름
; 이것도 0으로 끝나는 문자열이다.
; ------------------------------------------------------------

MsgPalError db 'Failed to load HELLO.PAL$'
; ------------------------------------------------------------
; 팔레트 파일 로딩 실패 시 출력할 메시지
; DOS int 21h / AH=09h 문자열 출력은
; 문자열 끝을 '$' 로 구분한다.
; ------------------------------------------------------------

MsgRawError db 'Failed to load HELLO.RAW$'
; ------------------------------------------------------------
; 이미지 파일 로딩 실패 시 출력할 메시지
; 이것도 '$' 종료 문자열이다.
; ------------------------------------------------------------

PaletteBuffer db 768 dup(0)
; ------------------------------------------------------------
; HELLO.PAL 의 내용을 읽어둘 메모리 버퍼
; 팔레트는 256색 * RGB 3바이트 = 총 768바이트
; 따라서 정확히 768바이트를 확보한다.
; ------------------------------------------------------------

ReadBuffer db 4096 dup(0)
; ------------------------------------------------------------
; HELLO.RAW 를 조금씩 읽을 임시 버퍼
; 한 번에 4096바이트씩 읽어서 VRAM으로 복사할 것이다.
; ------------------------------------------------------------

FileHandle dw 0
; ------------------------------------------------------------
; DOS 파일 핸들을 저장할 변수
; int 21h / AH=3Dh 파일 열기 성공 시 AX에 핸들이 들어온다.
; 그 값을 여기에 저장해두고 나중에 닫을 때 사용한다.
; ------------------------------------------------------------

BytesLeft dw 0
; ------------------------------------------------------------
; RAW 파일에서 아직 읽어야 할 남은 바이트 수
; 처음에는 64000으로 시작하고,
; 매 루프마다 ChunkSize만큼 줄어든다.
; ------------------------------------------------------------

ChunkSize dw 0
; ------------------------------------------------------------
; 이번 루프에서 실제로 읽을 크기
; 보통 4096이지만, 마지막 조각은 그보다 작을 수 있다.
; ------------------------------------------------------------



; ============================================================
; 프로그램 시작
; ============================================================

start:
; ------------------------------------------------------------
; 프로그램 시작 지점
; ------------------------------------------------------------

    push cs
    pop ds
    ; --------------------------------------------------------
    ; DS를 우리 프로그램 세그먼트로 맞춘다.
    ; .COM에서는 보통 시작 시 CS=DS 이지만,
    ; 명시적으로 맞춰두는 편이 안전하다.
    ; --------------------------------------------------------

    push cs
    pop es
    ; --------------------------------------------------------
    ; ES도 프로그램 세그먼트로 맞춘다.
    ; 나중에 LoadExactFile 에서 ES:BX 버퍼를 쓰므로
    ; 지금 미리 맞춰두면 편하다.
    ; --------------------------------------------------------


    lea dx, [PaletteFileName]
    ; --------------------------------------------------------
    ; DX에 HELLO.PAL 파일명 문자열 주소를 넣는다.
    ; --------------------------------------------------------

    lea bx, [PaletteBuffer]
    ; --------------------------------------------------------
    ; BX에 팔레트 데이터를 저장할 목적지 버퍼 주소를 넣는다.
    ; 즉 ES:BX = PaletteBuffer 가 된다.
    ; --------------------------------------------------------

    mov cx, 768
    ; --------------------------------------------------------
    ; HELLO.PAL 은 정확히 768바이트여야 한다.
    ; 따라서 정확히 768바이트를 읽도록 지정한다.
    ; --------------------------------------------------------

    call LoadExactFile
    ; --------------------------------------------------------
    ; HELLO.PAL 을 읽는 서브루틴 호출
    ;
    ; 입력:
    ;   DS:DX = 파일명
    ;   ES:BX = 목적지 버퍼
    ;   CX    = 읽을 바이트 수
    ;
    ; 출력:
    ;   CF=0 성공
    ;   CF=1 실패
    ; --------------------------------------------------------

    jc pal_error
    ; --------------------------------------------------------
    ; Carry Flag가 세트되어 있으면 팔레트 로딩 실패
    ; pal_error 로 이동한다.
    ; --------------------------------------------------------


    mov ax, 0013h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h
    ; AX=0013h -> VGA Mode 13h 설정
    ;
    ; 즉:
    ;   320x200
    ;   256색
    ; 그래픽 모드로 전환한다.
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 실제로 그래픽 모드로 들어간다.
    ; --------------------------------------------------------


    call ApplyPalette
    ; --------------------------------------------------------
    ; 방금 읽은 PaletteBuffer 내용을 VGA DAC에 적용한다.
    ;
    ; HELLO.PAL 안의 RGB 값은 0~255 범위라고 가정한다.
    ; VGA DAC는 0~63 범위를 받으므로
    ; 각 바이트를 4로 나눈 뒤 출력한다.
    ; --------------------------------------------------------


    call LoadImageToVRAM
    ; --------------------------------------------------------
    ; HELLO.RAW 파일을 열고
    ; 4096바이트씩 읽어서 A000:0000 화면 메모리에 복사한다.
    ;
    ; 출력:
    ;   CF=0 성공
    ;   CF=1 실패
    ; --------------------------------------------------------

    jc raw_error_graphics
    ; --------------------------------------------------------
    ; 그래픽 모드에서 RAW 로딩 실패 시
    ; 먼저 텍스트 모드로 돌아간 뒤 메시지를 출력해야 하므로
    ; raw_error_graphics 로 이동한다.
    ; --------------------------------------------------------



main_loop:
; ------------------------------------------------------------
; ESC 키를 누를 때까지 대기
; 다른 키는 무시
; ------------------------------------------------------------

    mov ah, 01h
    ; --------------------------------------------------------
    ; BIOS 키보드 확인 기능
    ; AH=01h -> 키가 있는지 검사
    ;
    ; 키가 없으면 ZF=1
    ; 키가 있으면 ZF=0
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키보드 상태 확인
    ; --------------------------------------------------------

    jz main_loop
    ; --------------------------------------------------------
    ; 아직 키가 없으면 계속 루프
    ; --------------------------------------------------------


    mov ah, 00h
    ; --------------------------------------------------------
    ; BIOS 키보드 입력 기능
    ; AH=00h -> 키 하나를 실제로 가져온다
    ; --------------------------------------------------------

    int 16h
    ; --------------------------------------------------------
    ; 키 하나를 읽는다.
    ; ASCII 코드는 AL에 들어온다.
    ; --------------------------------------------------------

    cmp al, 27
    ; --------------------------------------------------------
    ; ESC는 ASCII 코드 27, 즉 1Bh 이다.
    ; --------------------------------------------------------

    jne main_loop
    ; --------------------------------------------------------
    ; ESC가 아니면 다시 대기 루프로 돌아간다.
    ; --------------------------------------------------------



exit_to_text:
; ------------------------------------------------------------
; 정상 종료 전 텍스트 모드로 복귀한 뒤
; DOS 종료 함수로 프로그램을 끝낸다.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; BIOS 비디오 인터럽트 int 10h
    ; AX=0003h -> 80x25 텍스트 모드 복귀
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 실제로 텍스트 모드로 돌아간다.
    ; --------------------------------------------------------

    mov ax, 4C00h
    ; --------------------------------------------------------
    ; DOS 종료 함수
    ;
    ; AH=4Ch : 프로그램 종료
    ; AL=00h : 종료 코드 0
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; DOS로 정상 복귀
    ; --------------------------------------------------------



pal_error:
; ------------------------------------------------------------
; HELLO.PAL 로딩 실패 처리
; 아직 그래픽 모드에 들어가기 전이므로
; 그냥 텍스트에서 메시지를 출력하면 된다.
; ------------------------------------------------------------

    mov dx, MsgPalError
    ; --------------------------------------------------------
    ; 출력할 문자열 주소를 DX에 넣는다.
    ; --------------------------------------------------------

    call PrintString
    ; --------------------------------------------------------
    ; '$' 종료 문자열 출력
    ; --------------------------------------------------------

    mov ax, 4C01h
    ; --------------------------------------------------------
    ; DOS 종료 함수
    ;
    ; AH=4Ch : 종료
    ; AL=01h : 오류 종료 코드 1
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 에러 종료
    ; --------------------------------------------------------



raw_error_graphics:
; ------------------------------------------------------------
; HELLO.RAW 로딩 실패 처리
; 이 시점은 이미 그래픽 모드이므로
; 먼저 텍스트 모드로 돌아가야 메시지가 정상적으로 보인다.
; ------------------------------------------------------------

    mov ax, 0003h
    ; --------------------------------------------------------
    ; 텍스트 모드 복귀 준비
    ; --------------------------------------------------------

    int 10h
    ; --------------------------------------------------------
    ; 실제로 텍스트 모드로 돌아간다.
    ; --------------------------------------------------------


raw_error:
; ------------------------------------------------------------
; RAW 파일 에러 메시지 출력
; ------------------------------------------------------------

    mov dx, MsgRawError
    ; --------------------------------------------------------
    ; 출력할 문자열 주소를 DX에 넣는다.
    ; --------------------------------------------------------

    call PrintString
    ; --------------------------------------------------------
    ; '$' 종료 문자열 출력
    ; --------------------------------------------------------

    mov ax, 4C02h
    ; --------------------------------------------------------
    ; DOS 종료 함수
    ;
    ; AH=4Ch : 종료
    ; AL=02h : 오류 종료 코드 2
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 에러 종료
    ; --------------------------------------------------------



; ============================================================
; LoadExactFile
;
; 목적:
;   파일을 열고 정확히 지정한 크기만큼 읽어서 버퍼에 저장한다.
;
; 입력:
;   DS:DX = 파일명 문자열 주소
;   ES:BX = 읽어들일 목적지 버퍼
;   CX    = 읽어야 하는 바이트 수
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;
; 주의:
;   int 21h / AH=3Fh 파일 읽기는 버퍼 주소를 DS:DX 로 받는다.
;   그런데 지금 우리 목적지는 ES:BX 로 들어온다.
;
;   따라서 이 루틴 안에서 잠깐 DS를 ES로 바꿔서
;   DS:DX = 목적지 버퍼가 되도록 맞춘 뒤 읽는다.
; ============================================================

LoadExactFile:
; ------------------------------------------------------------
; 일반 파일 하나를 정확한 크기로 읽는 루틴
; ------------------------------------------------------------

    push ax
    ; --------------------------------------------------------
    ; 아래에서 AX를 사용하므로 원래 값 보존
    ; --------------------------------------------------------

    push bx
    ; --------------------------------------------------------
    ; BX 보존
    ; --------------------------------------------------------

    push cx
    ; --------------------------------------------------------
    ; CX 보존
    ; --------------------------------------------------------

    push dx
    ; --------------------------------------------------------
    ; DX 보존
    ; --------------------------------------------------------

    push si
    ; --------------------------------------------------------
    ; SI도 내부에서 임시 보관용으로 사용할 것이므로 저장
    ; --------------------------------------------------------

    push di
    ; --------------------------------------------------------
    ; DI 보존
    ; --------------------------------------------------------


    mov ax, 3D00h
    ; --------------------------------------------------------
    ; DOS int 21h 파일 열기 기능
    ;
    ; AH=3Dh : 파일 열기
    ; AL=00h : 읽기 전용
    ; DS:DX    : 파일명
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 열기 수행
    ;
    ; 성공하면:
    ;   AX = 파일 핸들
    ;
    ; 실패하면:
    ;   CF = 1
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 실패하면 바로 공통 실패 경로로 간다.
    ; --------------------------------------------------------

    mov [FileHandle], ax
    ; --------------------------------------------------------
    ; 나중에 닫을 수 있도록 파일 핸들을 저장한다.
    ; --------------------------------------------------------


    mov si, ds
    ; --------------------------------------------------------
    ; 원래 DS 값을 SI에 백업한다.
    ; 나중에 반드시 복구해야 한다.
    ; --------------------------------------------------------

    push es
    pop ds
    ; --------------------------------------------------------
    ; DS를 ES로 바꾼다.
    ;
    ; 이유:
    ;   DOS 파일 읽기 함수(int 21h / AH=3Fh)는
    ;   데이터를 DS:DX 버퍼에 써넣는다.
    ;
    ; 지금 목적지 버퍼는 원래 ES:BX였으므로,
    ; DS를 ES로 맞춰주면 DS:BX가 같은 위치를 가리키게 된다.
    ; --------------------------------------------------------

    mov dx, bx
    ; --------------------------------------------------------
    ; 이제 DS:DX = 목적지 버퍼 가 되도록
    ; DX에 BX 값을 넣는다.
    ; --------------------------------------------------------

    mov bx, [cs:FileHandle]
    ; --------------------------------------------------------
    ; BX에 저장된 파일 핸들을 다시 불러온다.
    ; 여기서 [cs:FileHandle] 처럼 CS 세그먼트 지정자를 사용한 이유는
    ; 현재 DS를 ES로 바꿔놨기 때문이다.
    ;
    ; 그냥 [FileHandle] 라고 쓰면 현재 DS 기준으로 잘못 읽을 수 있다.
    ; --------------------------------------------------------

    mov ah, 3Fh
    ; --------------------------------------------------------
    ; DOS int 21h 파일 읽기 기능
    ;
    ; 입력:
    ;   BX    = 파일 핸들
    ;   CX    = 읽을 바이트 수
    ;   DS:DX = 목적지 버퍼
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 읽기 수행
    ;
    ; 성공하면:
    ;   AX = 실제로 읽은 바이트 수
    ;
    ; 실패하면:
    ;   CF = 1
    ; --------------------------------------------------------

    jc .read_fail
    ; --------------------------------------------------------
    ; 읽기 실패 시 DS를 복구한 뒤 실패 처리한다.
    ; --------------------------------------------------------

    mov ds, si
    ; --------------------------------------------------------
    ; DS를 원래 프로그램 세그먼트로 복구한다.
    ; --------------------------------------------------------

    cmp ax, cx
    ; --------------------------------------------------------
    ; 실제 읽은 바이트 수가 요청한 바이트 수와 같은지 확인
    ; --------------------------------------------------------

    jne .close_fail
    ; --------------------------------------------------------
    ; 정확히 그만큼 읽지 못했다면 실패 처리
    ; 파일 크기가 부족하거나 이상한 경우일 수 있다.
    ; --------------------------------------------------------


    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 닫을 파일 핸들을 BX에 넣는다.
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; DOS int 21h 파일 닫기 기능
    ;
    ; BX = 파일 핸들
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 수행
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 닫기 실패면 실패 처리
    ; --------------------------------------------------------


    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 저장해두었던 레지스터들을 복구한다.
    ; --------------------------------------------------------

    clc
    ; --------------------------------------------------------
    ; Carry Flag를 클리어해서 성공을 표시한다.
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; 호출자에게 복귀
    ; --------------------------------------------------------


.read_fail:
; ------------------------------------------------------------
; 읽기 도중 실패했을 때의 처리
; DS를 먼저 원래 값으로 복구해야 한다.
; ------------------------------------------------------------

    mov ds, si
    ; --------------------------------------------------------
    ; DS 복구
    ; --------------------------------------------------------


.close_fail:
; ------------------------------------------------------------
; 파일은 열렸으니 닫고 실패로 나간다.
; ------------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 닫을 파일 핸들
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; 파일 닫기 기능
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 시도
    ; --------------------------------------------------------


.fail:
; ------------------------------------------------------------
; 공통 실패 경로
; ------------------------------------------------------------

    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 저장한 레지스터 복구
    ; --------------------------------------------------------

    stc
    ; --------------------------------------------------------
    ; Carry Flag를 세트해서 실패를 표시한다.
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; 호출자에게 복귀
    ; --------------------------------------------------------



; ============================================================
; ApplyPalette
;
; 목적:
;   PaletteBuffer(768 bytes) 내용을 VGA DAC에 적용한다.
;
; VGA 포트:
;   03C8h = 시작 색 인덱스 지정
;   03C9h = R, G, B 값 순서대로 쓰는 포트
;
; 주의:
;   파이썬에서 저장한 팔레트는 보통 RGB가 0~255 범위이다.
;   VGA DAC는 각 색 성분을 0~63 범위로 받는다.
;   따라서 각 바이트를 2비트 오른쪽 시프트해서 4로 나눠야 한다.
; ============================================================

ApplyPalette:
; ------------------------------------------------------------
; PaletteBuffer -> VGA DAC
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push si
    ; --------------------------------------------------------
    ; 내부에서 사용할 레지스터 보존
    ; --------------------------------------------------------


    mov dx, 03C8h
    ; --------------------------------------------------------
    ; VGA DAC 시작 색 인덱스 지정 포트
    ; --------------------------------------------------------

    xor al, al
    ; --------------------------------------------------------
    ; AL = 0
    ; 즉 0번 색부터 쓰겠다는 뜻
    ; --------------------------------------------------------

    out dx, al
    ; --------------------------------------------------------
    ; 시작 색 인덱스 0 지정
    ; --------------------------------------------------------


    mov dx, 03C9h
    ; --------------------------------------------------------
    ; VGA DAC 데이터 포트
    ; 이후 여기로 R, G, B 값을 순서대로 보낸다.
    ; --------------------------------------------------------

    lea si, [PaletteBuffer]
    ; --------------------------------------------------------
    ; SI가 PaletteBuffer 시작 주소를 가리키게 한다.
    ; --------------------------------------------------------

    mov cx, 768
    ; --------------------------------------------------------
    ; 총 768바이트를 출력해야 한다.
    ; 256색 * RGB 3바이트
    ; --------------------------------------------------------


.next_byte:
; ------------------------------------------------------------
; 팔레트 한 바이트씩 처리하는 루프
; ------------------------------------------------------------

    lodsb
    ; --------------------------------------------------------
    ; DS:SI 가 가리키는 1바이트를 AL로 읽고
    ; SI를 1 증가시킨다.
    ;
    ; 즉 PaletteBuffer에서 다음 바이트를 꺼내온다.
    ; --------------------------------------------------------

    shr al, 1
    ; --------------------------------------------------------
    ; AL을 1비트 오른쪽 시프트
    ; --------------------------------------------------------

    shr al, 1
    ; --------------------------------------------------------
    ; 한 번 더 시프트해서 총 2비트 시프트
    ; 즉 AL = AL / 4 와 비슷한 효과
    ;
    ; 0~255 범위를 0~63 범위로 줄인다.
    ; --------------------------------------------------------

    out dx, al
    ; --------------------------------------------------------
    ; 줄인 값을 VGA DAC 데이터 포트로 출력
    ; --------------------------------------------------------

    loop .next_byte
    ; --------------------------------------------------------
    ; CX를 감소시키고 0이 아니면 반복
    ; 총 768번 반복
    ; --------------------------------------------------------


    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 보존했던 레지스터 복구
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; 호출자에게 복귀
    ; --------------------------------------------------------



; ============================================================
; LoadImageToVRAM
;
; 목적:
;   HELLO.RAW 파일을 열고 4096바이트씩 읽어서
;   VGA 화면 메모리 A000:0000 에 복사한다.
;
; 출력:
;   CF=0 성공
;   CF=1 실패
;
; 전략:
;   - 파일을 연다
;   - BytesLeft = 64000 으로 시작
;   - 매번 4096바이트 또는 남은 바이트만큼 읽는다
;   - ReadBuffer -> ES:DI(VRAM) 으로 복사한다
;   - 64000바이트를 다 채울 때까지 반복한다
; ============================================================

LoadImageToVRAM:
; ------------------------------------------------------------
; HELLO.RAW -> VRAM
; ------------------------------------------------------------

    push ax
    push bx
    push cx
    push dx
    push si
    push di
    push ds
    push es
    ; --------------------------------------------------------
    ; 내부에서 많이 건드릴 레지스터와 세그먼트를 보존한다.
    ; --------------------------------------------------------


    lea dx, [ImageFileName]
    ; --------------------------------------------------------
    ; DX에 HELLO.RAW 파일명 문자열 주소를 넣는다.
    ; DS는 현재 프로그램 세그먼트라고 가정한다.
    ; --------------------------------------------------------

    mov ax, 3D00h
    ; --------------------------------------------------------
    ; DOS 파일 열기 기능
    ; AH=3Dh, AL=00h = 읽기 전용 열기
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 열기 수행
    ; 성공 시 AX = 파일 핸들
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 파일 열기 실패 시 실패 처리
    ; --------------------------------------------------------

    mov [FileHandle], ax
    ; --------------------------------------------------------
    ; 파일 핸들을 저장해둔다.
    ; --------------------------------------------------------


    mov ax, 0A000h
    ; --------------------------------------------------------
    ; VGA Mode 13h 화면 메모리 세그먼트
    ; --------------------------------------------------------

    mov es, ax
    ; --------------------------------------------------------
    ; ES = A000h
    ; 즉 ES:DI 를 화면 메모리 주소로 사용할 준비
    ; --------------------------------------------------------

    xor di, di
    ; --------------------------------------------------------
    ; DI = 0
    ; 즉 화면 메모리 시작 위치 A000:0000 부터 채운다.
    ; --------------------------------------------------------


    mov word [BytesLeft], 64000
    ; --------------------------------------------------------
    ; 아직 채워야 할 총 바이트 수를 64000으로 설정
    ; 320 * 200 = 64000
    ; --------------------------------------------------------



.read_loop:
; ------------------------------------------------------------
; 파일을 조금씩 읽어서 VRAM으로 보내는 반복 루프
; ------------------------------------------------------------

    cmp word [BytesLeft], 0
    ; --------------------------------------------------------
    ; 남은 바이트가 0인지 확인
    ; --------------------------------------------------------

    je .success_close
    ; --------------------------------------------------------
    ; 더 이상 읽을 것이 없으면 성공적으로 종료
    ; --------------------------------------------------------


    mov ax, [BytesLeft]
    ; --------------------------------------------------------
    ; 남은 바이트 수를 AX에 가져온다.
    ; --------------------------------------------------------

    cmp ax, 4096
    ; --------------------------------------------------------
    ; 남은 양이 4096보다 큰지 비교
    ; --------------------------------------------------------

    jbe .use_rest
    ; --------------------------------------------------------
    ; 4096 이하라면 남은 만큼만 읽어야 하므로 .use_rest 로 감
    ; --------------------------------------------------------

    mov ax, 4096
    ; --------------------------------------------------------
    ; 남은 양이 더 많으면 이번에는 4096바이트를 읽는다.
    ; --------------------------------------------------------


.use_rest:
; ------------------------------------------------------------
; 이번에 읽을 실제 크기를 정하는 지점
; ------------------------------------------------------------

    mov [ChunkSize], ax
    ; --------------------------------------------------------
    ; 이번에 읽을 크기를 ChunkSize에 저장
    ; --------------------------------------------------------


    push cs
    pop ds
    ; --------------------------------------------------------
    ; DS를 프로그램 세그먼트로 맞춘다.
    ; ReadBuffer는 프로그램 데이터 영역에 있으므로
    ; DS가 여기로 맞아 있어야 한다.
    ; --------------------------------------------------------

    lea dx, [ReadBuffer]
    ; --------------------------------------------------------
    ; 파일 읽기 버퍼 주소를 DX에 넣는다.
    ; 즉 DS:DX = ReadBuffer
    ; --------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 읽을 파일 핸들을 BX에 넣는다.
    ; --------------------------------------------------------

    mov cx, [ChunkSize]
    ; --------------------------------------------------------
    ; 이번에 읽을 바이트 수를 CX에 넣는다.
    ; --------------------------------------------------------

    mov ah, 3Fh
    ; --------------------------------------------------------
    ; DOS 파일 읽기 기능
    ;
    ; 입력:
    ;   BX    = 파일 핸들
    ;   CX    = 읽을 크기
    ;   DS:DX = 버퍼
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; ReadBuffer 로 파일을 읽는다.
    ;
    ; 성공 시 AX = 실제 읽은 바이트 수
    ; --------------------------------------------------------

    jc .close_fail
    ; --------------------------------------------------------
    ; 읽기 자체가 실패하면 파일을 닫고 실패 처리
    ; --------------------------------------------------------

    cmp ax, [ChunkSize]
    ; --------------------------------------------------------
    ; 실제 읽은 바이트 수가 기대한 크기와 같은지 검사
    ; --------------------------------------------------------

    jne .close_fail
    ; --------------------------------------------------------
    ; 다르면 파일 크기 이상 또는 읽기 이상으로 간주
    ; --------------------------------------------------------


    push cs
    pop ds
    ; --------------------------------------------------------
    ; rep movsb 에서 원본은 DS:SI 를 사용한다.
    ; ReadBuffer는 프로그램 세그먼트에 있으므로
    ; 다시 DS를 프로그램 세그먼트로 맞춘다.
    ; --------------------------------------------------------

    lea si, [ReadBuffer]
    ; --------------------------------------------------------
    ; SI = ReadBuffer 시작 주소
    ; --------------------------------------------------------

    mov cx, [ChunkSize]
    ; --------------------------------------------------------
    ; 이번에 복사할 바이트 수
    ; --------------------------------------------------------

    rep movsb
    ; --------------------------------------------------------
    ; DS:SI -> ES:DI 로 CX 바이트 복사
    ;
    ; 여기서:
    ;   DS:SI = ReadBuffer
    ;   ES:DI = A000:화면위치
    ;
    ; 따라서 읽은 데이터를 화면 메모리로 그대로 복사한다.
    ;
    ; 그리고 rep movsb 는 자동으로:
    ;   SI 증가
    ;   DI 증가
    ;   CX 감소
    ; 를 반복한다.
    ; --------------------------------------------------------


    mov ax, [BytesLeft]
    ; --------------------------------------------------------
    ; 남은 바이트 수를 AX에 가져온다.
    ; --------------------------------------------------------

    sub ax, [ChunkSize]
    ; --------------------------------------------------------
    ; 이번에 읽은 만큼 빼준다.
    ; --------------------------------------------------------

    mov [BytesLeft], ax
    ; --------------------------------------------------------
    ; 줄어든 남은 바이트 수를 다시 저장
    ; --------------------------------------------------------

    jmp .read_loop
    ; --------------------------------------------------------
    ; 다음 조각을 읽으러 반복
    ; --------------------------------------------------------



.success_close:
; ------------------------------------------------------------
; 모든 64000바이트를 정상적으로 읽고 복사한 경우
; 파일을 닫고 성공으로 복귀한다.
; ------------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 닫을 파일 핸들
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; DOS 파일 닫기 기능
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 수행
    ; --------------------------------------------------------

    jc .fail
    ; --------------------------------------------------------
    ; 닫기 실패 시 실패 처리
    ; --------------------------------------------------------


    pop es
    pop ds
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 저장했던 레지스터/세그먼트 복구
    ; --------------------------------------------------------

    clc
    ; --------------------------------------------------------
    ; Carry Flag 클리어 = 성공
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; 호출자에게 복귀
    ; --------------------------------------------------------



.close_fail:
; ------------------------------------------------------------
; 파일은 열려 있으므로 먼저 닫고 실패로 나간다.
; ------------------------------------------------------------

    mov bx, [FileHandle]
    ; --------------------------------------------------------
    ; 닫을 파일 핸들
    ; --------------------------------------------------------

    mov ah, 3Eh
    ; --------------------------------------------------------
    ; DOS 파일 닫기 기능
    ; --------------------------------------------------------

    int 21h
    ; --------------------------------------------------------
    ; 파일 닫기 시도
    ; --------------------------------------------------------


.fail:
; ------------------------------------------------------------
; 공통 실패 경로
; ------------------------------------------------------------

    pop es
    pop ds
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ; --------------------------------------------------------
    ; 저장한 레지스터/세그먼트 복구
    ; --------------------------------------------------------

    stc
    ; --------------------------------------------------------
    ; Carry Flag 세트 = 실패
    ; --------------------------------------------------------

    ret
    ; --------------------------------------------------------
    ; 호출자에게 복귀
    ; --------------------------------------------------------



